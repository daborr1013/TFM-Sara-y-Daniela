function extractInsertStatements(sql) {
  const statements = [];
  let index = 0;

  while (index < sql.length) {
    const start = sql.indexOf('INSERT INTO', index);
    if (start === -1) break;

    let cursor = start;
    let inString = false;
    let escaped = false;

    while (cursor < sql.length) {
      const char = sql[cursor];

      if (inString) {
        if (escaped) {
          escaped = false;
        } else if (char === '\\') {
          escaped = true;
        } else if (char === "'") {
          inString = false;
        }
      } else if (char === "'") {
        inString = true;
      } else if (char === ';') {
        statements.push(sql.slice(start, cursor + 1));
        cursor += 1;
        break;
      }

      cursor += 1;
    }

    index = cursor;
  }

  return statements;
}

function convertInsert(statement) {
  const match = statement.match(/^INSERT INTO `([^`]+)` \(([^]+?)\) VALUES\s*([\s\S]*);$/);

  if (!match) {
    throw new Error(`Cannot parse INSERT statement: ${statement.slice(0, 120)}`);
  }

  const [, tableName, rawColumns, rawValues] = match;
  const columns = [...rawColumns.matchAll(/`([^`]+)`/g)].map(([, column]) => column);
  const rows = parseRows(rawValues, tableName);

  return [
    `INSERT INTO ${quoteIdentifier(tableName)} (${columns.map(quoteIdentifier).join(', ')}) VALUES`,
    rows.map((row) => `(${row.map(formatValue).join(', ')})`).join(',\n'),
    ';',
  ].join('\n');
}

function parseRows(rawValues, tableName) {
  const rows = [];
  let index = 0;

  while (index < rawValues.length) {
    index = skipSeparators(rawValues, index);

    if (index >= rawValues.length) break;

    if (rawValues[index] !== '(') {
      throw new Error(`Expected tuple start in ${tableName} at ${index}`);
    }

    index += 1;
    const row = [];

    while (index < rawValues.length) {
      index = skipWhitespace(rawValues, index);
      const parsed = parseValue(rawValues, index, tableName);
      row.push(parsed.value);
      index = skipWhitespace(rawValues, parsed.nextIndex);

      if (rawValues[index] === ',') {
        index += 1;
        continue;
      }

      if (rawValues[index] === ')') {
        index += 1;
        break;
      }

      throw new Error(`Expected tuple separator in ${tableName} at ${index}`);
    }

    rows.push(row);
  }

  return rows;
}

function parseValue(input, index, tableName) {
  const char = input[index];

  if (char === "'") {
    return parseStringValue(input, index, tableName);
  }

  const nullable = input.slice(index, index + 4).toUpperCase();
  if (nullable === 'NULL') {
    return { value: null, nextIndex: index + 4 };
  }

  let cursor = index;
  while (cursor < input.length && input[cursor] !== ',' && input[cursor] !== ')') {
    cursor += 1;
  }

  const raw = input.slice(index, cursor).trim();
  if (!raw) {
    throw new Error(`Empty value in ${tableName} at ${index}`);
  }

  return { value: { raw }, nextIndex: cursor };
}

function parseStringValue(input, index, tableName) {
  let cursor = index + 1;
  let value = '';

  while (cursor < input.length) {
    const char = input[cursor];

    if (char === '\\') {
      const next = input[cursor + 1];
      value += decodeMysqlEscape(next);
      cursor += 2;
      continue;
    }

    if (char === "'") {
      if (input[cursor + 1] === "'") {
        value += "'";
        cursor += 2;
        continue;
      }

      return { value, nextIndex: cursor + 1 };
    }

    value += char;
    cursor += 1;
  }

  throw new Error(`Unterminated string in ${tableName} at ${index}`);
}

function decodeMysqlEscape(char) {
  const escapes = {
    0: '\0',
    b: '\b',
    n: '\n',
    r: '\r',
    t: '\t',
    Z: '\x1A',
    '\\': '\\',
    "'": "'",
    '"': '"',
  };

  return Object.prototype.hasOwnProperty.call(escapes, char) ? escapes[char] : char;
}

function skipSeparators(input, index) {
  let cursor = skipWhitespace(input, index);

  if (input[cursor] === ',') {
    cursor += 1;
  }

  return skipWhitespace(input, cursor);
}

function skipWhitespace(input, index) {
  let cursor = index;

  while (cursor < input.length && /\s/.test(input[cursor])) {
    cursor += 1;
  }

  return cursor;
}

function quoteIdentifier(identifier) {
  return `"${identifier.replace(/"/g, '""')}"`;
}

function formatValue(value) {
  if (value === null) {
    return 'NULL';
  }

  if (typeof value === 'object' && value.raw) {
    return value.raw;
  }

  return `'${String(value).replace(/'/g, "''")}'`;
}

