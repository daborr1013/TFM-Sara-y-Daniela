            <h3>Marco histórico. Un siglo de cambios.</h3>
            <p>Para entender a Jane, es esencial situarla en la línea del tiempo de la monarquía británica:</p>
            <ul>
                <li><b>Regencia (1811-1820)</b>: La infancia de las Brontë ocurre bajo la influencia de este periodo pre-victoriano, marcado por las 
                    Guerras Napoleónicas y la tensión entre la razón y la pasión.</li>
                <li><b>Era Victoriana (1837-1901)</b>:El contexto real de la novela. Una época de responsabilidad moral, expansión del Imperio y una fe 
                que empieza a tambalearse por descubrimientos científicos (como la geología de Lyell o la evolución de Darwin).</li>
                <li><b>La Gran Exposición (1851)</b>:El clímax del orgullo británico, mostrando la riqueza y los inventos (telégrafo, fotografía) que cambiaron 
                el mundo mientras Charlotte escribía.</li>
            </ul>

            <h3>Romanticismo vs. Ilustración</h3>
            <p>La novela es un campo de batalla entre dos formas de ver el mundo:</p>
            
            <table>
                <tr>
                    <th>Romanticismo<br><i>El corazón</i></th>
                    <th>Ilustración<br><i>La razón</i></th>
                </tr>

                <tr>
                    <td>El corazón controla la cabeza.</td>
                    <td>La cabeza controla el corazón.</td>
                </tr>

                <tr>
                    <td>La razón y el intelecto pueden ser peligrosos.</td>
                    <td>La imaginación y los sentimientos son peligrosos.</td>
                </tr>

                <tr>
                    <td>Jane Eyre se deja llevar por su pasión y sus visiones.</td>
                    <td>John Rivers representa el control y el deber frío.</td>
                </tr>
            </table>
            <br>

            <p><b>Concepto clave:</b> Jane es una anti-heroína romántica. No es la clásica heroína perfecta y bella; es "pobre, oscura, fea y pequeña", pero con una voluntad de hierro.</p>
            <br>
            
            <h3>Las Hermanas Brontë: Realidad tras la Ficción</h3>
            <p>La vida de Charlotte, Emily y Anne es tan fascinante como sus libros. Podemos ver sus experiencias reflejadas en sus historias, como el paisaje rudo y sombrío de los 
                páramos de Yorkshire que se refleja en la atmósfera de la novela, o el trabajo que hicieron de institutrices y maestras.</p>
            
            <p>A pesar de la tragedia marcar sus vidas, con la muerte de su madre y sus hermanas, esta les dio una capacidad narrativa única y las Brontë se hicieron escritoras, creando
                mundos imaginarios que marcaron la literatura y les dio fama mundial.</p>

            <p>Las tres tuvieron que usar pseudónimos para poder publicar en un inicio: Currer, Ellis y Acton Bell fueron su táctica para evitar los prejuicios que había contra las escritoras.</p>

            <h3>El Legado: ¿Por qué seguimos leyendo Jane Eyre?</h3>
            <p>El enfoque de Charlotte en la consciencia interior y los sentimientos de Jane fue precursor de la "corriente de conciencia" del siglo XX. Jane no solo narra lo que pasa, sino cómo 
                se siente su mente.</p>
            <p>Hoy entendemos <i>Jane Eyre</i> a través de la intertextualidad. Un ejemplo es <i>Wide Sargasso Sea</i> de Jean Rhys, que reescribe la historia desde el punto de vista de Bertha Mason (la mujer en el ático), 
                cuestionando el canon original.</p>
            <p>La novela ha sido adaptada innumerables veces al cine, la televisión y el teatro, demostrando su vigencia y capacidad para conectar con nuevas generaciones.</p>

            <p><i>Jane Eyre</i> sigue siendo un referente feminista y un clásico de la literatura universal que nos invita a reflexionar sobre la libertad, la justicia y el amor verdadero.</p>
