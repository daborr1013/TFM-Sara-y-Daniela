            <section class="pjustificado">
                <h1 class="h1personajes">Bertha Mason</h1>

                <img class="fpersonaje" src="../../../../../media/images/bertha.png">

                <p>Bertha es la esposa secreta de Edward Rochester. Es una mujer de la familia Mason, de la isla de Jamaica.</p>

                <p>Debido a una combinación de herencia genética (su madre también padecía enfermedades mentales) y una vida de excesos, Bertha perdió la razón y ha estado encerrada 
                    en el tercer piso de Thornfield durante años.</p>

                <p>Durante gran parte del libro es solo un sonido, una risa o una sombra, hasta que se revela como el gran obstáculo para la felicidad de Jane.</p>

                <h3>Personalidad e impacto en la vida de Jane</h3>
                <p>No conocemos la personalidad real de Bertha antes de su enfermedad, solo a través de la versión subjetiva de Rochester, quien la describe como una mujer hermosa pero grosera y violenta.</p>
                    
                <p>Es descrita con términos animales: gruñe, camina a cuatro patas y tiene una fuerza física sobrehumana. Representa la pasión pura, la rabia y la sexualidad reprimida que la sociedad victoriana tanto temía.</p>

                <p>Es una amenaza constante. Aunque Jane no sabe quién es, sus actos crean un clima de terror.</p>

                <p>Su existencia es lo que convierte la boda de Jane en un acto de bigamia ilegal, obligando a Jane a huir de Thornfield y a abandonar al hombre que ama para no perder su integridad moral.</p>

                <p>Al quemar Thornfield y suicidarse saltando desde el tejado, Bertha "libera" a Rochester (aunque lo deja lisiado y ciego), permitiendo que Jane regrese y pueda casarse con él legalmente.</p>
                
                <h3>Importancia del personaje</h3>
                <p>Bertha es el doble oscuro de Jane. Representa lo que le ocurre a una mujer que no puede controlar sus pasiones o que es oprimida por un matrimonio sin amor. Mientras Jane es pequeña, pálida y se controla 
                    a sí misma, Bertha es grande, oscura y salvaje.</p>

                <p>La autora la usa para mostrar los secretos oscuros del colonialismo y las consecuencias de los matrimonios por conveniencia.</p>
                
                <p>Es la encarnación del secreto. Representa todo lo que Rochester ha intentado ocultar de su pasado. Su risa "demoníaca" es el recordatorio constante de que el pasado nunca muere del todo y siempre vuelve para reclamar su lugar.</p>
