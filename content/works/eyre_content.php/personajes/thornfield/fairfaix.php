                <h1 class="h1personajes">Señora Fairfax</h1>

                <img class="fpersonaje" src="../../../../../media/images/fairfaix.png">

                <p>Es el primer rostro amable que recibe a Jane en Thornfield Hall. Su presencia es fundamental para
                    rebajar la tensión gótica de la mansión y ofrecerle a Jane,
                    por primera vez, la sensación de estar en un "hogar" y no en una institución o una casa enemiga.</p>

                <p>Es la ama de llaves de Thornfield. Aunque Jane inicialmente piensa que es la dueña de la casa, la
                    Sra. Fairfax se apresura a aclarar que es solo una empleada
                    (aunque con un parentesco lejano con los Rochester).</p>

                <h3>Personalidad e impacto en la vida de Jane</h3>
                <p>La señora Fairfax es una mujer sencilla, sin grandes pretensiones intelectuales, pero con un corazón
                    enorme. Representa el orden, la tradición y la decencia.
                    Cree firmemente en las jerarquías sociales y en que cada uno debe conocer su lugar.</p>

                <p>Con su recibimiento, consigue que Jane pueda bajar la guardia y empiece una nueva vida con esperanza.
                </p>

                <p>Es el personaje que expresa la preocupación más lógica sobre la relación entre Jane y Rochester.
                    Advierte a Jane que los hombres de la clase de Rochester no suelen
                    casarse con sus institutrices y le pide que sea cautelosa. Aunque a Jane le molesta este consejo, el
                    tiempo demuestra que la señora tenía razón en que la situación era peligrosa.</p>

                <p>Su ignorancia sobre lo que ocurre en el ático resalta que el secreto de Rochester está oculto incluso
                    para los más cercanos. A pesar de oír
                    los ruidos extraños en la noche, decide creer que es "solo Grace Poole con sus excentricidades".</p>

                <h3>Importancia del personaje</h3>
                <p>Con la señora Fairfax se muestra el lado amable de la servidumbre y la estructura social inglesa.</p>

                <p>Sin ella, Thornfield sería un lugar puramente de pesadilla; ella lo mantiene funcionando como una
                    casa real.</p>
