                <h1>Capítulo V</h1>
                
                <p>Apenas habían dado las cinco de la mańana del 19 de enero, cuando
                Bessie trajo una vela a mi cuartito y me encontró ya levantada y casi
                vestida. Me había levantado media hora antes de su entrada, y me
                había lavado la cara y puesto la ropa a la luz de una media luna que
                estaba a punto de ponerse, cuyos rayos se colaban por la estrecha
                ventana cerca de mi cuna. Aquel día debía dejar Gateshead en una
                diligencia que pasaba por las puertas de la finca a las seis de la
                mańana. Bessie era la única persona levantada; había encendido un
                fuego en el cuarto de los nińos, donde ahora procedió a prepararme
                el desayuno. Pocos nińos pueden comer cuando están excitados con
                los pensamientos de un viaje; yo tampoco podía. Bessie, tras
                instarme en vano a tomar unas cucharadas de la leche hervida con
                pan que me había preparado, envolvió unas galletas en un papel y
                las metió en mi bolsa; luego me ayudó a ponerme la pelliza y el
                gorro y, envolviéndose ella en un chal, salimos del cuarto de los
                nińos. Al pasar por el dormitorio de la seńora Reed, dijo:</p>

                <p>—żQuiere entrar a despedirse de la seńora?</p>

                <p>—No, Bessie. Anoche vino a mi cuna, cuando habías bajado a
                cenar, y dijo que no era necesario que la molestara por la mańana,
                ni a mis primos tampoco; y me dijo que recordara que ella siempre
                había sido mi mejor amiga, y que hablara de ella y le estuviera
                agradecida en consecuencia.</p>

                <p>—żQué le dijo usted, seńorita?</p>

                <p>—Nada. Me cubrí la cara con las sábanas y me volví hacia la
                pared.</p>

                <p>—Eso estuvo mal, seńorita Jane.</p>

                <p>—Estuvo muy bien, Bessie. Su seńora no ha sido mi amiga, ha
                sido mi enemiga.</p>

                <p>—ˇOh, seńorita Jane! ˇNo diga eso!</p>

                <p>—ˇAdiós a Gateshead! —grité, mientras cruzábamos el vestíbulo y
                salíamos por la puerta principal.</p>

                <p>La luna se había puesto y estaba muy oscuro; Bessie llevaba un
                farol, cuya luz se reflejaba en los escalones mojados y en el camino
                de grava empapado por un deshielo reciente. La mańana de invierno
                era cruda y fría; mis dientes castańeteaban mientras bajaba
                apresuradamente por el camino. Había una luz en la garita del
                portero; cuando llegamos, encontramos a la mujer del portero
                encendiendo el fuego. Mi baúl, que había sido bajado la noche
                anterior, estaba atado con cuerdas junto a la puerta. Faltaban pocos
                minutos para las seis, y poco después de que diera la hora, el lejano
                rodar de ruedas anunció la llegada de la diligencia. Fui a la puerta y
                observé sus faroles acercarse rápidamente a través de la penumbra.</p>

                <p>—żVa sola? —preguntó la mujer del portero.</p>

                <p>—Sí.</p>

                <p>—żY a qué distancia está?</p>

                <p>—Cincuenta millas.</p>

                <p>—ˇQué camino tan largo! Me extrańa que la seńora Reed no tema
                confiarla tan lejos sola.</p>

                <p>La diligencia se detuvo; allí estaba, en las puertas, con sus cuatro
                caballos y su techo cargado de pasajeros. El guarda y el cochero
                apremiaban a voces. Subieron mi baúl; me apartaron del cuello de
                Bessie, al que me aferraba a besos.</p>

                <p>—Asegúrese de cuidarla bien —le gritó ella al guarda, mientras me
                subía al interior.</p>

                <p>—ˇClaro, claro! —fue la respuesta. La portezuela se cerró de un
                portazo, una voz exclamó «ˇTodo en orden!», y partimos. Así fui
                separada de Bessie y de Gateshead; así fui arrastrada a regiones
                desconocidas y, como entonces las consideraba, remotas y
                misteriosas.</p>

                <p>Recuerdo poco del viaje; solo sé que el día me pareció de una
                longitud sobrenatural, y que me pareció que recorríamos cientos de
                millas de camino. Pasamos por varias ciudades, y en una, muy
                grande, la diligencia se detuvo; desengancharon los caballos y los
                pasajeros se apearon para comer. Me llevaron a una posada, donde
                el guarda quiso que comiera algo; pero, como no tenía apetito, me
                dejó en una inmensa sala con una chimenea en cada extremo, un
                candelabro colgando del techo y una pequeńa galería roja en lo alto
                de la pared, llena de instrumentos musicales. Aquí paseé durante
                mucho tiempo, sintiéndome muy extrańa y mortalmente aprensiva
                de que alguien entrara y me secuestrara; pues creía en los
                secuestradores, cuyas hazańas habían figurado con frecuencia en las
                crónicas de Bessie junto al fuego. Por fin, el guarda regresó; una vez
                más me acomodaron en la diligencia, mi protector subió a su propio
                asiento, hizo sonar su corneta hueca y partimos traqueteando por la
                «calle empedrada» de L...</p>

                <p>La tarde llegó húmeda y algo neblinosa. A medida que se
                convertía en crepúsculo, empecé a sentir que nos alejábamos mucho
                de Gateshead. Dejamos de pasar por ciudades; el paisaje cambió;
                grandes colinas grises se alzaban en el horizonte. A medida que el
                crepúsculo se profundizaba, descendimos a un valle, oscuro de
                bosques, y mucho después de que la noche hubiera nublado la vista,
                oí un viento salvaje que se precipitaba entre los árboles.</p>

                <p>Arrullada por el sonido, finalmente me quedé dormida. No había
                dormitado mucho cuando el cese repentino del movimiento me
                despertó. La portezuela de la diligencia estaba abierta, y una
                persona con aspecto de criada estaba de pie junto a ella. Vi su
                rostro y su vestido a la luz de los faroles.</p>

                <p>—żHay aquí una nińa llamada Jane Eyre? —preguntó.</p>

                <p>Respondí «Sí», y entonces me bajaron. Descargaron mi baúl y la
                diligencia se marchó al instante.</p>

                <p>Estaba entumecida de tanto estar sentada y aturdida por el ruido
                y el movimiento de la diligencia. Recobrando mis facultades, miré a
                mi alrededor. Lluvia, viento y oscuridad llenaban el aire; sin
                embargo, distinguí vagamente un muro ante mí y una puerta abierta
                en él. A través de esta puerta pasé con mi nueva guía; ella la cerró y
                echó la llave tras de sí. Ahora se veía una casa o casas —pues el
                edificio se extendía a lo lejos— con muchas ventanas, y luces
                encendidas en algunas. Subimos por un ancho sendero de guijarros,
                chapoteando en el agua, y nos admitieron en una puerta; luego, la
                criada me condujo por un pasillo hasta una habitación con fuego,
                donde me dejó sola.</p>

                <p>Me quedé de pie y calenté mis dedos entumecidos sobre las
                llamas, luego miré a mi alrededor; no había vela, pero la luz incierta
                del hogar mostraba, a intervalos, paredes empapeladas, alfombra,
                cortinas, muebles de caoba reluciente. Era un salón, no tan
                espacioso ni espléndido como el de Gateshead, pero bastante
                cómodo. Estaba tratando de descifrar el tema de un cuadro en la
                pared, cuando la puerta se abrió y entró un individuo que llevaba
                una luz; otro le seguía de cerca.</p>

                <p>La primera era una dama alta, de pelo oscuro, ojos oscuros y una
                frente pálida y ancha; su figura estaba parcialmente envuelta en un
                chal, su semblante era grave, su porte erguido.</p>

                <p>—La nińa es muy joven para ser enviada sola —dijo, dejando su
                vela sobre la mesa. Me consideró atentamente durante un minuto o
                dos, y luego ańadió:</p>

                <p>—Será mejor que la acuesten pronto; parece cansada. żEstás
                cansada? —preguntó, poniendo su mano en mi hombro.</p>

                <p>—Un poco, seńora.</p>

                <p>—Y hambrienta también, sin duda. Que cene algo antes de
                acostarse, seńorita Miller. żEs la primera vez que dejas a tus padres
                para venir a la escuela, mi pequeńa?</p>

                <p>Le expliqué que no tenía padres. Me preguntó cuánto tiempo
                llevaban muertos; luego qué edad tenía, cuál era mi nombre, si
                sabía leer, escribir y coser un poco. Entonces me tocó la mejilla
                suavemente con el dedo índice y, diciendo: «Espero que seas una
                buena nińa», me despidió junto con la seńorita Miller.</p>

                <p>La dama que había dejado atrás podría tener unos veintinueve
                ańos; la que iba conmigo parecía algunos ańos más joven. La
                primera me impresionó por su voz, su mirada y su aire. La seńorita
                Miller era más corriente; de tez sonrosada, aunque de semblante
                preocupado; de andar y acción apresurados, como quien siempre
                tiene una multiplicidad de tareas entre manos. Parecía, en efecto, lo
                que después descubrí que era realmente, una submaestra. Guiada
                por ella, pasé de compartimento en compartimento, de pasillo en
                pasillo, de un edificio grande e irregular; hasta que, emergiendo del
                silencio total y algo lúgubre que impregnaba esa parte de la casa
                que habíamos atravesado, nos encontramos con el murmullo de
                muchas voces, y al instante entramos en una sala ancha y larga, con
                grandes mesas de pino, dos en cada extremo, sobre cada una de las
                cuales ardían un par de velas, y sentadas alrededor en bancos, una
                congregación de nińas de todas las edades, desde los nueve o diez
                hasta los veinte ańos. Vistas a la tenue luz de las velas de sebo, su
                número me pareció incontable, aunque en realidad no excedía de
                ochenta; iban vestidas uniformemente con vestidos de estameńa
                marrón de hechura anticuada y largos delantales de tela de Holanda.
                Era la hora de estudio; estaban ocupadas repasando la lección del
                día siguiente, y el murmullo que había oído era el resultado
                combinado de sus susurradas repeticiones.</p>

                <p>La seńorita Miller me hizo seńas para que me sentara en un banco
                cerca de la puerta, luego caminó hasta el final de la larga sala y
                gritó:</p>

                <p>—ˇMonitoras, recoged los libros de lecciones y guardadlos!</p>

                <p>Cuatro chicas altas se levantaron de diferentes mesas y, dando la
                vuelta, recogieron los libros y los retiraron. La seńorita Miller volvió a
                dar la orden:</p>

                <p>—ˇMonitoras, traed las bandejas de la cena!</p>

                <p>Las chicas altas salieron y regresaron al momento, cada una con
                una bandeja, con porciones de algo, no sabía qué, dispuestas sobre
                ella, y una jarra de agua y una taza en el centro de cada bandeja.
                Se repartieron las porciones; las que quisieron tomaron un trago de
                agua, siendo la taza común para todas. Cuando me llegó el turno,
                bebí, porque tenía sed, pero no toqué la comida; la excitación y la
                fatiga me incapacitaban para comer. Ahora vi, sin embargo, que era
                una fina torta de avena partida en trozos.</p>

                <p>Terminada la comida, la seńorita Miller leyó las oraciones, y las
                clases salieron en fila, de dos en dos, escaleras arriba. Abrumada a
                esas alturas por el cansancio, apenas me di cuenta de qué tipo de
                lugar era el dormitorio, excepto que, como el aula, vi que era muy
                largo. Esta noche iba a ser compańera de cama de la seńorita Miller;
                me ayudó a desvestirme. Una vez acostada, eché un vistazo a las
                largas hileras de camas, cada una de las cuales se llenó rápidamente
                con dos ocupantes; en diez minutos se apagó la única luz, y en
                medio del silencio y la completa oscuridad me quedé dormida.</p>

                <p>La noche pasó rápidamente; estaba demasiado cansada incluso
                para sońar. Solo me desperté una vez para oír al viento rugir en
                furiosas ráfagas y la lluvia caer a torrentes, y para darme cuenta de
                que la seńorita Miller había ocupado su lugar a mi lado. Cuando volví
                a abrir los ojos, sonaba una fuerte campana; las nińas estaban
                levantadas y vistiéndose; el día aún no había empezado a amanecer,
                y una o dos lamparillas ardían en la habitación. Yo también me
                levanté a regańadientes; hacía un frío glacial, y me vestí como pude
                entre tiritones, y me lavé cuando hubo una palangana libre, lo que
                no ocurrió pronto, ya que solo había una palangana para cada seis
                nińas, en los soportes del centro de la habitación. De nuevo sonó la
                campana; todas formaron en fila, de dos en dos, y en ese orden
                descendieron las escaleras y entraron en el aula fría y tenuemente
                iluminada. Aquí, la seńorita Miller leyó las oraciones; después gritó:</p>

                <p>—ˇFormad las clases!</p>

                <p>Siguió un gran tumulto durante algunos minutos, durante los
                cuales la seńorita Miller exclamó repetidamente: «ˇSilencio!» y
                «ˇOrden!». Cuando amainó, las vi a todas dispuestas en cuatro
                semicírculos, frente a cuatro sillas, colocadas en las cuatro mesas;
                todas sostenían libros en sus manos, y un gran libro, como una
                Biblia, yacía sobre cada mesa, ante el asiento vacío. Siguió una
                pausa de algunos segundos, llenada por el murmullo bajo y vago de
                las voces; la seńorita Miller caminaba de clase en clase, acallando
                este sonido indefinido.</p>

                <p>Una campana lejana tintineó; inmediatamente tres damas
                entraron en la sala, cada una se dirigió a una mesa y tomó asiento.
                La seńorita Miller ocupó la cuarta silla vacía, que era la más cercana
                a la puerta, y alrededor de la cual se reunían las nińas más
                pequeńas. A esta clase inferior fui llamada y colocada al final de ella.</p>

                <p>El trabajo comenzó entonces: se repitió la colecta del día, luego se
                recitaron ciertos textos de las Escrituras, y a estos les sucedió una
                prolongada lectura de capítulos de la Biblia, que duró una hora. Para
                cuando terminó ese ejercicio, el día había amanecido por completo.
                La infatigable campana sonó entonces por cuarta vez: las clases
                fueron formadas y marcharon a otra sala para desayunar. ˇQué
                contenta me puse al ver la perspectiva de conseguir algo de comer!
                Estaba ya casi enferma de inanición, habiendo comido tan poco el
                día anterior.</p>

                <p>El refectorio era una sala grande, de techo bajo y sombría; sobre
                dos largas mesas humeaban cuencos de algo caliente que, sin
                embargo, para mi consternación, desprendían un olor nada
                apetecible. Vi una manifestación universal de descontento cuando
                los vapores del rancho llegaron a las fosas nasales de las destinadas
                a tragarlo; desde la vanguardia de la procesión, las chicas altas de la
                primera clase, se alzaron las palabras susurradas:</p>

                <p>—ˇQué asco! ˇLas gachas se han quemado otra vez!</p>

                <p>—ˇSilencio! —exclamó una voz; no la de la seńorita Miller, sino la
                de una de las maestras superiores, una persona pequeńa y morena,
                elegantemente vestida, pero de aspecto algo hosco, que se instaló
                en la cabecera de una mesa, mientras una dama más rolliza presidía
                la otra. Busqué en vano a la que había visto por primera vez la
                noche anterior; no estaba visible. La seńorita Miller ocupaba el pie
                de la mesa donde yo me sentaba, y una extrańa dama de aspecto
                extranjero y edad avanzada, la profesora de francés, como descubrí
                después, ocupaba el asiento correspondiente en la otra mesa. Se
                rezó una larga oración de gracias y se cantó un himno; luego un
                criado trajo un poco de té para las maestras, y comenzó la comida.</p>

                <p>Hambrienta, y ahora muy débil, devoré una o dos cucharadas de
                mi porción sin pensar en su sabor; pero una vez mitigada la primera
                punzada del hambre, me di cuenta de que tenía en las manos un
                mejunje nauseabundo; las gachas quemadas son casi tan malas
                como las patatas podridas; la propia hambruna pronto se harta de
                ellas. Las cucharas se movían lentamente; vi a cada nińa probar su
                comida e intentar tragarla; pero en la mayoría de los casos el
                esfuerzo se abandonaba pronto. El desayuno terminó, y nadie había
                desayunado. Dadas las gracias por lo que no habíamos recibido, y
                cantado un segundo himno, el refectorio fue evacuado hacia el aula.
                Fui una de las últimas en salir, y al pasar por las mesas, vi a una
                maestra tomar un cuenco de las gachas y probarlas; miró a las
                otras; todos sus semblantes expresaban desagrado, y una de ellas,
                la robusta, susurró:</p>

                <p>—ˇCosa abominable! ˇQué vergüenza!</p>

                <p>Pasó un cuarto de hora antes de que las lecciones comenzaran de
                nuevo, durante el cual el aula se sumió en un glorioso tumulto;
                durante ese lapso de tiempo parecía estar permitido hablar en voz
                alta y con más libertad, y ellas usaron su privilegio. Toda la
                conversación giraba en torno al desayuno, que todas y cada una
                denostaron sin miramientos. ˇPobres! Era el único consuelo que
                tenían. La seńorita Miller era ahora la única maestra en la sala; un
                grupo de chicas mayores de pie a su alrededor hablaba con gestos
                serios y hoscos. Oí el nombre del seńor Brocklehurst pronunciado
                por algunos labios, a lo que la seńorita Miller meneó la cabeza con
                desaprobación; pero no hizo un gran esfuerzo por frenar la ira
                general; sin duda, la compartía.</p>

                <p>Un reloj en el aula dio las nueve; la seńorita Miller dejó su círculo
                y, de pie en medio de la sala, gritó:</p>

                <p>—ˇSilencio! ˇA vuestros sitios!</p>

                <p>La disciplina prevaleció: en cinco minutos la confusa multitud se
                resolvió en orden, y un silencio comparativo acalló el clamor de
                Babel de las lenguas. Las maestras superiores reanudaron
                puntualmente sus puestos, pero aún así, todas parecían esperar.
                Dispuestas en bancos a los lados de la sala, las ochenta nińas se
                sentaban inmóviles y erguidas; parecían una curiosa asamblea,
                todas con el pelo liso peinado hacia atrás, sin un solo rizo visible;
                con vestidos marrones, de cuello alto y rodeados por una estrecha
                lorza alrededor de la garganta, con pequeńos bolsillos de tela de
                Holanda (con una forma parecida al sporran de un highlander)
                atados al frente de sus vestidos, y destinados a servir como bolsa de
                labores; todas, también, llevaban medias de lana y zapatos de
                fabricación rústica, abrochados con hebillas de latón. Más de veinte
                de las vestidas con este atuendo eran chicas ya crecidas, o más bien
                mujeres jóvenes; les sentaba mal y daba un aire de extrańeza
                incluso a las más bonitas.</p>

                <p>Todavía las estaba mirando, y también examinando a intervalos a
                las maestras, ninguna de las cuales me agradaba precisamente;
                pues la robusta era un poco tosca, la morena no poco fiera, la
                extranjera áspera y grotesca, y la seńorita Miller, ˇpobrecilla!, parecía
                amoratada, curtida por el tiempo y agotada por el trabajo, cuando,
                mientras mi ojo vagaba de rostro en rostro, toda la escuela se
                levantó simultáneamente, como movida por un resorte común.</p>

                <p>żQué ocurría? No había oído dar ninguna orden; estaba perpleja.
                Antes de que pudiera recuperar el juicio, las clases volvieron a
                sentarse; pero como todos los ojos se volvían ahora hacia un punto,
                los míos siguieron la dirección general y se encontraron con el
                personaje que me había recibido la noche anterior. Estaba de pie al
                fondo de la larga sala, junto al hogar, pues había un fuego en cada
                extremo. Contempló las dos filas de nińas en silencio y con
                gravedad. La seńorita Miller, acercándose, pareció hacerle una
                pregunta y, habiendo recibido su respuesta, volvió a su lugar y dijo
                en voz alta:</p>

                <p>—ˇMonitora de la primera clase, traed los globos terráqueos!</p>

                <p>Mientras se ejecutaba la orden, la dama consultada avanzó
                lentamente por la sala. Supongo que tengo un órgano de veneración
                considerable, pues aún conservo la sensación de admirado asombro
                con la que mis ojos siguieron sus pasos. Vista ahora, a plena luz del
                día, parecía alta, rubia y bien formada; unos ojos castańos con una
                luz benigna en sus iris, y un fino trazo de largas pestańas alrededor,
                aliviaban la blancura de su ancha frente; en cada una de sus sienes,
                su cabello, de un castańo muy oscuro, se agrupaba en rizos
                redondos, según la moda de aquellos tiempos, cuando no estaban
                en boga ni las bandas lisas ni los largos tirabuzones; su vestido,
                también a la moda del día, era de pańo púrpura, realzado por una
                especie de adorno espańol de terciopelo negro; un reloj de oro (los
                relojes no eran tan comunes entonces como ahora) brillaba en su
                cinturón. Ańada el lector, para completar el cuadro, rasgos refinados;
                una tez, si bien pálida, clara; y un aire y porte majestuosos, y
                tendrá, al menos, tan claramente como las palabras pueden darlo,
                una idea correcta del exterior de la seńorita Temple —Maria Temple,
                como vi después el nombre escrito en un libro de oraciones que se
                me confió para llevar a la iglesia.</p>

                <p>La superintendente de Lowood (pues tal era esta dama), habiendo
                tomado asiento ante un par de globos terráqueos colocados sobre
                una de las mesas, convocó a la primera clase a su alrededor y
                comenzó a dar una lección de geografía; las clases inferiores fueron
                llamadas por las maestras. Las repeticiones de historia, gramática,
                etc., continuaron durante una hora; siguieron la escritura y la
                aritmética, y la seńorita Temple dio lecciones de música a algunas de
                las chicas mayores. La duración de cada lección se medía por el
                reloj, que finalmente dio las doce. La superintendente se levantó.</p>

                <p>—Tengo unas palabras que dirigir a las alumnas —dijo.</p>

                <p>El tumulto del cese de las lecciones ya estaba estallando, pero se
                acalló ante su voz. Continuó:</p>

                <p>—Esta mańana habéis tenido un desayuno que no habéis podido
                comer; debéis de estar hambrientas. He ordenado que se sirva a
                todas un almuerzo de pan y queso.</p>

                <p>Las maestras la miraron con una especie de sorpresa.</p>

                <p>—Se hará bajo mi responsabilidad —ańadió, en tono explicativo
                hacia ellas, e inmediatamente después abandonó la sala.</p>

                <p>El pan y el queso fueron traídos al momento y distribuidos, para
                gran deleite y refrigerio de toda la escuela. Se dio entonces la orden:
                «ˇAl jardín!». Cada una se puso un tosco sombrero de paja, con
                cintas de calicó de colores, y una capa de frisa gris. A mí me
                equiparon de manera similar y, siguiendo la corriente, salí al aire
                libre.</p>

                <p>El jardín era un amplio recinto, rodeado de muros tan altos que
                excluían cualquier atisbo de vista; una galería cubierta corría por un
                lado, y anchos paseos bordeaban un espacio central dividido en
                veintenas de pequeńos arriates. Estos arriates estaban asignados
                como jardines para que las alumnas los cultivaran, y cada arriate
                tenía una dueńa. Cuando estuvieran llenos de flores, sin duda se
                verían bonitos; pero ahora, a finales de enero, todo era desolación
                invernal y decadencia parda. Me estremecí mientras estaba de pie y
                miraba a mi alrededor. Era un día inclemente para el ejercicio al aire
                libre; no positivamente lluvioso, pero oscurecido por una neblina
                amarillenta y lloviznosa; todo bajo los pies seguía empapado por las
                inundaciones de ayer. Las más fuertes de las nińas corrían y
                participaban en juegos activos, pero varias, pálidas y delgadas, se
                agrupaban en busca de refugio y calor en la galería; y entre estas,
                mientras la densa niebla penetraba en sus tiritantes cuerpos, oí con
                frecuencia el sonido de una tos cavernosa.</p>

                <p>Hasta el momento no había hablado con nadie, ni nadie parecía
                fijarse en mí; estaba bastante sola, pero a ese sentimiento de
                aislamiento estaba acostumbrada; no me oprimía mucho. Me apoyé
                en un pilar de la galería, me ceńí bien mi manto gris y, tratando de
                olvidar el frío que me atenazaba por fuera y el hambre insatisfecha
                que me roía por dentro, me entregué a la tarea de observar y
                pensar. Mis reflexiones eran demasiado indefinidas y fragmentarias
                para merecer ser registradas; apenas sabía todavía dónde estaba;
                Gateshead y mi vida pasada parecían haberse alejado a una
                distancia inconmensurable; el presente era vago y extrańo, y del
                futuro no podía formar ninguna conjetura. Miré alrededor del jardín
                de aspecto conventual, y luego hacia la casa, un gran edificio, la
                mitad del cual parecía gris y antiguo, la otra mitad completamente
                nueva. La parte nueva, que contenía el aula y el dormitorio, estaba
                iluminada por ventanas de menejillos y celosías, que le daban un
                aspecto de iglesia; una lápida de piedra sobre la puerta llevaba esta
                inscripción:</p>

                <p class="centradoP">INSTITUCIÓN LOWOOD. Esta parte fue reconstruida en el ańo de nuestro Seńor..., por
                Naomi Brocklehurst, de Brocklehurst Hall, en este condado. 
                «Así alumbre vuestra luz delante de los hombres, para que vean
                vuestras buenas obras, y glorifiquen a vuestro Padre que está en los
                cielos.» —S. Mateo v. 16.</p>

                <p>Leí estas palabras una y otra vez; sentí que les correspondía una
                explicación y fui incapaz de penetrar plenamente su significado.
                Todavía estaba meditando sobre el significado de «Institución», y
                tratando de establecer una conexión entre las primeras palabras y el
                versículo de las Escrituras, cuando el sonido de una tos justo detrás
                de mí me hizo girar la cabeza. Vi a una nińa sentada en un banco de
                piedra cercano; estaba inclinada sobre un libro, en cuya lectura
                parecía absorta. Desde donde yo estaba podía ver el título, era
                Rasselas, un nombre que me pareció extrańo y, por consiguiente,
                atractivo. Al pasar una hoja, acertó a levantar la vista, y le dije
                directamente:</p>

                <p>—żEs interesante tu libro? —Ya había formado la intención de
                pedirle que me lo prestara algún día.</p>

                <p>—Me gusta —respondió, tras una pausa de uno o dos segundos,
                durante los cuales me examinó.</p>

                <p>—żDe qué trata? —continué. Apenas sé de dónde saqué la osadía
                para entablar así una conversación con una extrańa; el paso era
                contrario a mi naturaleza y a mis hábitos, pero creo que su
                ocupación tocó una fibra de simpatía en alguna parte; pues a mí
                también me gustaba leer, aunque de un tipo frívolo e infantil; no
                podía digerir ni comprender lo serio o sustancial.</p>

                <p>—Puedes mirarlo —replicó la nińa, ofreciéndome el libro.</p>

                <p>Así lo hice; un breve examen me convenció de que el contenido
                era menos atractivo que el título: Rasselas parecía aburrido para mi
                gusto trivial; no vi nada sobre hadas, nada sobre genios; ninguna
                variedad brillante parecía extenderse sobre las páginas densamente
                impresas. Se lo devolví; ella lo recibió tranquilamente y, sin decir
                nada, estaba a punto de volver a sumirse en su anterior estado de
                ánimo estudioso. De nuevo me aventuré a molestarla:</p>

                <p>—żPuedes decirme qué significa lo que está escrito en esa piedra
                sobre la puerta? żQué es la Institución Lowood?</p>

                <p>—Esta casa donde has venido a vivir.</p>

                <p>—żY por qué la llaman Institución? żEs diferente de otras escuelas
                de alguna manera?</p>

                <p>—Es en parte una escuela de caridad. Tú y yo, y todas las demás,
                somos nińas de caridad. Supongo que eres huérfana. żNo han
                muerto tu padre o tu madre?</p>

                <p>—Ambos murieron antes de que yo pueda recordar.</p>

                <p>—Bueno, todas las nińas aquí han perdido a uno o a ambos
                padres, y esto se llama una institución para educar huérfanas.</p>

                <p>—żNo pagamos dinero? żNos mantienen gratis?</p>

                <p>—Pagamos, o nuestros amigos pagan, quince libras al ańo por
                cada una.</p>

                <p>—Entonces, żpor qué nos llaman nińas de caridad?</p>

                <p>—Porque quince libras no es suficiente para la manutención y la
                enseńanza, y la diferencia se cubre con donaciones.</p>

                <p>—żQuién dona?</p>

                <p>—Diferentes damas y caballeros de buen corazón de esta vecindad
                y de Londres.</p>

                <p>—żQuién fue Naomi Brocklehurst?</p>

                <p>—La dama que construyó la parte nueva de esta casa, como
                registra esa lápida, y cuyo hijo supervisa y dirige todo aquí.</p>

                <p>—żPor qué?</p>

                <p>—Porque es el tesorero y gerente del establecimiento.</p>

                <p>—Entonces, żesta casa no pertenece a esa dama alta que lleva un
                reloj y que dijo que íbamos a tomar pan y queso?</p>

                <p>—żA la seńorita Temple? ˇOh, no! Ojalá fuera así. Ella tiene que
                responder ante el seńor Brocklehurst por todo lo que hace. El seńor
                Brocklehurst compra toda nuestra comida y toda nuestra ropa.</p>

                <p>—żVive él aquí?</p>

                <p>—No, a dos millas de aquí, en una gran mansión.</p>

                <p>—żEs un buen hombre?</p>

                <p>—Es un clérigo, y se dice que hace mucho bien.</p>

                <p>—żDijiste que esa dama alta se llamaba seńorita Temple?</p>

                <p>—Sí.</p>

                <p>—żY cómo se llaman las otras maestras?</p>

                <p>—La de mejillas rojas se llama seńorita Smith; se ocupa de las
                labores y corta los patrones, pues nosotras hacemos nuestra propia
                ropa, nuestros vestidos y pellizas, y todo. La pequeńa de pelo negro
                es la seńorita Scatcherd; enseńa historia y gramática, y escucha las
                repeticiones de la segunda clase. Y la que lleva un chal y tiene un
                pańuelo atado a un lado con una cinta amarilla es Madame Pierrot;
                viene de Lille, en Francia, y enseńa francés.</p>

                <p>—żTe gustan las maestras?</p>

                <p>—Bastante.</p>

                <p>—żTe gustan la pequeńa de pelo negro y la Madame...? No puedo
                pronunciar su nombre como tú.</p>

                <p>—La seńorita Scatcherd es impulsiva, debes tener cuidado de no
                ofenderla. Madame Pierrot no es una mala persona.</p>

                <p>—Pero la seńorita Temple es la mejor, żno es así?</p>

                <p>—La seńorita Temple es muy buena y muy inteligente; está por
                encima de las demás, porque sabe mucho más que ellas.</p>

                <p>—żLlevas mucho tiempo aquí?</p>

                <p>—Dos ańos.</p>

                <p>—żEres huérfana?</p>

                <p>—Mi madre ha muerto.</p>

                <p>—żEres feliz aquí?</p>

                <p>—Haces demasiadas preguntas. Te he dado suficientes respuestas
                por el momento. Ahora quiero leer.</p>

                <p>Pero en ese momento sonó la llamada para la cena; todas
                volvimos a entrar en la casa. El olor que ahora llenaba el refectorio
                era apenas más apetitoso que el que había deleitado nuestras fosas
                nasales en el desayuno. La cena se sirvió en dos enormes
                recipientes de hojalata, de los que se elevaba un fuerte vapor con
                olor a grasa rancia. Descubrí que el rancho consistía en patatas
                mediocres y extrańos jirones de carne de color herrumbroso,
                mezclados y cocinados juntos. De esta preparación se asignó un
                plato tolerable y abundante a cada alumna. Comí lo que pude y me
                pregunté si la comida de todos los días sería así.</p>

                <p>Después de la cena, nos trasladamos inmediatamente al aula. Las
                lecciones se reanudaron y continuaron hasta las cinco en punto.</p>

                <p>El único acontecimiento notable de la tarde fue que vi a la nińa
                con la que había conversado en la galería ser expulsada con
                deshonor de una clase de historia por la seńorita Scatcherd y
                enviada a permanecer de pie en medio de la gran aula. El castigo
                me pareció en alto grado ignominioso, especialmente para una chica
                tan mayor —parecía tener trece ańos o más—. Esperaba que
                mostrara signos de gran angustia y vergüenza; pero para mi
                sorpresa, no lloró ni se sonrojó. Serena, aunque grave, permaneció
                de pie, el centro de todas las miradas. «żCómo puede soportarlo tan
                tranquilamente, tan firmemente?», me pregunté. «Si yo estuviera en
                su lugar, me parece que desearía que la tierra se abriera y me
                tragara. Parece como si estuviera pensando en algo más allá de su
                castigo, más allá de su situación; en algo que no está a su alrededor
                ni ante ella. He oído hablar de sońar despierto, żestá sońando
                despierta ahora? Sus ojos están fijos en el suelo, pero estoy segura
                de que no lo ven; su vista parece haberse vuelto hacia adentro,
                haber descendido a su corazón. Está mirando lo que puede recordar,
                creo; no lo que está realmente presente. Me pregunto qué clase de
                chica será, si buena o mala».</p>

                <p>Poco después de las cinco de la tarde tuvimos otra comida, que
                consistía en una pequeńa taza de café y media rebanada de pan
                moreno. Devoré mi pan y bebí mi café con deleite; pero me habría
                alegrado de recibir otro tanto, todavía tenía hambre. Siguió media
                hora de recreo, luego estudio; luego el vaso de agua y el trozo de
                torta de avena, las oraciones y a la cama. Tal fue mi primer día en
                Lowood.</p>
                
