                   <h1>Capítulo XIV</h1>
    
                    <p>Durante varios días subsiguientes vi poco al señor Rochester. Por las
                    mañanas parecía muy ocupado con negocios y, por las tardes,
                    caballeros de Millcote o de los alrededores lo visitaban, y a veces se
                    quedaban a cenar con él. Cuando su esguince estuvo lo
                    suficientemente bien como para permitir el ejercicio a caballo, salió a
                    cabalgar mucho; probablemente para devolver estas visitas, ya que
                    generalmente no regresaba hasta bien entrada la noche.</p>

                    <p>Durante este intervalo, incluso a Adèle rara vez se la mandaba
                    llamar a su presencia, y todo mi trato con él se limitaba a un
                    encuentro ocasional en el vestíbulo, en las escaleras o en la galería,
                    donde a veces pasaba a mi lado altiva y fríamente, apenas
                    reconociendo mi presencia con una distante inclinación de cabeza o
                    una mirada fría, y a veces hacía una reverencia y sonreía con
                    afabilidad de caballero. Sus cambios de humor no me ofendían,
                    porque veía que no tenía nada que ver con su alternancia; el flujo y
                    reflujo dependían de causas completamente desconectadas de mí.</p>

                    <p>Un día había tenido invitados a cenar y había mandado a buscar
                    mi portafolio; sin duda, para exhibir su contenido. Los caballeros se
                    marcharon temprano para asistir a una reunión pública en Millcote,
                    como me informó la señora Fairfax; pero como la noche era húmeda
                    e inclemente, el señor Rochester no los acompañó. Poco después de
                    que se fueran, tocó la campanilla: llegó un mensaje de que Adèle y
                    yo debíamos bajar. Cepillé el pelo de Adèle y la arreglé, y
                    habiéndome asegurado de que yo misma estaba con mi habitual
                    atuendo cuáquero, donde no había nada que retocar —siendo todo
                    demasiado ceñido y sencillo, incluidos los cabellos trenzados, para
                    admitir desorden—, descendimos, Adèle preguntándose si el petit
                    coffre habría llegado por fin; pues, debido a algún error, su llegada
                    se había retrasado hasta ahora. Quedó satisfecha: allí estaba, un
                    pequeño cartón, sobre la mesa cuando entramos en el comedor.
                    Pareció reconocerlo por instinto.</p>

                    <p><i>—Ma boîte! ma boîte!</i> —exclamó, corriendo hacia ella.</p>

                    <p>—Sí, ahí está tu «<i>boîte</i>» por fin. Llévala a un rincón, genuina hija
                    de París, y diviértete destripándola —dijo la voz profunda y algo
                    sarcástica del señor Rochester, que procedía de las profundidades de
                    un inmenso sillón junto al fuego—. Y ten cuidado —continuó—, no
                    me molestes con ningún detalle del proceso anatómico, ni con
                    ninguna noticia sobre el estado de las entrañas. Que tu operación se
                    realice en silencio.<i>Tiens-toi tranquille, enfant; comprends-tu?</i></p>

                    <p>Adèle apenas pareció necesitar la advertencia; ya se había retirado
                    a un sofá con su tesoro y estaba ocupada desatando el cordón que
                    aseguraba la tapa. Habiendo quitado este impedimento y levantado
                    ciertos sobres plateados de papel de seda, simplemente exclamó:</p>

                    <p>—<i>Oh ciel! Que c'est beau!</i>—y luego permaneció absorta en una
                    contemplación extática.</p>

                    <p>—¿Está ahí la señorita Eyre? —demandó ahora el amo, medio
                    levantándose de su asiento para mirar hacia la puerta, cerca de la
                    cual yo todavía estaba de pie.</p>

                    <p>—¡Ah! Bueno, acérquese; siéntese aquí. —Acercó una silla a la
                    suya—. No me gusta el parloteo de los niños —continuó—; pues,
                    solterón como soy, no tengo asociaciones agradables relacionadas
                    con su ceceo. Me sería intolerable pasar toda una velada tête-à-tête
                    con una mocosa. No aleje más esa silla, señorita Eyre; siéntese
                    exactamente donde la coloqué, si le parece bien, claro. ¡Malditas
                    sean estas cortesías! Las olvido continuamente. Tampoco me afectan
                    particularmente las ancianas de mente simple. Por cierto, debo tener
                    en cuenta a la mía; no conviene descuidarla; es una Fairfax, o está
                    casada con uno; y se dice que la sangre es más espesa que el agua.</p>

                    <p>Tocó la campanilla y despachó una invitación a la señora Fairfax,
                    que pronto llegó, con la cesta de la costura en la mano.</p>

                    <p>—Buenas noches, señora; la he mandado llamar con un propósito
                    caritativo. Le he prohibido a Adèle que me hable de sus regalos, y
                    está a punto de reventar de repleción; tenga la bondad de servirle
                    de oyente e interlocutora; será uno de los actos más benévolos que
                    haya realizado jamás.</p>

                    <p>Adèle, en efecto, tan pronto como vio a la señora Fairfax, la
                    convocó a su sofá, y allí rápidamente llenó su regazo con la
                    porcelana, el marfil, el contenido de cera de su «<i>boîte</i>», vertiendo,
                    mientras tanto, explicaciones y éxtasis en un inglés tan chapurreado
                    como dominaba.</p>

                    <p>—Ahora que he desempeñado el papel de un buen anfitrión —
                    prosiguió el señor Rochester—, y he puesto a mis invitadas en
                    camino de entretenerse mutuamente, debería tener la libertad de
                    atender a mi propio placer. Señorita Eyre, acerque su silla un poco
                    más. Todavía está demasiado atrás; no puedo verla sin perturbar mi
                    posición en esta cómoda silla, lo cual no tengo intención de hacer.</p>

                    <p>Hice lo que se me ordenó, aunque habría preferido mucho más
                    permanecer algo en la sombra; pero el señor Rochester tenía una
                    forma tan directa de dar órdenes que parecía natural obedecerle
                    prontamente.</p>

                    <p>Estábamos, como he dicho, en el comedor. El candelabro, que se
                    había encendido para la cena, llenaba la habitación con una festiva
                    amplitud de luz; el gran fuego estaba todo rojo y claro; las cortinas
                    púrpuras colgaban ricas y amplias ante la elevada ventana y el arco
                    más elevado aún. Todo estaba en silencio, salvo la charla contenida
                    de Adèle (no se atrevía a hablar en voz alta) y, llenando cada pausa,
                    el golpeteo de la lluvia invernal contra los cristales.</p>

                    <p>El señor Rochester, sentado en su silla cubierta de damasco,
                    parecía diferente a como lo había visto antes; no tan severo, mucho
                    menos sombrío. Había una sonrisa en sus labios y sus ojos brillaban,
                    ya fuera por el vino o no, no estoy segura, pero creo que es muy
                    probable. Estaba, en resumen, en su humor de después de la cena;
                    más expansivo y cordial, y también más autocomplaciente que el
                    temperamento frígido y rígido de la mañana. Aun así, parecía
                    preciosamente adusto, apoyando su cabeza maciza contra el
                    respaldo abombado de su silla y recibiendo la luz del fuego en sus
                    rasgos tallados en granito y en sus grandes ojos oscuros; pues tenía
                    grandes ojos oscuros, y muy hermosos también, no sin un cierto
                    cambio en sus profundidades a veces, que, si no era suavidad, te
                    recordaba, al menos, ese sentimiento.</p>

                    <p>Había estado mirando el fuego durante dos minutos, y yo lo había
                    estado mirando a él el mismo tiempo, cuando, volviéndose de
                    repente, sorprendió mi mirada fija en su fisonomía.</p>

                    <p>—Me examina, señorita Eyre —dijo—. ¿Me encuentra guapo?</p>

                    <p>De haberlo deliberado, habría respondido a esta pregunta con algo
                    convencionalmente vago y cortés; pero la respuesta se me escapó
                    de la lengua de alguna manera antes de darme cuenta:</p>

                    <p>—No, señor.</p>

                    <p>—¡Ah! ¡Por mi vida! Hay algo singular en usted —dijo—. Tiene el
                    aire de una pequeña nonnette; pintoresca, tranquila, grave y
                    sencilla, sentada con las manos juntas ante usted y los ojos
                    generalmente fijos en la alfombra (excepto, por cierto, cuando se
                    dirigen penetrantemente a mi rostro, como hace un momento, por
                    ejemplo); y cuando uno le hace una pregunta o un comentario al
                    que está obligada a responder, suelta una réplica redonda que, si no
                    es brusca, es al menos cortante. ¿Qué quiere decir con eso?</p>

                    <p>—Señor, fui demasiado directa; le pido perdón. Debería haber
                    respondido que no era fácil dar una respuesta improvisada a una
                    pregunta sobre apariencias; que los gustos suelen diferir; y que la
                    belleza tiene poca importancia, o algo por el estilo.</p>

                    <p>—No debería haber respondido tal cosa. ¡La belleza de poca
                    importancia, vaya! Y así, bajo el pretexto de suavizar el ultraje
                    anterior, de acariciarme y calmarme hasta la placidez, ¡me clava una
                    taimada navaja bajo la oreja! Continúe, ¿qué defecto me encuentra,
                    por favor? Supongo que tengo todos mis miembros y todos mis
                    rasgos como cualquier otro hombre.</p>

                    <p>—Señor Rochester, permítame retractarme de mi primera
                    respuesta. No pretendía una réplica mordaz, fue solo un desliz.</p>

                    <p>—Exacto. Eso creo. Y usted será responsable de ello. Critíqueme,
                    ¿no le agrada mi frente?</p>

                    <p>Levantó las ondas oscuras de cabello que yacían horizontalmente
                    sobre su frente y mostró una masa bastante sólida de órganos
                    intelectuales, pero una abrupta deficiencia donde debería haberse
                    alzado el suave signo de la benevolencia.</p>

                    <p>—Ahora, señora, ¿soy un tonto?</p>

                    <p>—Lejos de ello, señor. Usted, quizás, me consideraría grosera si le
                    preguntara a cambio si es usted un filántropo.</p>

                    <p>—¡Ahí va otra vez! Otro pinchazo de la navaja, cuando pretendía
                    darme una palmadita en la cabeza. Y eso es porque dije que no me
                    gustaba la compañía de niños y ancianas (¡dicho sea en voz baja!).
                    No, señorita, no soy un filántropo general; pero tengo conciencia —y
                    señaló las protuberancias que se dice que indican esa facultad, y
                    que, afortunadamente para él, eran suficientemente conspicuas,
                    dando, de hecho, una marcada anchura a la parte superior de su
                    cabeza—. Y, además, una vez tuve una especie de ruda ternura de
                    corazón. Cuando tenía su edad, era un tipo bastante sensible; parcial
                    hacia los implumes, los desamparados y los desafortunados. Pero la
                    Fortuna me ha zarandeado desde entonces; incluso me ha amasado
                    con sus nudillos, y ahora me jacto de ser duro y resistente como una
                    pelota de caucho; permeable, sin embargo, a través de una o dos
                    grietas todavía, y con un punto sensible en medio de la masa. Sí,
                    ¿deja eso alguna esperanza para mí?</p>

                    <p>—¿Esperanza de qué, señor?</p>

                    <p>—¿De mi retransformación final de caucho a carne?</p>

                    <p>«Decididamente ha bebido demasiado vino», pensé; y no supe
                    qué responder a su extraña pregunta. ¿Cómo podía saber yo si era
                    capaz de ser retransformado?</p>

                    <p>—Parece muy perpleja, señorita Eyre; y aunque no es usted
                    bonita, como tampoco yo soy guapo, un aire perplejo le sienta bien.
                    Además, es conveniente, porque mantiene esos ojos inquisitivos
                    suyos lejos de mi fisonomía y los ocupa con las flores de estambre
                    de la alfombra; así que siga perpleja. Señorita, estoy dispuesto a ser
                    gregario y comunicativo esta noche.</p>

                    <p>Con este anuncio se levantó de su silla y se quedó de pie,
                    apoyando el brazo en la repisa de mármol. En esa actitud, su figura
                    se veía claramente, así como su rostro; su inusual anchura de pecho,
                    desproporcionada casi con la longitud de sus miembros. Estoy
                    segura de que la mayoría de la gente lo habría considerado un
                    hombre feo; sin embargo, había tanto orgullo inconsciente en su
                    porte, tanta soltura en su comportamiento, una mirada de completa
                    indiferencia hacia su propia apariencia externa, una confianza tan
                    altiva en el poder de otras cualidades, intrínsecas o adventicias, para
                    compensar la falta de mero atractivo personal, que, al mirarlo, una
                    inevitablemente compartía la indiferencia e, incluso en un sentido
                    ciego e imperfecto, depositaba fe en la confianza.</p>

                    <p>—Estoy dispuesto a ser gregario y comunicativo esta noche —
                    repitió—, y por eso la he mandado llamar. El fuego y el candelabro
                    no eran compañía suficiente para mí; tampoco lo habría sido Pilot,
                    pues ninguno de ellos puede hablar. Adèle es un grado mejor, pero
                    aún está muy por debajo de la marca; la señora Fairfax, ídem.
                    Usted, estoy persuadido, puede convenirme si quiere. Me
                    desconcertó la primera noche que la invité a bajar aquí. Casi la he
                    olvidado desde entonces; otras ideas han expulsado las suyas de mi
                    cabeza. Pero esta noche estoy resuelto a estar a gusto; a desechar
                    lo que importuna y a recordar lo que agrada. Me agradaría ahora
                    hacerla hablar, saber más de usted. Por lo tanto, hable.</p>

                    <p>En lugar de hablar, sonreí; y no una sonrisa muy complaciente o sumisa, tampoco.</p>

                    <p>—Hable —insistió.</p>

                    <p>—¿Sobre qué, señor?</p>

                    <p>—Sobre lo que quiera. Le dejo a usted enteramente tanto la
                    elección del tema como la manera de tratarlo.</p>

                    <p>En consecuencia, me senté y no dije nada. «Si espera que hable
                    por el mero hecho de hablar y lucirme, descubrirá que se ha dirigido
                    a la persona equivocada», pensé.</p>

                    <p>—Está usted muda, señorita Eyre.</p>

                    <p>Seguía muda. Inclinó un poco la cabeza hacia mí y, con una sola
                    mirada rápida, pareció zambullirse en mis ojos.</p>

                    <p>—¿Terca? —dijo—, y molesta. ¡Ah! Es coherente. Hice mi petición
                    de una forma absurda, casi insolente. Señorita Eyre, le pido perdón.
                    El hecho es que, de una vez por todas, no deseo tratarla como a una
                    inferior. Es decir (corrigiéndose), solo reclamo la superioridad que
                    debe resultar de veinte años de diferencia de edad y un siglo de
                    avance en experiencia. Esto es legítimo, et j'y tiens, como diría
                    Adèle; y es en virtud de esta superioridad, y solo de esta, que deseo
                    que tenga la bondad de hablarme un poco ahora y desviar mis
                    pensamientos, que están irritados de tanto insistir en un punto,
                    enconándose como un clavo oxidado.</p>

                    <p>Se había dignado a dar una explicación, casi una disculpa, y no
                    me sentí insensible a su condescendencia, y no quise parecerlo.</p>

                    <p>—Estoy dispuesta a entretenerle, si puedo, señor, muy dispuesta;
                    pero no puedo introducir un tema, porque, ¿cómo sé lo que le
                    interesará? Hágame preguntas y haré todo lo posible por
                    responderlas.</p>

                    <p>—Entonces, en primer lugar, ¿está de acuerdo conmigo en que
                    tengo derecho a ser un poco autoritario, brusco, quizás exigente, a
                    veces, por las razones que he expuesto, a saber, que tengo edad
                    suficiente para ser su padre y que he luchado a través de una
                    variada experiencia con muchos hombres de muchas naciones y he
                    vagado por medio mundo, mientras que usted ha vivido
                    tranquilamente con un solo grupo de personas en una sola casa?</p>

                    <p>—Haga lo que le plazca, señor.</p>

                    <p>—Esa no es una respuesta; o más bien es una muy irritante,
                    porque es muy evasiva. Responda claramente.</p>

                    <p>—No creo, señor, que tenga derecho a mandarme, simplemente
                    porque sea mayor que yo, o porque haya visto más mundo que yo.
                    Su derecho a la superioridad depende del uso que haya hecho de su
                    tiempo y experiencia.</p>

                    <p>—¡Hum! Dicho con prontitud. Pero no lo permitiré, viendo que
                    nunca se ajustaría a mi caso, ya que he hecho un uso indiferente,
                    por no decir malo, de ambas ventajas. Dejando de lado la
                    superioridad, entonces, aún debe aceptar recibir mis órdenes de vez
                    en cuando, sin sentirse ofendida o herida por el tono de mando. ¿Lo
                    hará?</p>

                    <p>Sonreí. Pensé para mis adentros: el señor Rochester es peculiar;
                    parece olvidar que me paga 30 libras anuales por recibir sus
                    órdenes.</p>

                    <p>—La sonrisa está muy bien —dijo, captando al instante la
                    expresión pasajera—; pero hable también.</p>

                    <p>—Estaba pensando, señor, que muy pocos amos se molestarían en
                    preguntar si sus subordinados pagados se sentían ofendidos y
                    heridos por sus órdenes.</p>

                    <p>—¡Subordinados pagados! ¡Cómo! ¿Es usted mi subordinada
                    pagada, verdad? ¡Oh, sí, había olvidado el salario! Bueno, entonces,
                    sobre esa base mercenaria, ¿aceptará que la mandonee un poco?</p>

                    <p>—No, señor, no sobre esa base; pero, sobre la base de que lo
                    olvidó, y de que le importa si un dependiente está cómodo en su
                    dependencia, acepto de todo corazón.</p>

                    <p>—¿Y consentirá en prescindir de muchas formas y frases
                    convencionales, sin pensar que la omisión surge de la insolencia?</p>

                    <p>—Estoy segura, señor, de que nunca confundiría la informalidad
                    con la insolencia. Una me gusta bastante; a la otra, nada que haya
                    nacido libre se sometería, ni siquiera por un salario.</p>

                    <p>—¡Paparruchas! La mayoría de las cosas nacidas libres se
                    someterán a cualquier cosa por un salario; por lo tanto, guárdeselo
                    para usted y no se aventure en generalidades de las que es
                    intensamente ignorante. Sin embargo, mentalmente le doy la mano
                    por su respuesta, a pesar de su inexactitud; y tanto por la manera
                    en que fue dicha como por la sustancia del discurso. La manera fue
                    franca y sincera; no se ve a menudo tal manera. No, por el contrario,
                    la afectación, o la frialdad, o la estúpida y grosera incomprensión del
                    significado de uno son las recompensas usuales de la franqueza. Ni
                    tres de cada tres mil institutrices colegialas inexpertas me habrían
                    respondido como usted acaba de hacer. Pero no pretendo halagarla.
                    Si está usted hecha de un molde diferente a la mayoría, no es mérito
                    suyo; la Naturaleza lo hizo. Y además, después de todo, voy
                    demasiado rápido en mis conclusiones; por lo que sé hasta ahora,
                    puede que no sea usted mejor que el resto; puede tener defectos
                    intolerables para contrarrestar sus pocos puntos buenos.</p>

                    <p>«Y también puede tenerlos usted», pensé. Mi ojo se encontró con
                    el suyo mientras la idea cruzaba mi mente. Pareció leer la mirada,
                    respondiendo como si su significado hubiera sido hablado además de
                    imaginado.</p>

                    <p>—Sí, sí, tiene razón —dijo—; tengo muchos defectos propios. Lo
                    sé, y no deseo paliarlos, se lo aseguro. Sabe Dios que no necesito
                    ser demasiado severo con los demás; tengo una existencia pasada,
                    una serie de actos, un color de vida que contemplar dentro de mi
                    propio pecho, que bien podrían desviar mis burlas y censuras de mis
                    vecinos hacia mí mismo. Empecé, o más bien (pues como otros
                    morosos, me gusta echar la mitad de la culpa a la mala fortuna y a
                    las circunstancias adversas) fui empujado a un rumbo equivocado a
                    la edad de veintiún años, y nunca he recuperado el rumbo correcto
                    desde entonces. Pero podría haber sido muy diferente; podría haber
                    sido tan bueno como usted, más sabio, casi tan inmaculado. Envidio
                    su paz mental, su conciencia limpia, su memoria no contaminada.
                    Niña, una memoria sin mancha ni contaminación debe de ser un
                    tesoro exquisito, una fuente inagotable de puro refrigerio. ¿No es
                    así?</p>

                    <p>—¿Cómo estaba su memoria cuando tenía dieciocho años, señor?</p>

                    <p>—Perfecta entonces; límpida, salubre. Ningún chorro de agua de
                    sentina la había convertido en un charco fétido. Yo era su igual a los
                    dieciocho, completamente su igual. La Naturaleza quiso que yo
                    fuera, en general, un buen hombre, señorita Eyre; de la mejor clase,
                    y ya ve que no lo soy. Usted diría que no lo ve; al menos me halago
                    al leer tanto en su ojo (cuidado, por cierto, con lo que expresa con
                    ese órgano; soy rápido interpretando su lenguaje). Entonces,
                    créame, no soy un villano. No debe suponer eso, no debe atribuirme
                    ninguna eminencia tan mala; pero, debido, creo firmemente, más a
                    las circunstancias que a mi inclinación natural, soy un pecador trivial
                    y corriente, maleado en todas las pobres y mezquinas disipaciones
                    con las que los ricos y los inútiles intentan vivir la vida. ¿Se pregunta
                    por qué le confieso esto? Sepa que, en el transcurso de su vida
                    futura, a menudo se encontrará elegida como la confidente
                    involuntaria de los secretos de sus conocidos. La gente descubrirá
                    instintivamente, como yo lo he hecho, que su fuerte no es hablar de
                    sí misma, sino escuchar mientras otros hablan de sí mismos.
                    Sentirán, también, que escucha sin desprecio malévolo por su
                    indiscreción, sino con una especie de simpatía innata; no menos
                    reconfortante y alentadora porque es muy discreta en sus
                    manifestaciones.</p>

                    <p>—¿Cómo lo sabe? ¿Cómo puede adivinar todo esto, señor?</p>

                    <p>—Lo sé bien; por lo tanto, procedo casi tan libremente como si
                    estuviera escribiendo mis pensamientos en un diario. Usted diría que
                    debería haber sido superior a las circunstancias; así debería, así
                    debería; pero ya ve, no lo fui. Cuando el destino me agravió, no tuve
                    la sabiduría de mantener la calma; me desesperé; luego degeneré.
                    Ahora, cuando cualquier simplón vicioso excita mi disgusto con su
                    miserable grosería, no puedo halagarme de ser mejor que él; me
                    veo obligado a confesar que él y yo estamos al mismo nivel. ¡Ojalá
                    me hubiera mantenido firme, sabe Dios que sí! Tema al
                    remordimiento cuando se sienta tentada a errar, señorita Eyre; el
                    remordimiento es el veneno de la vida.</p>

                    <p>—Se dice que el arrepentimiento es su cura, señor.</p>

                    <p>—No es su cura. La reforma puede ser su cura; y yo podría
                    reformarme —aún tengo fuerzas para eso— si... pero ¿de qué sirve
                    pensar en ello, obstaculizado, agobiado, maldito como estoy?
                    Además, ya que la felicidad me es irrevocablemente negada, tengo
                    derecho a obtener placer de la vida, y lo obtendré, cueste lo que
                    cueste.</p>

                    <p>—Entonces degenerará aún más, señor.</p>

                    <p>—Posiblemente. Sin embargo, ¿por qué debería hacerlo, si puedo
                    obtener un placer dulce y fresco? Y puedo obtenerlo tan dulce y
                    fresco como la miel silvestre que la abeja recoge en el páramo.</p>

                    <p>—Picará, sabrá amargo, señor.</p>

                    <p>—¿Cómo lo sabe? Usted nunca lo ha probado. Qué seria, qué
                    solemne se ve. Y es usted tan ignorante en la materia como esta
                    cabeza de camafeo (tomando una de la repisa de la chimenea). No
                    tiene derecho a sermonearme, neófita, que no ha pasado del porche
                    de la vida y desconoce absolutamente sus misterios.</p>

                    <p>—Solo le recuerdo sus propias palabras, señor. Dijo que el error
                    traía remordimiento, y pronunció el remordimiento como el veneno
                    de la existencia.</p>

                    <p>—¿Y quién habla de error ahora? Apenas creo que la idea que
                    revoloteó por mi cerebro fuera un error. Creo que fue una inspiración
                    más que una tentación. Fue muy agradable, muy reconfortante, eso
                    lo sé. ¡Aquí viene de nuevo! No es un demonio, se lo aseguro; o si lo
                    es, se ha puesto las vestiduras de un ángel de luz. Creo que debo
                    admitir a un huésped tan hermoso cuando pide entrada a mi
                    corazón.</p>

                    <p>—Desconfíe de él, señor; no es un verdadero ángel.</p>

                    <p>—Una vez más, ¿cómo lo sabe? ¿Con qué instinto pretende
                    distinguir entre un serafín caído del abismo y un mensajero del trono
                    eterno, entre un guía y un seductor?</p>

                    <p>—Juzgué por su semblante, señor, que estaba turbado cuando dijo
                    que la sugerencia había vuelto a usted. Estoy segura de que le
                    causará más miseria si la escucha.</p>

                    <p>—En absoluto, trae el mensaje más grato del mundo. Por lo
                    demás, no es usted la guardiana de mi conciencia, así que no se
                    inquiete. ¡Ea, entra, hermosa errante!</p>

                    <p>Dijo esto como si hablara a una visión, invisible para cualquier ojo
                    que no fuera el suyo; luego, cruzando los brazos, que había medio
                    extendido, sobre el pecho, pareció encerrar en su abrazo al ser
                    invisible.</p>

                    <p>—Ahora —continuó, dirigiéndose de nuevo a mí—, he recibido al
                    peregrino, una deidad disfrazada, como creo firmemente. Ya me ha
                    hecho bien. Mi corazón era una especie de osario; ahora será un
                    santuario.</p>

                    <p>—A decir verdad, señor, no le entiendo en absoluto. No puedo
                    seguir la conversación, porque ha superado mi profundidad. Solo sé
                    una cosa: dijo que no era tan bueno como le gustaría ser, y que
                    lamentaba su propia imperfección. Una cosa puedo comprender:
                    insinuó que tener una memoria manchada era una perdición
                    perpetua. Me parece que si se esforzara, con el tiempo encontraría
                    posible llegar a ser lo que usted mismo aprobaría; y que si desde
                    este día comenzara con resolución a corregir sus pensamientos y
                    acciones, en pocos años habría acumulado un nuevo e inmaculado
                    acervo de recuerdos, a los que podría recurrir con placer.</p>

                    <p>—Justamente pensado; correctamente dicho, señorita Eyre; y, en
                    este momento, estoy pavimentando el infierno con energía.</p>

                    <p>—¿Señor?</p>

                    <p>—Estoy sentando buenas intenciones, que creo duraderas como el
                    sílex. Ciertamente, mis compañías y ocupaciones serán otras de las
                    que han sido.</p>

                    <p>—¿Y mejores?</p>

                    <p>—Y mejores, tanto mejores como el mineral puro lo es de la
                    escoria inmunda. Parece dudar de mí; yo no dudo de mí mismo. Sé
                    cuál es mi objetivo, cuáles son mis motivos; y en este momento
                    dicto una ley, inalterable como la de los medos y los persas, de que
                    ambos son correctos.</p>

                    <p>—No pueden serlo, señor, si requieren un nuevo estatuto para legalizarlos.</p>

                    <p>—Lo son, señorita Eyre, aunque requieran absolutamente un
                    nuevo estatuto. Combinaciones de circunstancias inauditas exigen
                    reglas inauditas.</p>

                    <p>—Esa suena como una máxima peligrosa, señor; porque se ve de
                    inmediato que es susceptible de abuso.</p>

                    <p>—¡Sabia sentenciosa! Así es. Pero juro por mis dioses domésticos
                    no abusar de ella.</p>

                    <p>—Es usted humano y falible.</p>

                    <p>—Lo soy. También lo es usted. ¿Y qué?</p>

                    <p>—Lo humano y falible no debería arrogarse un poder que solo
                    puede confiarse con seguridad a lo divino y perfecto.</p>

                    <p>—¿Qué poder?</p>

                    <p>—El de decir de cualquier línea de acción extraña y no sancionada:
                    «Que sea correcta».</p>

                    <p>—«Que sea correcta», las palabras exactas. Las ha pronunciado.</p>

                    <p>—Que sea correcta, entonces —dije, mientras me levantaba,
                    considerando inútil continuar un discurso que era todo oscuridad
                    para mí; y, además, sensible de que el carácter de mi interlocutor
                    estaba más allá de mi penetración; al menos, más allá de su alcance
                    actual; y sintiendo la incertidumbre, la vaga sensación de
                    inseguridad, que acompaña a una convicción de ignorancia.</p>

                    <p>—¿Adónde va?</p>

                    <p>—A acostar a Adèle. Ya pasó su hora de dormir.</p>

                    <p>—Me tiene miedo, porque hablo como una esfinge.</p>

                    <p>—Su lenguaje es enigmático, señor. Pero aunque estoy
                    desconcertada, ciertamente no tengo miedo.</p>

                    <p>—Tiene miedo. Su amor propio teme cometer un error.</p>

                    <p>—En ese sentido sí me siento aprensiva, no deseo decir tonterías.</p>

                    <p>—Si las dijera, sería de una manera tan grave y tranquila que las
                    confundiría con sensatez. ¿Nunca ríe, señorita Eyre? No se moleste
                    en responder, veo que ríe rara vez; pero puede reír muy
                    alegremente. Créame, no es usted naturalmente austera, como
                    tampoco yo soy naturalmente vicioso. La contención de Lowood
                    todavía se le adhiere un poco; controlando sus rasgos, amortiguando
                    su voz y restringiendo sus miembros. Y teme, en presencia de un
                    hombre y un hermano —o padre, o amo, o lo que quiera—, sonreír
                    demasiado alegremente, hablar demasiado libremente o moverse
                    demasiado rápido. Pero, con el tiempo, creo que aprenderá a ser
                    natural conmigo, como yo encuentro imposible ser convencional con
                    usted; y entonces sus miradas y movimientos tendrán más vivacidad
                    y variedad de las que se atreven a ofrecer ahora. Veo a intervalos la
                    mirada de una curiosa especie de pájaro a través de los barrotes
                    apretados de una jaula. Hay allí un cautivo vívido, inquieto, resuelto.
                    Si tan solo fuera libre, se elevaría hasta las nubes. ¿Sigue empeñada
                    en irse?</p>

                    <p>—Han dado las nueve, señor.</p>

                    <p>—No importa, espere un minuto. Adèle aún no está lista para irse
                    a la cama. Mi posición, señorita Eyre, con la espalda al fuego y la
                    cara a la habitación, favorece la observación. Mientras hablaba con
                    usted, también he observado ocasionalmente a Adèle (tengo mis
                    propias razones para considerarla un estudio curioso, razones que
                    puedo, no, que le comunicaré algún día). Sacó de su caja, hace unos
                    diez minutos, un pequeño vestido de seda rosa; el éxtasis iluminó su
                    rostro mientras lo desplegaba. La coquetería corre por su sangre, se
                    mezcla con su cerebro y sazona la médula de sus huesos. «<i>Il faut
                    que je l'essaie!</i>», exclamó, «<i>et à l'instant même!</i>». Y salió corriendo
                    de la habitación. Ahora está con Sophie, sometiéndose a un proceso
                    de vestimenta. En pocos minutos volverá a entrar; y sé lo que veré:
                    una miniatura de Céline Varens, como solía aparecer en el escenario
                    al levantarse... Pero no importa eso. Sin embargo, mis sentimientos
                    más tiernos están a punto de recibir un golpe. Tal es mi
                    presentimiento; quédese ahora, para ver si se cumple.</p>

                    <p>En poco tiempo, se oyó el piececito de Adèle correteando por el
                    vestíbulo. Entró, transformada como su guardián había predicho. Un
                    vestido de satén color de rosa, muy corto y con la falda tan fruncida
                    como podía estar, reemplazó el vestido marrón que había llevado
                    previamente; una guirnalda de capullos de rosa ceñía su frente; sus
                    pies estaban vestidos con medias de seda y pequeñas sandalias de
                    satén blanco.</p>

                    <p>—<i>Est-ce que ma robe va bien?</i>—exclamó, saltando hacia adelante
                    —;<i>et mes souliers? et mes bas? Tenez, je crois que je vais danser!</i></p>

                    <p>Y extendiendo su vestido, hizo un chassé a través de la habitación
                    hasta que, habiendo llegado al señor Rochester, giró ligeramente
                    sobre las puntas de los pies ante él, y luego se arrodilló a sus pies,
                    exclamando:</p>

                    <p>—<i>Monsieur, je vous remercie mille fois de votre bonté.</i>—Luego,
                    levantándose, añadió—;<i>C'est comme cela que maman faisait, n'estce
                    pas, monsieur?</i></p>

                    <p>—¡Pre-ci-sa-men-te! —fue la respuesta—. Y, «<i>comme cela</i>»,
                    encantó mi oro inglés fuera del bolsillo de mis calzones británicos. Yo
                    también he sido verde, señorita Eyre, sí, verde hierba. Ningún tinte
                    más primaveral la refresca a usted ahora del que una vez me
                    refrescó a mí. Mi primavera se ha ido, sin embargo, pero me ha
                    dejado esa florecilla francesa en mis manos, de la que, en algunos
                    humores, de buena gana me desharía. No valorando ahora la raíz de
                    la que brotó, habiendo descubierto que era de una especie que nada
                    más que polvo de oro podía abonar, solo tengo un aprecio a medias
                    por la flor, especialmente cuando parece tan artificial como ahora
                    mismo. La conservo y la crío más bien por el principio católico
                    romano de expiar numerosos pecados, grandes o pequeños, con una
                    buena obra. Ya le explicaré todo esto algún día. Buenas noches.</p>

