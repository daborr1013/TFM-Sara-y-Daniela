                <h1>Capítulo XXIV</h1>
                
                <p>Mientras me levantaba y me vestía, pensé en lo que había sucedido
                y me pregunté si era un sueńo. No podía estar segura de la realidad
                hasta que hubiera visto de nuevo al seńor Rochester y lo hubiera
                oído renovar sus palabras de amor y promesa.</p>

                <p>Mientras me arreglaba el pelo, me miré la cara en el espejo y sentí
                que ya no era sencilla. Había esperanza en su aspecto y vida en su
                color; y mis ojos parecían como si hubieran contemplado la fuente
                de la fruición y hubieran tomado prestados rayos de la lustrosa
                onda. A menudo había sido reacia a mirar a mi amo, porque temía
                que no pudiera agradarle mi aspecto; pero estaba segura de que
                ahora podría levantar mi rostro hacia el suyo y no enfriar su afecto
                con su expresión. Saqué de mi cajón un vestido de verano sencillo
                pero limpio y ligero y me lo puse. Parecía que ningún atuendo me
                había sentado nunca tan bien, porque ninguno había llevado nunca
                con un humor tan dichoso.</p>

                <p>No me sorprendió, cuando bajé corriendo al vestíbulo, ver que una
                brillante mańana de junio había sucedido a la tempestad de la
                noche; y sentir, a través de la puerta de cristal abierta, el aliento de
                una brisa fresca y fragante. La naturaleza debía estar alegre cuando
                yo era tan feliz. Una mendiga y su hijito —ambos objetos pálidos y
                harapientos— subían por el paseo, y corrí y les di todo el dinero que
                casualmente tenía en mi monedero, unos tres o cuatro chelines.
                Buenos o malos, debían participar de mi júbilo. Las grajas graznaban
                y pájaros más alegres cantaban; pero nada era tan alegre ni tan
                musical como mi propio corazón regocijado.</p>

                <p>La seńora Fairfax me sorprendió al mirar por la ventana con un
                semblante triste y decir gravemente:</p>

                <p>—Seńorita Eyre, żquiere venir a desayunar? —Durante la comida
                estuvo silenciosa y fría. Pero no podía desengańarla entonces. Debía
                esperar a que mi amo diera explicaciones; y ella también. Comí lo
                que pude y luego me apresuré a subir. Me encontré a Adčle saliendo
                del aula.</p>

                <p>—żAdónde vas? Es hora de las lecciones.</p>

                <p>—El seńor Rochester me ha mandado al cuarto de los nińos.</p>

                <p>—żDónde está él?</p>

                <p>—Ahí dentro —dijo, seńalando el apartamento que había dejado.
                Entré y allí estaba él.</p>

                <p>—Ven a darme los buenos días —dijo. Avancé con gusto; y no fue
                meramente una palabra fría ahora, ni siquiera un apretón de manos
                lo que recibí, sino un abrazo y un beso. Parecía natural; parecía
                cordial ser tan bien amada, tan acariciada por él.</p>

                <p>—Jane, pareces radiante, y sonriente, y bonita —dijo—.
                Verdaderamente bonita esta mańana. żEs este mi pálido y pequeńo
                duende? żEs esta mi semilla de mostaza? żEsta nińa de rostro
                soleado con la mejilla con hoyuelos y los labios sonrosados; el
                cabello avellana liso como el satén y los radiantes ojos avellana?
                (Tenía los ojos verdes, lector; pero debe excusar el error; para él
                estaban recién teńidos, supongo).</p>

                <p>—Es Jane Eyre, seńor.</p>

                <p>—Pronto será Jane Rochester —ańadió—. En cuatro semanas,
                Janet; ni un día más. żOyes eso?</p>

                <p>Lo oí, y no pude comprenderlo del todo. Me mareó. El
                sentimiento, el anuncio que me recorrió, fue algo más fuerte de lo
                que era consistente con la alegría, algo que golpeó y aturdió. Fue,
                creo, casi miedo.</p>

                <p>—Te sonrojaste, y ahora estás blanca, Jane. żA qué se debe eso?</p>

                <p>—Porque me ha dado un nuevo nombre, Jane Rochester; y parece tan extrańo.</p>

                <p>—Sí, seńora Rochester —dijo—. La joven seńora Rochester, la
                nińa-novia de Fairfax Rochester.</p>

                <p>—Nunca puede ser, seńor; no suena probable. Los seres humanos
                nunca disfrutan de una felicidad completa en este mundo. No nací
                para un destino diferente al del resto de mi especie. Imaginar que
                tal suerte me acontece es un cuento de hadas, un ensueńo.</p>

                <p>—Que puedo y realizaré. Empezaré hoy mismo. Esta mańana
                escribí a mi banquero en Londres para que me enviara ciertas joyas
                que tiene en su custodia, reliquias familiares para las damas de
                Thornfield. En uno o dos días espero verterlas en tu regazo; pues
                todo privilegio, toda atención será tuya, la que concedería a la hija
                de un par, si estuviera a punto de casarme con ella.</p>

                <p>—ˇOh, seńor! ˇNunca lluevan joyas! No me gusta oír hablar de
                ellas. Joyas para Jane Eyre suena antinatural y extrańo. Preferiría no
                tenerlas.</p>

                <p>—Yo mismo te pondré la cadena de diamantes alrededor del cuello
                y la diadema en la frente, que te sentará bien, pues la naturaleza, al
                menos, ha estampado su patente de nobleza en esta frente, Jane; y
                abrocharé los brazaletes en estas finas muńecas y cargaré estos
                dedos de hada con anillos.</p>

                <p>—ˇNo, no, seńor! Piense en otros temas y hable de otras cosas, y
                en otro tono. No se dirija a mí como si fuera una belleza; soy su
                institutriz sencilla y cuáquera.</p>

                <p>—Eres una belleza a mis ojos, y una belleza justo como el deseo
                de mi corazón: delicada y etérea.</p>

                <p>—Insignificante y menuda, quiere decir. Está usted sońando,
                seńor, o se está burlando. ˇPor el amor de Dios, no sea irónico!</p>

                <p>—Haré que el mundo te reconozca como una belleza también —
                continuó, mientras yo realmente me inquietaba por el tono que
                había adoptado, porque sentía que o se estaba engańando a sí
                mismo o intentaba engańarme a mí—. Vestiré a mi Jane de satén y
                encaje, y tendrá rosas en el pelo; y cubriré la cabeza que más amo
                con un velo de valor incalculable.</p>

                <p>—Y entonces no me reconocerá, seńor; y ya no seré su Jane Eyre,
                sino un mono con chaqueta de arlequín, un arrendajo con plumas
                prestadas. Preferiría verlo a usted, seńor Rochester, ataviado con
                ropajes de teatro, que a mí misma vestida con el traje de una dama
                de la corte; y no lo llamo a usted guapo, seńor, aunque lo amo con
                todo mi corazón, demasiado para halagarlo. No me halague.</p>

                <p>Continuó con su tema, sin embargo, sin hacer caso de mi desaprobación.</p>
                
                <p>—Este mismo día te llevaré en el carruaje a Millcote, y debes
                elegir algunos vestidos para ti. Te dije que nos casaremos en cuatro
                semanas. La boda tendrá lugar discretamente, en la iglesia de allá
                abajo; y luego te llevaré de inmediato a la ciudad. Después de una
                breve estancia allí, llevaré mi tesoro a regiones más cercanas al sol:
                a los vińedos franceses y a las llanuras italianas; y ella verá todo lo
                que es famoso en la historia antigua y en los anales modernos.
                Probará también la vida de las ciudades; y aprenderá a valorarse a sí
                misma por justa comparación con los demás.</p>

                <p>—żViajaré? żY con usted, seńor?</p>

                <p>—Residirás en París, Roma y Nápoles; en Florencia, Venecia y
                Viena. Todo el terreno que he recorrido será pisado de nuevo por ti.
                Dondequiera que yo estampé mi casco, tu pie de sílfide también
                pisará. Hace diez ańos, volé por Europa medio loco; con asco, odio e
                ira como compańeros. Ahora la revisitaré sanado y limpio, con un
                verdadero ángel como mi consoladora.</p>

                <p>Me reí de él cuando dijo esto.</p>

                <p>—No soy un ángel —afirmé—. Y no lo seré hasta que muera. Seré
                yo misma. Seńor Rochester, no debe esperar ni exigir nada celestial
                de mí, porque no lo obtendrá, como tampoco yo lo obtendré de
                usted, lo cual no anticipo en absoluto.</p>

                <p>—żQué anticipas de mí?</p>

                <p>—Por un tiempito quizás serás como eres ahora, un tiempito muy
                pequeńo; y luego te volverás frío; y luego serás caprichoso; y luego
                serás severo, y tendré mucho que hacer para complacerte. Pero
                cuando te acostumbres bien a mí, quizás vuelvas a gustar de mí,
                gustar, digo, no amar. Supongo que tu amor se evaporará en seis
                meses, o menos. He observado en libros escritos por hombres que
                ese período se asigna como el más lejano al que se extiende el ardor
                de un marido. Sin embargo, después de todo, como amiga y
                compańera, espero no volverme nunca del todo desagradable para
                mi querido amo.</p>

                <p>—ˇDesagradable! ˇY gustarte de nuevo! Creo que me gustarás de
                nuevo, y otra vez. Y te haré confesar que no solo me gustas, sino
                que te amo, con verdad, fervor y constancia.</p>

                <p>—Sin embargo, żno es usted caprichoso, seńor?</p>

                <p>—Con las mujeres que me complacen solo por sus rostros, soy el
                mismo diablo cuando descubro que no tienen ni alma ni corazón,
                cuando me abren una perspectiva de insipidez, trivialidad y quizás
                imbecilidad, grosería y mal genio. Pero para el ojo claro y la lengua
                elocuente, para el alma hecha de fuego y el carácter que se dobla
                pero no se rompe —a la vez flexible y estable, dócil y consistente—,
                soy siempre tierno y fiel.</p>

                <p>—żTuvo alguna vez experiencia de tal carácter, seńor? żAmó
                alguna vez a alguien así?</p>

                <p>—La amo ahora.</p>

                <p>—Pero antes de mí, żsi es que yo, en algún aspecto, cumplo con
                su difícil estándar?</p>

                <p>—Nunca encontré a nadie como tú. Jane, me complaces y me
                dominas. Pareces someterte, y me gusta la sensación de flexibilidad
                que impartes; y mientras enrosco la suave madeja de seda alrededor
                de mi dedo, envía un escalofrío por mi brazo hasta mi corazón. Soy
                influenciado, conquistado; y la influencia es más dulce de lo que
                puedo expresar; y la conquista que sufro tiene una hechicería más
                allá de cualquier triunfo que pueda ganar. żPor qué sonríes, Jane?
                żQué significa ese giro inexplicable, casi sobrenatural, de tu
                semblante?</p>

                <p>—Estaba pensando, seńor (disculpará la idea; fue involuntaria),
                estaba pensando en Hércules y Sansón con sus encantadoras...</p>

                <p>—Lo estabas, pequeńo duende...</p>

                <p>—ˇSilencio, seńor! No habla usted muy sabiamente en este
                momento; como tampoco aquellos caballeros actuaron muy
                sabiamente. Sin embargo, de haberse casado, sin duda por su
                severidad como maridos habrían compensado su blandura como
                pretendientes; y así lo hará usted, me temo. Me pregunto cómo me
                responderá dentro de un ańo si le pido un favor que no se ajuste a
                su conveniencia o placer.</p>

                <p>—Pídeme algo ahora, Janet, lo más mínimo. Deseo que me lo supliquen...</p>

                <p>—De hecho lo haré, seńor; tengo mi petición lista.</p>

                <p>—ˇHabla! Pero si levantas la vista y sonríes con ese semblante,
                juraré concesión antes de saber a qué, y eso me hará parecer un
                tonto.</p>

                <p>—En absoluto, seńor; solo pido esto: no mande a buscar las joyas
                y no me corone de rosas. Bien podría poner un borde de encaje de
                oro alrededor de ese sencillo pańuelo que tiene ahí.</p>

                <p>—Bien podría «dorar el oro refinado». Lo sé. Tu petición está
                concedida, entonces, por el momento. Anularé la orden que
                despaché a mi banquero. Pero aún no has pedido nada; has rogado
                que se retire un regalo. Inténtalo de nuevo.</p>

                <p>—Bueno, entonces, seńor, tenga la bondad de satisfacer mi
                curiosidad, que está muy picada en un punto.</p>

                <p>Pareció perturbado.</p>

                <p>—żQué? żQué? —dijo apresuradamente—. La curiosidad es una
                petición peligrosa. Menos mal que no he hecho un voto de conceder
                cada petición...</p>

                <p>—Pero no puede haber peligro en cumplir con esta, seńor.</p>

                <p>—Dila, Jane. Pero desearía que, en lugar de una mera indagación
                sobre, quizás, un secreto, fuera un deseo de la mitad de mi
                patrimonio.</p>

                <p>—ˇAhora, rey Asuero! żQué quiero yo con la mitad de su
                patrimonio? żCree que soy un judío usurero, buscando una buena
                inversión en tierras? Preferiría mucho más tener toda su confianza.
                żNo me excluirá de su confianza si me admite en su corazón?</p>

                <p>—Eres bienvenida a toda mi confianza que valga la pena tener,
                Jane; pero, por el amor de Dios, ˇno desees una carga inútil! ˇNo
                anheles veneno, no resultes ser una auténtica Eva en mis manos!</p>

                <p>—żPor qué no, seńor? Acaba de decirme cuánto le gustaba ser
                conquistado y cuán placentera le resulta la persuasión. żNo cree que
                sería mejor que aprovechara la confesión y empezara a engatusar y
                suplicar, incluso a llorar y a estar de mal humor si es necesario, por
                el simple hecho de ensayar mi poder?</p>

                <p>—Te desafío a cualquier experimento de ese tipo. Extralimítate,
                presume, y el juego se acaba.</p>

                <p>—żDe veras, seńor? Se rinde usted pronto. ˇQué severo se ve
                ahora! Sus cejas se han vuelto tan gruesas como mi dedo, y su
                frente se asemeja a lo que, en una poesía muy asombrosa, vi una
                vez llamado «un altillo de truenos apilados en azul». żSerá esa su
                mirada de casado, seńor, supongo?</p>

                <p>—Si esa va a ser tu mirada de casada, yo, como cristiano, pronto
                abandonaré la idea de consorciarme con un mero duende o
                salamandra. Pero, żqué tenías que preguntar, criatura? ˇSuéltalo!</p>

                <p>—Vaya, ahora es usted menos que cortés; y me gusta mucho más
                la rudeza que la adulación. Prefiero ser una criatura que un ángel.
                Esto es lo que tengo que preguntar: żPor qué se esforzó tanto en
                hacerme creer que deseaba casarse con la seńorita Ingram?</p>

                <p>—żEso es todo? ˇGracias a Dios que no es peor! —Y ahora
                desfrunció sus negras cejas; bajó la vista, sonriéndome, y me
                acarició el pelo, como si estuviera muy complacido de ver un peligro
                evitado—. Creo que puedo confesar —continuó—, aunque te indigne
                un poco, Jane, y he visto qué espíritu de fuego puedes ser cuando
                estás indignada. Resplandeciste bajo la fría luz de la luna anoche,
                cuando te amotinaste contra el destino y reclamaste tu rango como
                mi igual. Janet, por cierto, fuiste tú quien me hizo la oferta.</p>

                <p>—Por supuesto que sí. Pero al grano, si le parece, seńor: żla
                seńorita Ingram?</p>

                <p>—Bueno, fingí cortejar a la seńorita Ingram porque deseaba que
                te enamoraras de mí tan locamente como yo lo estaba de ti; y sabía
                que los celos serían el mejor aliado que podría convocar para la
                consecución de ese fin.</p>

                <p>—ˇExcelente! Ahora es usted pequeńo, ni una pizca más grande
                que la punta de mi dedo meńique. Fue una vergüenza y una
                deshonra escandalosa actuar de esa manera. żNo pensó nada en los
                sentimientos de la seńorita Ingram, seńor?</p>

                <p>—Sus sentimientos se concentran en uno: el orgullo; y eso
                necesita ser humillado. żEstabas celosa, Jane?</p>

                <p>—No importa, seńor Rochester. No es de ningún interés para usted
                saberlo. Respóndame con sinceridad una vez más. żCree que la
                seńorita Ingram no sufrirá por su deshonesta coquetería? żNo se
                sentirá abandonada y desamparada?</p>

                <p>—ˇImposible! Cuando te conté cómo ella, por el contrario, me
                abandonó. La idea de mi insolvencia enfrió, o más bien extinguió, su
                llama en un momento.</p>

                <p>—Tiene usted una mente curiosa y calculadora, seńor Rochester.
                Me temo que sus principios en algunos puntos son excéntricos.</p>

                <p>—Mis principios nunca fueron entrenados, Jane. Puede que hayan
                crecido un poco torcidos por falta de atención.</p>

                <p>—Una vez más, seriamente. żPuedo disfrutar del gran bien que se
                me ha concedido, sin temer que nadie más esté sufriendo el amargo
                dolor que yo misma sentí hace un rato?</p>

                <p>—Puedes, mi buena nińa. No hay otro ser en el mundo que tenga
                el mismo amor puro por mí que tú, pues me aplico ese grato
                ungüento a mi alma, Jane, la creencia en tu afecto.</p>

                <p>Volví mis labios hacia la mano que descansaba en mi hombro. Lo
                amaba mucho, más de lo que podía atreverme a decir, más de lo
                que las palabras tenían poder para expresar.</p>

                <p>—Pide algo más —dijo al instante—. Es mi deleite que me
                supliquen y ceder.</p>

                <p>Estaba de nuevo lista con mi petición.</p>

                <p>—Comunique sus intenciones a la seńora Fairfax, seńor. Me vio
                con usted anoche en el vestíbulo y se escandalizó. Dele alguna
                explicación antes de que la vea de nuevo. Me duele ser mal juzgada
                por una mujer tan buena.</p>

                <p>—Ve a tu habitación y ponte el gorro —replicó—. Pienso que me
                acompańes a Millcote esta mańana; y mientras te preparas para el
                paseo, iluminaré el entendimiento de la anciana. żPensó, Janet, que
                habías dado el mundo por amor y lo considerabas bien perdido?</p>

                <p>—Creo que pensó que había olvidado mi posición y la suya, seńor.</p>

                <p>—ˇPosición! ˇPosición! Tu posición está en mi corazón y sobre los
                cuellos de aquellos que te insultarían, ahora o en el futuro. ˇVe!</p>

                <p>Pronto estuve vestida; y cuando oí al seńor Rochester salir del
                salón de la seńora Fairfax, me apresuré a bajar. La anciana había
                estado leyendo su porción matutina de las Escrituras, la Lección del
                día; su Biblia yacía abierta ante ella y sus gafas estaban sobre ella.
                Su ocupación, suspendida por el anuncio del seńor Rochester,
                parecía ahora olvidada. Sus ojos, fijos en la pared en blanco de
                enfrente, expresaban la sorpresa de una mente tranquila agitada por
                noticias insólitas. Al verme, se reanimó. Hizo una especie de
                esfuerzo por sonreír y formuló unas pocas palabras de felicitación;
                pero la sonrisa expiró y la frase quedó abandonada sin terminar. Se
                guardó las gafas, cerró la Biblia y apartó su silla de la mesa.</p>

                <p>—Me siento tan asombrada —comenzó—, que apenas sé qué
                decirle, seńorita Eyre. Seguramente no he estado sońando, żverdad?
                A veces me quedo medio dormida cuando estoy sentada sola e
                imagino cosas que nunca han sucedido. Me ha parecido más de una
                vez, cuando he estado en un duermevela, que mi querido esposo,
                que murió hace quince ańos, ha entrado y se ha sentado a mi lado;
                y que incluso lo he oído llamarme por mi nombre, Alice, como solía
                hacer. Ahora, żpuede decirme si es realmente cierto que el seńor
                Rochester le ha pedido que se case con él? No se ría de mí. Pero
                realmente pensé que entró aquí hace cinco minutos y dijo que en un
                mes sería usted su esposa.</p>

                <p>—Me ha dicho lo mismo a mí —repliqué.</p>

                <p>—ˇLo ha hecho! żLe cree? żLo ha aceptado?</p>

                <p>—Sí.</p>

                <p>Me miró desconcertada.</p>

                <p>—Nunca podría haberlo pensado. Es un hombre orgulloso; todos
                los Rochester eran orgullosos. Y su padre, al menos, gustaba del
                dinero. A él también siempre se le ha llamado cuidadoso. żPretende
                casarse con usted?</p>

                <p>—Así me lo dice.</p>

                <p>Examinó toda mi persona. En sus ojos leí que no habían
                encontrado allí ningún encanto lo suficientemente poderoso como
                para resolver el enigma.</p>

                <p>—ˇMe supera! —continuó—. Pero sin duda es cierto, ya que usted
                lo dice. Cómo resultará, no puedo decirlo; realmente no lo sé. La
                igualdad de posición y fortuna suele ser aconsejable en tales casos;
                y hay veinte ańos de diferencia en sus edades. Casi podría ser su
                padre.</p>

                <p>—ˇNo, en absoluto, seńora Fairfax! —exclamé, irritada—. ˇNo se
                parece en nada a mi padre! Nadie, que nos viera juntos, lo
                supondría por un instante. El seńor Rochester parece tan joven, y es
                tan joven, como algunos hombres a los veinticinco.</p>

                <p>—żEs realmente por amor que se va a casar con usted? — preguntó.</p>

                <p>Me sentí tan herida por su frialdad y escepticismo que las lágrimas
                asomaron a mis ojos.</p>

                <p>—Lamento afligirla —prosiguió la viuda—. Pero es usted tan joven
                y tan poco conocedora de los hombres, que deseaba ponerla en
                guardia. Es un viejo dicho que “no es oro todo lo que reluce”; y en
                este caso temo mucho que se descubra algo diferente a lo que usted
                o yo esperamos.</p>

                <p>—żPor qué? żSoy un monstruo? —dije—. żEs imposible que el
                seńor Rochester sienta un afecto sincero por mí?</p>

                <p>—No. Está usted muy bien, y ha mejorado mucho últimamente; y
                el seńor Rochester, me atrevo a decir, le tiene afecto. Siempre he
                notado que era una especie de mascota suya. Hay momentos en
                que, por su bien, me he sentido un poco inquieta por su marcada
                preferencia, y he deseado ponerla en guardia. Pero no me gustaba
                sugerir siquiera la posibilidad de un mal. Sabía que tal idea la
                escandalizaría, quizás la ofendería; y era usted tan discreta, y tan
                completamente modesta y sensata, que esperaba que se pudiera
                confiar en que se protegiera a sí misma. Anoche no puedo decirle lo
                que sufrí cuando la busqué por toda la casa y no pude encontrarla
                en ninguna parte, ni al amo tampoco; y luego, a las doce, la vi
                entrar con él.</p>

                <p>—Bueno, no importa eso ahora —interrumpí con impaciencia—.
                Basta con que todo estuviera bien.</p>

                <p>—Espero que todo esté bien al final —dijo—. Pero créame, no
                puede ser demasiado cuidadosa. Intente mantener al seńor
                Rochester a distancia. Desconfíe de sí misma tanto como de él. Los
                caballeros de su posición no acostumbran a casarse con sus
                institutrices.</p>

                <p>Me estaba irritando de verdad. Felizmente, Adčle entró corriendo.</p>

                <p>—ˇDéjeme ir, déjeme ir a Millcote también! —gritó—. El seńor
                Rochester no quiere, aunque hay tanto sitio en el nuevo carruaje.
                ˇPídale que me deje ir, <i>mademoiselle</i>!</p>

                <p>—Eso haré, Adčle. —Y me apresuré a salir con ella, contenta de
                abandonar a mi sombría monitora. El carruaje estaba listo; lo
                estaban trayendo al frente, y mi amo paseaba por el pavimento, con
                Pilot siguiéndolo de un lado a otro.</p>

                <p>—Adčle puede acompańarnos, żno es así, seńor?</p>

                <p>—Le dije que no. ˇNo quiero mocosos! ˇSolo te quiero a ti!</p>

                <p>—Deje que vaya, seńor Rochester, por favor. Sería mejor.</p>

                <p>—No lo es. Será un estorbo.</p>

                <p>Fue bastante perentorio, tanto en la mirada como en la voz. El frío
                de las advertencias de la seńora Fairfax y la humedad de sus dudas
                estaban sobre mí. Algo de insustancialidad e incertidumbre había
                asediado mis esperanzas. Perdí a medias el sentido de poder sobre
                él. Estaba a punto de obedecerle mecánicamente, sin más réplica;
                pero mientras me ayudaba a subir al carruaje, me miró la cara.</p>

                <p>—żQué pasa? —preguntó—. Todo el sol se ha ido. żDe verdad
                quieres que vaya la nińa? żTe molestará si se queda atrás?</p>

                <p>—Preferiría con mucho que fuera, seńor.</p>

                <p>—ˇEntonces, a por tu gorro, y vuelve como un relámpago! —le
                gritó a Adčle.</p>

                <p>Ella le obedeció con la mayor rapidez que pudo.</p>

                <p>—Después de todo, una sola interrupción matutina no importará
                mucho —dijo él—, cuando pretendo reclamarte en breve —tus
                pensamientos, conversación y compańía— de por vida.</p>

                <p>Adčle, al ser subida, comenzó a besarme, a modo de expresar su
                gratitud por mi intercesión. Fue instantáneamente acomodada en un
                rincón al otro lado de él. Luego se asomó a donde yo estaba
                sentada. Un vecino tan severo era demasiado restrictivo. A él, en su
                actual humor irritable, no se atrevía a susurrarle ninguna
                observación, ni a pedirle ninguna información.</p>

                <p>—Déjela que venga conmigo —supliqué—. Quizás le moleste, seńor. Hay mucho sitio a este lado.</p>

                <p>Me la entregó como si hubiera sido un perrito faldero.</p>

                <p>—La enviaré a la escuela todavía —dijo, pero ahora sonreía.</p>

                <p>Adčle lo oyó y preguntó si iba a ir a la escuela «<i>sans mademoiselle</i>».</p>

                <p>—Sí —replicó—, absolutamente <i>sans mademoiselle</i>; porque voy a
                llevar a <i>mademoiselle</i> a la luna, y allí buscaré una cueva en uno de
                los valles blancos entre las cimas de los volcanes, y <i>mademoiselle</i>
                vivirá conmigo allí, y solo conmigo.</p>

                <p>—No tendrá nada que comer. La matará de hambre —observó Adčle.</p>

                <p>—Recogeré maná para ella mańana y noche. Las llanuras y
                laderas de la luna están blanqueadas de maná, Adčle.</p>

                <p>—Querrá calentarse. żQué hará para tener fuego?</p>

                <p>—El fuego surge de las montańas lunares. Cuando tenga frío, la
                llevaré a una cima y la acostaré al borde de un cráter.</p>

                <p>—<i>Oh, qu'elle y sera mal, peu confortable!</i> Y su ropa, se gastará.
                żCómo conseguirá una nueva?</p>

                <p>El seńor Rochester profesó estar perplejo.</p>

                <p>—ˇHum! —dijo—. żQué harías tú, Adčle? Estruja tus sesos en
                busca de un expediente. żQué tal una nube blanca o rosa para un
                vestido, crees? Y se podría cortar una bufanda bastante bonita de un
                arcoíris.</p>

                <p>—Está mucho mejor como está —concluyó Adčle, después de
                meditar un rato—. Además, se cansaría de vivir solo contigo en la
                luna. Si yo fuera <i>mademoiselle</i>, nunca consentiría en ir contigo.</p>

                <p>—Ha consentido. Ha empeńado su palabra.</p>

                <p>—Pero no puedes llevarla allí; no hay camino a la luna. Es todo
                aire; y ni tú ni ella podéis volar.</p>

                <p>—Adčle, mira ese campo. —Estábamos ahora fuera de las verjas
                de Thornfield y rodando ligeramente por el suave camino hacia
                Millcote, donde el polvo estaba bien asentado por la tormenta y
                donde los setos bajos y los altos árboles a cada lado brillaban verdes
                y refrescados por la lluvia.</p>

                <p>—En ese campo, Adčle, estaba yo paseando tarde una noche hace
                unas dos semanas, la tarde del día en que me ayudaste a segar el
                heno en los prados del huerto; y, como estaba cansado de rastrillar
                hileras, me senté a descansar en una estela; y allí saqué un librito y
                un lápiz, y empecé a escribir sobre una desgracia que me ocurrió
                hace mucho tiempo y un deseo que tenía de que vinieran días
                felices. Estaba escribiendo muy rápido, aunque la luz del día se
                desvanecía de la hoja, cuando algo subió por el sendero y se detuvo
                a dos yardas de mí. Lo miré. Era una cosita con un velo de gasa en
                la cabeza. Le hice seńas para que se acercara; pronto estuvo a mi
                rodilla. Nunca le hablé, ni ella me habló a mí, con palabras; pero leí
                sus ojos, y ella leyó los míos; y nuestro coloquio sin palabras fue a
                este efecto:</p>

                <p>»Era un hada, y venía de la tierra de los Elfos, dijo; y su recado
                era hacerme feliz. Debía ir con ella fuera del mundo común a un
                lugar solitario, como la luna, por ejemplo, y asintió con la cabeza
                hacia su cuerno, que se alzaba sobre Hay-hill. Me habló de la cueva
                de alabastro y el valle de plata donde podríamos vivir. Dije que me
                gustaría ir; pero le recordé, como tú a mí, que no tenía alas para
                volar.</p>

                <p>»“ˇOh!”, respondió el hada, “ˇeso no importa! Aquí tienes un
                talismán que eliminará todas las dificultades”. Y extendió un bonito
                anillo de oro. “Póntelo”, dijo, “en el cuarto dedo de mi mano
                izquierda, y soy tuya, y tú eres mío; y dejaremos la tierra y haremos
                nuestro propio cielo allá arriba”. Asintió de nuevo hacia la luna. El
                anillo, Adčle, está en el bolsillo de mis calzones, bajo el disfraz de un
                soberano. Pero pienso pronto cambiarlo de nuevo por un anillo.</p>
                
                <p>—żPero qué tiene que ver <i>mademoiselle</i> con eso? No me importa
                el hada. żDijo usted que era a <i>mademoiselle</i> a quien llevaría a la
                luna?</p>

                <p>—<i>Mademoiselle</i> es un hada —dijo, susurrando misteriosamente.
                Con lo cual le dije que no hiciera caso de sus bromas; y ella, por su
                parte, demostró un fondo de genuino escepticismo francés,
                denominando al seńor Rochester «<i>un vrai menteur</i>» y asegurándole
                que no hacía ningún caso de sus «<i>contes de fée</i>», y que «<i>du reste, il
                n'y avait pas de fées, et quand męme il y en avait</i>», estaba segura
                de que nunca se le aparecerían a él, ni le darían anillos, ni le
                ofrecerían vivir con él en la luna.</p>

                <p>La hora pasada en Millcote fue algo agotadora para mí. El seńor
                Rochester me obligó a ir a un cierto almacén de sedas. Allí se me
                ordenó elegir media docena de vestidos. Odiaba el asunto, supliqué
                permiso para posponerlo. No, debía hacerse ahora. A fuerza de
                súplicas expresadas en enérgicos susurros, reduje la media docena a
                dos. Estos, sin embargo, juró que los elegiría él mismo. Con
                ansiedad observé su ojo vagar por las alegres existencias. Se fijó en
                una rica seda del más brillante tinte amatista y un soberbio satén
                rosa. Le dije en una nueva serie de susurros que bien podría
                comprarme un vestido de oro y un gorro de plata de una vez.
                Ciertamente nunca me atrevería a llevar su elección. Con infinita
                dificultad, pues era terco como una piedra, lo persuadí de que
                hiciera un cambio en favor de un sobrio satén negro y una seda gris
                perla. «Podría pasar por el momento», dijo, «pero aún me vería
                reluciente como un parterre».</p>

                <p>Contenta estuve de sacarlo del almacén de sedas y luego de una
                joyería. Cuanto más me compraba, más me ardía la mejilla con una
                sensación de molestia y degradación. Al volver a entrar en el
                carruaje, y sentarme atrás, febril y fatigada, recordé lo que, en el
                apuro de los acontecimientos, oscuros y brillantes, había olvidado
                por completo: la carta de mi tío, John Eyre, a la seńora Reed; su
                intención de adoptarme y hacerme su legataria. «Sería, en verdad,
                un alivio», pensé, «si tuviera aunque fuera una pequeńa
                independencia. Nunca podré soportar ser vestida como una muńeca
                por el seńor Rochester, o sentarme como una segunda Dánae con la
                lluvia dorada cayendo diariamente a mi alrededor. Escribiré a
                Madeira en el momento en que llegue a casa y le diré a mi tío John
                que voy a casarme y con quién. Si tan solo tuviera la perspectiva de
                un día traerle al seńor Rochester un aumento de fortuna, podría
                soportar mejor que me mantuviera ahora». Y algo aliviada por esta
                idea (que no dejé de ejecutar ese día), me aventuré una vez más a
                encontrar la mirada de mi amo y amante, que buscaba la mía con la
                mayor pertinacia, aunque yo apartaba tanto el rostro como la
                mirada. Él sonrió; y pensé que su sonrisa era como la que un sultán
                podría, en un momento dichoso y afectuoso, otorgar a una esclava a
                la que su oro y sus gemas habían enriquecido. Apreté su mano, que
                siempre buscaba la mía, vigorosamente, y se la devolví enrojecida
                por la apasionada presión.</p>

                <p>—No necesita mirar de esa manera —dije—. Si lo hace, no llevaré
                más que mis viejos vestidos de Lowood hasta el final del capítulo.
                Me casaré con este percal lila. Puede hacerse una bata para usted
                con la seda gris perla y una serie infinita de chalecos con el satén
                negro.</p>

                <p>Se rio entre dientes; se frotó las manos.</p>

                <p>—ˇOh, es un deleite verla y oírla! —exclamó—. żEs original? żEs
                picante? ˇNo cambiaría a esta pequeńa inglesa por todo el serrallo
                del Gran Turco, con ojos de gacela, formas de hurí y todo!</p>

                <p>La alusión oriental volvió a picarme.</p>

                <p>—No le serviré ni un ápice en lugar de un serrallo —dije—. Así que
                no me considere un equivalente de uno. Si tiene usted afición por
                algo de ese tipo, váyase, seńor, a los bazares de Estambul sin
                demora y gaste en extensas compras de esclavas parte de ese
                dinero extra que parece no saber cómo gastar satisfactoriamente
                aquí.</p>

                <p>—żY qué harás tú, Janet, mientras yo regateo por tantas
                toneladas de carne y tal surtido de ojos negros?</p>

                <p>—Me prepararé para salir como misionera a predicar la libertad a
                los que están esclavizados, entre ellos las internas de su harén. Me
                admitirán allí y promoveré un motín; y usted, bajá de tres colas
                como es, seńor, se encontrará en un santiamén encadenado entre
                nuestras manos. Y yo, por mi parte, no consentiré en cortar sus
                ataduras hasta que haya firmado una carta de derechos, la más
                liberal que déspota alguno haya conferido jamás.</p>

                <p>—Consentiría en estar a tu merced, Jane.</p>

                <p>—No tendría piedad, seńor Rochester, si suplicara por ella con una
                mirada como esa. Mientras mirara así, estaría segura de que
                cualquier carta de derechos que pudiera otorgar bajo coacción, su
                primer acto, al ser liberado, sería violar sus condiciones.</p>

                <p>—Vaya, Jane, żqué quieres? Me temo que me obligarás a pasar
                por una ceremonia de matrimonio privada, además de la celebrada
                en el altar. Estipularás, ya veo, condiciones peculiares. żCuáles
                serán?</p>

                <p>—Solo quiero una mente tranquila, seńor; no aplastada por
                obligaciones acumuladas. żRecuerda lo que dijo de Céline Varens, de
                los diamantes, las cachemiras que le dio? No seré su Céline Varens
                inglesa. Seguiré actuando como institutriz de Adčle; con eso ganaré
                mi manutención y alojamiento, y treinta libras al ańo además.
                Amueblaré mi propio guardarropa con ese dinero, y usted no me
                dará nada más que...</p>

                <p>—Bueno, żpero qué?</p>

                <p>—Su aprecio; y si yo le doy el mío a cambio, esa deuda quedará saldada.</p>

                <p>—Bueno, por descaro innato y puro orgullo innato, no tienes igual
                —dijo. Nos acercábamos ahora a Thornfield—. żTendrá el gusto de
                cenar conmigo hoy? —preguntó, mientras volvíamos a entrar por las
                verjas.</p>

                <p>—No, gracias, seńor.</p>

                <p>—żY por qué «no, gracias», si se puede preguntar?</p>

                <p>—Nunca he cenado con usted, seńor, y no veo razón para hacerlo
                ahora. Hasta...</p>

                <p>—żHasta qué? Le encantan las medias frases.</p>

                <p>—Hasta que no pueda evitarlo.</p>

                <p>—żSupone que como como un ogro o un gul, que teme ser la
                compańera de mi rancho?</p>

                <p>—No he formado ninguna suposición al respecto, seńor; pero
                quiero seguir como de costumbre durante otro mes.</p>

                <p>—Abandonará su esclavitud de institutriz de inmediato.</p>

                <p>—De hecho, con su perdón, seńor, no lo haré. Seguiré con ella
                como de costumbre. Me mantendré fuera de su camino todo el día,
                como he acostumbrado a hacer. Puede mandarme a buscar por la
                tarde, cuando se sienta dispuesto a verme, y entonces vendré; pero
                en ningún otro momento.</p>

                <p>—Necesito un cigarro, Jane, o una pizca de rapé, para consolarme
                bajo todo esto, «<i>pour me donner une contenance</i>», como diría
                Adčle; y desafortunadamente no tengo ni mi cigarrera ni mi
                tabaquera. Pero escucha... susurra. Ahora es tu momento, pequeńa
                tirana, pero pronto será el mío; y una vez que te haya agarrado
                firmemente, para tenerte y retenerte, simplemente —hablando en
                sentido figurado— te sujetaré a una cadena como esta (tocando la
                cadena de su reloj). Sí, hermosa criaturita, te llevaré en mi pecho,
                no sea que mi joya se me pierda.</p>

                <p>Dijo esto mientras me ayudaba a bajar del carruaje, y mientras
                después bajaba a Adčle, yo entré en la casa y me retiré escaleras
                arriba.</p>

                <p>Me convocó debidamente a su presencia por la tarde. Le había
                preparado una ocupación; pues estaba decidida a no pasar todo el
                tiempo en una conversación tęte-ŕ-tęte. Recordaba su hermosa voz;
                sabía que le gustaba cantar, los buenos cantantes generalmente lo
                hacen. Yo no era una vocalista, y, a su juicio exigente, tampoco una
                música; pero me deleitaba escuchar cuando la interpretación era
                buena. Tan pronto como el crepúsculo, esa hora del romance,
                comenzó a bajar su estandarte azul y estrellado sobre la celosía, me
                levanté, abrí el piano y le supliqué, por el amor de Dios, que me
                diera una canción. Dijo que era una bruja caprichosa y que preferiría
                cantar en otro momento; pero afirmé que no había momento como
                el presente.</p>

                <p>—żMe gustaba su voz? —preguntó.</p>

                <p>—Mucho. —No era yo aficionada a mimar esa susceptible vanidad
                suya; pero por una vez, y por motivos de conveniencia, la calmaría y
                estimularía.</p>

                <p>—Entonces, Jane, debes tocar el acompańamiento.</p>

                <p>—Muy bien, seńor, lo intentaré.</p>

                <p>Lo intenté, pero al instante fui barrida del taburete y denominada
                «una pequeńa chapucera». Siendo empujada sin ceremonias a un
                lado —que era precisamente lo que yo deseaba—, usurpó mi lugar y
                procedió a acompańarse a sí mismo, pues sabía tocar tan bien como
                cantar. Me dirigí al hueco de la ventana. Y mientras estaba sentada
                allí y miraba los árboles inmóviles y el césped oscuro, con una dulce
                melodía se cantó en tonos melodiosos la siguiente estrofa:</p>

            
                <p class="centradoP">«El amor más verdadero que jamás corazón<br>
                Sintió en su núcleo encendido,<br>
                Hizo por cada vena, en rápido impulso,<br>
                La marea del ser verter.</p>

                <p class="centradoP">Su llegada era mi esperanza cada día,<br>
                Su partida era mi dolor;<br>
                La casualidad que sus pasos retrasaba<br>
                Era hielo en cada vena.</p>

                <p class="centradoP">Sońé que sería una dicha sin nombre,<br>
                Como amaba, ser amado;<br>
                Y a este objeto me aferré<br>
                Tan ciego como ansiosamente.</p>

                <p class="centradoP">Pero ancho como sin sendero era el espacio<br>
                Que yacía entre nuestras vidas,<br>
                Y peligroso como la carrera espumosa<br>
                De las olas verdes del océano.</p>

                <p class="centradoP">Y embrujado como un camino de ladrones<br>
                A través del yermo o el bosque;<br>
                Pues Poder y Derecho, y Angustia e Ira,<br>
                Entre nuestros espíritus se interponían.</p>

                <p class="centradoP">Peligros desafié; obstáculos desprecié;<br>
                Presagios desafié:<br>
                Cualquier cosa que amenazara, acosara, advirtiera,<br>
                La pasé impetuosamente.</p>

                <p class="centradoP">Avanzó mi arcoíris, rápido como la luz;<br>
                Volé como en un sueńo;<br>
                Pues gloriosa se alzó ante mi vista<br>
                Aquella hija de la Lluvia y el Resplandor.</p>

                <p class="centradoP">Aún brillante sobre nubes de sufrimiento oscuro<br>
                Brilla esa suave y solemne alegría;<br>
                Ni me importa ahora, cuán densos y sombríos<br>
                Los desastres se acumulen cerca.</p>

                <p class="centradoP">No me importa en este dulce momento,<br>
                Aunque todo lo que he arrollado<br>
                Viniera sobre alas, fuertes y veloces,<br>
                Proclamando venganza amarga:</p>

                <p class="centradoP">Aunque el Odio altivo me derribara,<br>
                El Derecho, me cerrara el paso,<br>
                Y el Poder opresor, con ceńo furioso,<br>
                Jurara enemistad eterna.</p>

                <p class="centradoP">Mi amor ha puesto su pequeńa mano<br>
                Con noble fe en la mía,<br>
                Y ha jurado que el sagrado lazo del matrimonio<br>
                Nuestra naturaleza entrelazará.</p>

                <p class="centradoP">Mi amor ha jurado, con beso sellador,<br>
                Conmigo vivir, morir;<br>
                Tengo al fin mi dicha sin nombre.<br>
                ˇComo amo, amado soy!».</p>

                <p>Se levantó y se acercó a mí, y vi su rostro todo encendido, y su
                ojo de halcón lleno, destellante, y ternura y pasión en cada rasgo.
                Vacilé momentáneamente, luego me recuperé. Escena tierna,
                demostración audaz, no las tendría; y estaba en peligro de ambas.
                Debía prepararse un arma de defensa; afilé mi lengua. Al
                alcanzarme, pregunté con aspereza, «żcon quién se iba a casar
                ahora?».</p>

                <p>—Esa era una extrańa pregunta para ser formulada por su querida Jane.</p>

                <p>—ˇDe veras! La consideré una muy natural y necesaria. Había
                hablado de que su futura esposa muriera con él. żQué quería decir
                con una idea tan pagana? Yo no tenía intención de morir con él, de
                eso podía estar seguro.</p>

                <p>—ˇOh, todo lo que anhelaba, todo por lo que rezaba, era que yo
                pudiera vivir con él! La muerte no era para alguien como yo.</p>

                <p>—De hecho, lo era. Tenía tanto derecho a morir cuando llegara mi
                hora como él. Pero esperaría ese momento y no sería arrastrada a
                un satí.</p>

                <p>—żMe perdonaría por la idea egoísta y demostraría su perdón con
                un beso reconciliador?</p>

                <p>—No. Preferiría ser excusada.</p>

                <p>Aquí me oí apostrofada como una «pequeńa criatura dura»; y se
                ańadió que «cualquier otra mujer se habría derretido hasta la
                médula al oír tales estrofas cantadas en su alabanza».</p>

                <p>Le aseguré que era naturalmente dura, muy pétrea, y que a
                menudo me encontraría así; y que, además, estaba decidida a
                mostrarle diversos puntos rudos de mi carácter antes de que
                transcurrieran las cuatro semanas siguientes. Debía saber
                plenamente qué clase de trato había hecho, mientras aún había
                tiempo de rescindirlo.</p>

                <p>—żEstaría tranquila y hablaría racionalmente?</p>

                <p>—Estaría tranquila si a él le gustaba, y en cuanto a hablar
                racionalmente, me halagaba de estar haciéndolo ahora.</p>

                <p>Se irritó, bufó y resopló. «Muy bien», pensé; «puedes echar humo
                y agitarte como quieras, pero este es el mejor plan a seguir contigo,
                estoy segura. Me gustas más de lo que puedo decir; pero no me
                hundiré en un batos de sentimentalismo; y con esta aguja de la
                réplica te mantendré alejado también del borde del abismo; y,
                además, mantendré con su ayuda punzante esa distancia entre tú y
                yo, la más propicia para nuestra verdadera ventaja mutua».</p>

                <p>De menos a más, lo llevé a una irritación considerable; luego,
                después de que se hubo retirado, de mal humor, al otro extremo de
                la habitación, me levanté y, diciendo: «Le deseo buenas noches,
                seńor», a mi manera natural y habitual de respeto, me deslicé por la
                puerta lateral y me escapé.</p>

                <p>El sistema así iniciado lo seguí durante toda la temporada de
                prueba; y con el mejor de los éxitos. Se le mantuvo, por supuesto,
                bastante malhumorado y cascarrabias; pero en general pude ver que
                estaba excelentemente entretenido, y que una sumisión de cordero
                y una sensibilidad de tórtola, aunque fomentarían más su
                despotismo, habrían complacido menos su juicio, satisfecho menos
                su sentido común e incluso adecuado menos a su gusto.</p>

                <p>En presencia de otras personas, yo era, como antes, deferente y
                tranquila; cualquier otra línea de conducta era innecesaria. Solo en
                las conferencias vespertinas lo contrariaba y afligía así. Continuó
                mandándome a buscar puntualmente en el momento en que el reloj
                daba las siete; aunque cuando aparecía ante él ahora, no tenía
                términos tan melosos como «amor» y «querida» en sus labios. Las
                mejores palabras a mi servicio eran «marioneta provocadora»,
                «duende malicioso», «espíritu», «nińo cambiado», etc. Por caricias,
                también, ahora recibía muecas; por un apretón de manos, un
                pellizco en el brazo; por un beso en la mejilla, un severo tirón de
                oreja. Todo estaba bien. En el presente prefería decididamente estos
                favores feroces a cualquier cosa más tierna. La seńora Fairfax, vi, me
                aprobaba. Su ansiedad por mi cuenta se desvaneció; por lo tanto,
                estaba segura de que hacía bien. Mientras tanto, el seńor Rochester
                afirmaba que lo estaba consumiendo hasta los huesos, y amenazaba
                con una venganza terrible por mi conducta actual en algún período
                que se acercaba rápidamente. Me reí para mis adentros de sus
                amenazas. «Puedo mantenerte en un control razonable ahora»,
                reflexioné; «y no dudo de poder hacerlo en el futuro. Si un
                expediente pierde su virtud, otro debe ser ideado».</p>

                <p>Sin embargo, después de todo, mi tarea no era fácil; a menudo
                habría preferido complacerlo que fastidiarlo. Mi futuro esposo se
                estaba convirtiendo para mí en mi mundo entero; y más que el
                mundo: casi mi esperanza del cielo. Se interponía entre mí y todo
                pensamiento de religión, como un eclipse se interpone entre el
                hombre y el ancho sol. No podía, en aquellos días, ver a Dios por Su
                criatura, de quien había hecho un ídolo.</p>

