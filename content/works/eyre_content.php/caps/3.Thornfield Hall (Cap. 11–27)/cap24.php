                        Jane
                    </span>, pareces radiante, y sonriente, y bonita —dijo—.
                    Verdaderamente bonita esta mañana. ¿Es este mi pálido y pequeño
                    duende? ¿Es esta mi semilla de mostaza? ¿Esta niña de rostro
