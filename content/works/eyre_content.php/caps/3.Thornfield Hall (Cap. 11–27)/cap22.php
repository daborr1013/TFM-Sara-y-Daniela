                <h1>Capítulo XXII</h1>
    
                <p>El señor Rochester me había concedido solo una semana de
                permiso; sin embargo, transcurrió un mes antes de que abandonara
                Gateshead. Deseaba marcharme inmediatamente después del
                funeral, pero Georgiana me suplicó que me quedara hasta que ella
                pudiera partir hacia Londres, adonde por fin había sido invitada por
                su tío, el señor Gibson, que había venido para dirigir el entierro de
                su hermana y arreglar los asuntos familiares. Georgiana dijo que
                temía quedarse sola con Eliza; de ella no obtenía ni simpatía en su
                abatimiento, ni apoyo en sus temores, ni ayuda en sus preparativos;
                así que soporté sus lamentos pusilánimes y sus quejas egoístas lo
                mejor que pude, e hice todo lo posible por coser para ella y empacar
                sus vestidos. Es cierto que, mientras yo trabajaba, ella
                holgazaneaba; y pensé para mis adentros: «Si tú y yo estuviéramos
                destinadas a vivir siempre juntas, prima, empezaríamos las cosas de
                otra manera. No me conformaría dócilmente con ser la parte
                tolerante; te asignaría tu parte del trabajo y te obligaría a cumplirla,
                o de lo contrario quedaría sin hacer. Insistiría, también, en que
                guardaras algunas de esas quejas arrastradas y medio insinceras en
                tu propio pecho. Es solo porque nuestra conexión resulta ser muy
                transitoria, y llega en una estación peculiarmente lúgubre, que
                consiento en hacerla tan paciente y complaciente por mi parte».</p>

                <p>Por fin despedí a Georgiana; pero ahora era el turno de Eliza de
                pedirme que me quedara otra semana. Sus planes requerían todo su
                tiempo y atención, dijo; estaba a punto de partir hacia algún destino
                desconocido; y durante todo el día permanecía en su propia
                habitación, con la puerta cerrada por dentro, llenando baúles,
                vaciando cajones, quemando papeles y sin mantener comunicación
                con nadie. Deseaba que yo me ocupara de la casa, recibiera a las
                visitas y respondiera a las notas de condolencia.</p>

                <p>Una mañana me dijo que era libre.</p>

                <p>—Y —añadió—, ¡le estoy agradecida por sus valiosos servicios y su
                conducta discreta! Hay alguna diferencia entre vivir con alguien
                como usted y con Georgiana. Usted cumple su propio papel en la
                vida y no es una carga para nadie. Mañana —continuó—, parto para
                el Continente. Fijaré mi residencia en una casa religiosa cerca de
                Lille, un convento lo llamarían ustedes. Allí estaré tranquila y sin ser
                molestada. Me dedicaré durante un tiempo al examen de los dogmas
                católicos romanos y a un estudio cuidadoso del funcionamiento de
                su sistema. Si descubro que es, como medio sospecho, el más
                adecuado para asegurar que todas las cosas se hagan decentemente
                y en orden, abrazaré los principios de Roma y probablemente
                tomaré el velo.</p>

                <p>No expresé ni sorpresa por esta resolución ni intenté disuadirla de
                ella. «La vocación te sentará como un guante», pensé. «¡Que te
                aproveche mucho!».</p>

                <p>Cuando nos separamos, dijo:</p>

                <p>—Adiós, prima Jane Eyre; le deseo lo mejor. Tiene usted algo de
                sentido común.</p>

                <p>Entonces respondí:</p>

                <p>—Usted no carece de sentido común, prima Eliza; pero lo que
                tiene, supongo, en otro año estará emparedado vivo en un convento
                francés. Sin embargo, no es asunto mío, y si a usted le conviene, no
                me importa mucho.</p>

                <p>—Tiene usted razón —dijo; y con estas palabras cada una tomó su
                camino. Como no tendré ocasión de referirme de nuevo ni a ella ni a
                su hermana, bien puedo mencionar aquí que Georgiana hizo un
                ventajoso matrimonio con un hombre de mundo rico y desgastado, y
                que Eliza efectivamente tomó el velo y es hoy en día superiora del
                convento donde pasó el período de su noviciado, y al que dotó con
                su fortuna.</p>

                <p>Cómo se siente la gente cuando regresa a casa después de una
                ausencia, larga o corta, no lo sabía. Nunca había experimentado la
                sensación. Había sabido lo que era volver a Gateshead de niña
                después de un largo paseo, para ser regañada por parecer fría o
                sombría; y más tarde, lo que era volver de la iglesia a Lowood,
                anhelar una comida abundante y un buen fuego, y ser incapaz de
                conseguir ninguno de los dos. Ninguno de estos regresos era muy
                agradable o deseable. Ningún imán me atraía a un punto dado,
                aumentando su fuerza de atracción cuanto más me acercaba. El
                regreso a Thornfield aún estaba por probar.</p>

                <p>Mi viaje me pareció tedioso, muy tedioso: cincuenta millas un día,
                una noche pasada en una posada; cincuenta millas al día siguiente.
                Durante las primeras doce horas pensé en la señora Reed en sus
                últimos momentos; vi su rostro desfigurado y descolorido, y oí su
                voz extrañamente alterada. Medité sobre el día del funeral, el ataúd,
                el coche fúnebre, el negro cortejo de inquilinos y criados —escaso
                era el número de parientes—, la cripta abierta, la iglesia silenciosa,
                el servicio solemne. Luego pensé en Eliza y Georgiana; contemplé a
                una como el centro de atención de un salón de baile, a la otra como
                la interna de una celda de convento; y me detuve y analicé sus
                peculiaridades separadas de persona y carácter. La llegada
                vespertina a la gran ciudad de... dispersó estos pensamientos; la
                noche les dio un giro completamente diferente. Acostada en mi
                cama de viajera, dejé la reminiscencia por la anticipación.</p>

                <p>Iba a volver a Thornfield, pero ¿cuánto tiempo me quedaría allí?
                No mucho; de eso estaba segura. Había tenido noticias de la señora
                Fairfax en el ínterin de mi ausencia. La comitiva en la mansión se
                había dispersado; el señor Rochester se había marchado a Londres
                hacía tres semanas, pero se esperaba que regresara en quince días.
                La señora Fairfax conjeturaba que se había ido para hacer arreglos
                para su boda, ya que había hablado de comprar un nuevo carruaje.
                Dijo que la idea de que se casara con la señorita Ingram todavía le
                parecía extraña; pero por lo que todo el mundo decía, y por lo que
                ella misma había visto, ya no podía dudar de que el evento tendría
                lugar en breve. «Serías extrañamente incrédula si lo dudaras», fue
                mi comentario mental. «No lo dudo».</p>

                <p>La pregunta que siguió fue: «¿Adónde iba a ir yo?». Soñé con la
                señorita Ingram toda la noche. En un vívido sueño matutino la vi
                cerrando las verjas de Thornfield contra mí y señalándome otro
                camino; y el señor Rochester miraba con los brazos cruzados,
                sonriendo sardónicamente, al parecer, tanto a ella como a mí.</p>

                <p>No había notificado a la señor2a Fairfax el día exacto de mi
                regreso, pues no deseaba que ni calesa ni carruaje me esperaran en
                Millcote. Me propuse caminar la distancia tranquilamente por mi
                cuenta; y muy tranquilamente, después de dejar mi baúl al cuidado
                del mozo de cuadra, me escabullí de la Posada George, sobre las
                seis de una tarde de junio, y tomé el viejo camino hacia Thornfield,
                un camino que discurría principalmente a través de campos y que
                ahora era poco frecuentado.</p>

                <p>No era una tarde de verano brillante o espléndida, aunque sí
                buena y suave. Los segadores estaban trabajando a lo largo de todo
                el camino; y el cielo, aunque lejos de estar despejado, prometía un
                buen futuro: su azul —donde el azul era visible— era suave y
                sereno, y sus estratos de nubes, altos y delgados. El oeste, también,
                era cálido; ningún destello acuoso lo enfriaba; parecía como si se
                hubiera encendido un fuego, un altar ardiendo detrás de su pantalla
                de vapor veteado, y de las aberturas brillaba un rubor dorado.</p>

                <p>Me sentí contenta a medida que el camino se acortaba ante mí;
                tan contenta que me detuve una vez para preguntarme qué
                significaba esa alegría, y para recordarle a la razón que no iba a mi
                hogar, ni a un lugar de descanso permanente, ni a un lugar donde
                amigos cariñosos me esperaban y aguardaban mi llegada. «La
                señora Fairfax te sonreirá una tranquila bienvenida, por supuesto»,
                me dije; «y la pequeña Adèle aplaudirá y saltará al verte. Pero sabes
                muy bien que estás pensando en otro que no son ellos, y que él no
                está pensando en ti».</p>

                <p>Pero, ¿qué hay tan testarudo como la juventud? ¿Qué tan ciego
                como la inexperiencia? Estas afirmaban que era placer suficiente
                tener el privilegio de volver a mirar al señor Rochester, me mirara él
                o no; y añadían: «¡Apresúrate! ¡Apresúrate! Está con él mientras
                puedas. Solo unos pocos días o semanas más, como mucho, y te
                separarás de él para siempre». Y entonces estrangulé una agonía
                recién nacida, una cosa deforme que no podía persuadirme de
                reconocer y criar, y seguí corriendo.</p>

                <p>También están segando el heno en los prados de Thornfield; o
                más bien, los labradores acaban de dejar su trabajo y regresan a
                casa con sus rastrillos al hombro, ahora, a la hora en que llego. Solo
                tengo que atravesar uno o dos campos, y luego cruzaré el camino y
                llegaré a las verjas. ¡Qué llenos están los setos de rosas! Pero no
                tengo tiempo de coger ninguna; quiero estar en la casa. Pasé junto
                a un alto rosal silvestre, que lanzaba ramas frondosas y floridas a
                través del sendero; veo la estrecha estela con escalones de piedra; y
                veo... al señor Rochester sentado allí, con un libro y un lápiz en la
                mano; está escribiendo.</p>

                <p>Bueno, no es un fantasma; sin embargo, cada nervio que tengo
                está destensado. Por un momento estoy fuera de mi propio control.
                ¿Qué significa esto? No pensé que temblaría de esta manera cuando
                lo viera, o que perdería la voz o el poder de movimiento en su
                presencia. Volveré tan pronto como pueda moverme. No necesito
                hacer el ridículo absoluto. Conozco otro camino a la casa. No
                importa si conociera veinte caminos; porque me ha visto.</p>

                <p>—¡Eh! —grita; y guarda su libro y su lápiz—. ¡Ahí estás! Vamos, si
                te parece.</p>

                <p>Supongo que avanzo; aunque de qué manera no lo sé, apenas
                consciente de mis movimientos y solícita solo de parecer tranquila; y,
                sobre todo, de controlar los músculos de mi rostro, que siento que
                se rebelan insolentemente contra mi voluntad y luchan por expresar
                lo que había resuelto ocultar. Pero tengo un velo, está bajado;
                todavía puedo arreglármelas para comportarme con una compostura
                decente.</p>

                <p>—¿Y esta es Jane Eyre? ¿Vienes de Millcote, y a pie? Sí, uno de
                tus trucos. No mandar a buscar un carruaje y venir traqueteando por
                calles y caminos como una mortal común, sino deslizarte en las
                cercanías de tu hogar junto con el crepúsculo, como si fueras un
                sueño o una sombra. ¿Qué diablos has hecho contigo misma este
                último mes?</p>

                <p>—He estado con mi tía, señor, que ha muerto.</p>

                <p>—¡Una respuesta típicamente janiana! ¡Que los buenos ángeles
                me guarden! Viene del otro mundo, de la morada de los que han
                muerto; ¡y me lo dice cuando me encuentra sola aquí en el
                crepúsculo! Si me atreviera, te tocaría para ver si eres sustancia o
                sombra, ¡duende! Pero preferiría intentar agarrar una luz de fuego
                fatuo azul en un pantano. ¡Ausente! ¡Ausente! —añadió, después de
                una pausa de un instante—. ¡Ausente de mí un mes entero, y
                olvidándome por completo, lo juraría!</p>

                <p>Sabía que habría placer en encontrarme de nuevo con mi amo,
                aunque roto por el temor de que tan pronto dejara de ser mi amo y
                por el conocimiento de que yo no era nada para él. Pero siempre
                había en el señor Rochester (al menos eso pensaba yo) tal riqueza
                de poder para comunicar felicidad, que probar solo las migajas que
                esparcía a pájaros extraviados y extraños como yo, era un festín
                cordial. Sus últimas palabras fueron un bálsamo. Parecían implicar
                que le importaba algo si lo olvidaba o no. Y había hablado de
                Thornfield como mi hogar. ¡Ojalá fuera mi hogar!</p>

                <p>No se apartó de la estela y apenas me atreví a pedirle paso. Le
                pregunté pronto si no había estado en Londres.</p>

                <p>—Sí; supongo que lo descubriste por segunda vista.</p>

                <p>—La señora Fairfax me lo dijo en una carta.</p>

                <p>—¿Y te informó de lo que fui a hacer?</p>

                <p>—¡Oh, sí, señor! Todo el mundo conocía su recado.</p>

                <p>—Debes ver el carruaje, Jane, y decirme si no crees que le sentará
                a la señora Rochester exactamente; y si no parecerá la reina
                Boadicea, reclinada contra esos cojines púrpuras. Desearía, Jane,
                estar un poco mejor adaptado para hacer juego con ella
                exteriormente. Dime ahora, hada como eres, ¿no puedes darme un
                hechizo, o un filtro, o algo por el estilo, para convertirme en un
                hombre guapo?</p>

                <p>—Estaría más allá del poder de la magia, señor. —Y, en
                pensamiento, añadí—: Un ojo que ama es todo el hechizo necesario.
                Para tal ojo, es usted lo suficientemente guapo; o más bien, su
                severidad tiene un poder más allá de la belleza.</p>

                <p>El señor Rochester a veces había leído mis pensamientos no
                expresados con una agudeza para mí incomprensible. En el presente
                caso, no prestó atención a mi abrupta respuesta vocal; pero me
                sonrió con una cierta sonrisa que tenía, propia de él, y que usaba
                solo en raras ocasiones. Parecía pensar que era demasiado buena
                para propósitos comunes. Era el verdadero sol del sentimiento; lo
                derramó sobre mí ahora.</p>

                <p>—Pasa, Janet —dijo, haciéndome sitio para cruzar la estela—.
                Sube a casa y descansa tus pequeños y cansados pies errantes en el
                umbral de un amigo.</p>

                <p>Todo lo que tenía que hacer ahora era obedecerle en silencio; no
                necesitaba conversar más. Pasé por encima de la estela sin una
                palabra y pretendía dejarlo tranquilamente. Un impulso me detuvo
                en seco, una fuerza me hizo girar. Dije, o algo en mí dijo por mí, y a
                pesar de mí:</p>

                <p>—Gracias, señor Rochester, por su gran amabilidad. Estoy
                extrañamente contenta de volver de nuevo con usted. Y
                dondequiera que usted esté es mi hogar, mi único hogar.</p>

                <p>Caminé tan rápido que ni siquiera él podría haberme alcanzado si
                lo hubiera intentado. La pequeña Adèle estaba medio loca de alegría
                cuando me vio. La señora Fairfax me recibió con su habitual y
                sencilla amabilidad. Leah sonrió, e incluso Sophie me dijo «bon soir»
                con alegría. Esto fue muy agradable; no hay felicidad como la de ser
                amada por tus semejantes y sentir que tu presencia es una adición a
                su comodidad.</p>

                <p>Aquella tarde cerré los ojos resueltamente al futuro. Tapé mis
                oídos a la voz que seguía advirtiéndome de una separación cercana
                y de un dolor venidero. Cuando terminó el té y la señora Fairfax
                hubo tomado su labor de punto, y yo hube asumido un asiento bajo
                cerca de ella, y Adèle, arrodillada en la alfombra, se hubo
                acurrucado junto a mí, y una sensación de afecto mutuo pareció
                rodearnos con un anillo de paz dorada, pronuncié una oración
                silenciosa para que no nos separáramos lejos ni pronto. Pero
                cuando, así sentados, el señor Rochester entró, sin anunciarse, y
                mirándonos, pareció complacerse en el espectáculo de un grupo tan
                amigable; cuando dijo que suponía que la anciana dama estaba bien
                ahora que había recuperado a su hija adoptiva, y añadió que veía
                que Adèle estaba «prête à croquer sa petite maman Anglaise», casi
                me atreví a esperar que, incluso después de su matrimonio, nos
                mantuviera juntas en algún lugar bajo el amparo de su protección, y
                no completamente exiliadas del sol de su presencia.</p>

                <p>Una quincena de dudosa calma sucedió a mi regreso a Thornfield
                Hall. No se dijo nada del matrimonio del amo, y no vi ninguna
                preparación en marcha para tal evento. Casi todos los días le
                preguntaba a la señora Fairfax si ya había oído algo decidido. Su
                respuesta siempre era negativa. Una vez dijo que le había hecho la
                pregunta directamente al señor Rochester sobre cuándo iba a traer a
                su novia a casa; pero él le había respondido solo con una broma y
                una de sus miradas extrañas, y ella no sabía qué pensar de él.</p>

                <p>Una cosa me sorprendió especialmente, y fue que no había viajes
                de ida y vuelta, ni visitas a Ingram Park. Cierto es que estaba a
                veinte millas de distancia, en los límites de otro condado; pero ¿qué
                era esa distancia para un amante ardiente? Para un jinete tan
                experimentado e infatigable como el señor Rochester, no sería más
                que un paseo matutino. Comencé a albergar esperanzas que no
                tenía derecho a concebir: que el compromiso se había roto; que el
                rumor se había equivocado; que una o ambas partes habían
                cambiado de opinión. Solía mirar el rostro de mi amo para ver si
                estaba triste o fiero; pero no podía recordar el momento en que
                hubiera estado tan uniformemente despejado de nubes o malos
                sentimientos. Si, en los momentos que mi alumna y yo pasábamos
                con él, a mí me faltaba el ánimo y me hundía en una inevitable
                desolación, él se volvía incluso alegre. Nunca me había llamado con
                más frecuencia a su presencia; nunca había sido más amable
                conmigo cuando estaba allí, y, ¡ay!, nunca lo había amado tanto.</p>

