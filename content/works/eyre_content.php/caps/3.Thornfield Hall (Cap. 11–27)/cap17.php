            <section class="contenido">
                <h1>Capítulo XVII</h1>
    
                <p>Pasó una semana y no llegaron noticias del señor Rochester. Diez
                días, y aún no venía. La señora Fairfax dijo que no le sorprendería
                que fuera directamente de Leas a Londres, y de allí al Continente, y
                no volviera a mostrar su rostro por Thornfield en un año; no con
                poca frecuencia lo había abandonado de una manera igualmente
                abrupta e inesperada. Cuando oí esto, empecé a sentir un extraño
                frío y desfallecimiento en el corazón. De hecho, me estaba
                permitiendo experimentar una enfermiza sensación de decepción;
                pero, recobrando el juicio y recordando mis principios, puse
                inmediatamente en orden mis sensaciones; y fue maravilloso cómo
                superé el desliz temporal, cómo aclaré el error de suponer que los
                movimientos del señor Rochester eran un asunto en el que yo
                tuviera alguna causa para tomar un interés vital. No es que me
                humillara con una noción servil de inferioridad; al contrario,
                simplemente dije:</p>

                <p>«No tienes nada que ver con el amo de Thornfield, más allá de
                recibir el salario que te da por enseñar a su protegida, y de estar
                agradecida por el trato respetuoso y amable que, si cumples con tu
                deber, tienes derecho a esperar de sus manos. Asegúrate de que ese
                es el único lazo que él reconoce seriamente entre tú y él; así que no
                lo conviertas en el objeto de tus nobles sentimientos, tus éxtasis, tus
                agonías, y demás. Él no es de tu orden; mantente en tu casta y sé
                demasiado respetuosa contigo misma para prodigar el amor de todo
                el corazón, el alma y la fuerza, donde tal don no es deseado y sería
                despreciado».</p>

                <p>Continué con mis quehaceres del día tranquilamente; pero de vez
                en cuando, vagas sugerencias seguían vagando por mi cerebro sobre
                las razones por las que debería abandonar Thornfield; y seguía,
                involuntariamente, redactando anuncios y reflexionando sobre
                conjeturas acerca de nuevos puestos. No pensé en reprimir estos
                pensamientos; podían germinar y dar fruto si podían.</p>

                <p>El señor Rochester llevaba ausente más de quince días cuando el
                correo trajo una carta a la señora Fairfax.</p>

                <p>—Es del amo —dijo, mientras miraba la dirección—. Ahora
                supongo que sabremos si debemos esperar su regreso o no.</p>

                <p>Y mientras rompía el sello y leía el documento, yo seguí tomando
                mi café (estábamos en el desayuno). Estaba caliente, y atribuí a esa
                circunstancia un brillo ardiente que de repente subió a mi rostro. Por
                qué me temblaba la mano, y por qué derramé involuntariamente la
                mitad del contenido de mi taza en el platillo, no quise considerarlo.</p>

                <p>—Bueno, a veces pienso que estamos demasiado tranquilas; pero
                ahora corremos el riesgo de estar bastante ocupadas, al menos por
                un tiempo —dijo la señora Fairfax, todavía sosteniendo la nota ante
                sus gafas.</p>

                <p>Antes de permitirme solicitar una explicación, até el lazo del
                delantal de Adèle, que casualmente estaba suelto. Habiéndola
                ayudado también con otro bollo y rellenado su taza con leche, dije,
                con despreocupación:</p>

                <p>—El señor Rochester no es probable que regrese pronto, supongo.</p>

                <p>—De hecho, sí, en tres días, dice. Eso será el próximo jueves; y no
                solo, tampoco. No sé cuánta de la gente distinguida de Leas vendrá
                con él. Envía instrucciones para que se preparen todos los mejores
                dormitorios; y la biblioteca y los salones deben limpiarse. Tengo que
                conseguir más ayudantes de cocina de la Posada George, en
                Millcote, y de dondequiera que pueda; y las damas traerán a sus
                doncellas y los caballeros a sus criados. Así que tendremos la casa
                llena. —Y la señora Fairfax se tragó el desayuno y se apresuró a
                comenzar las operaciones.</p>

                <p>Los tres días fueron, como ella había predicho, bastante
                ajetreados. Había pensado que todas las habitaciones de Thornfield
                estaban hermosamente limpias y bien dispuestas, pero parece que
                me equivocaba. Se consiguieron tres mujeres para ayudar; y tal
                fregar, tal cepillar, tal lavar de pintura y sacudir de alfombras, tal
                quitar y poner de cuadros, tal pulir de espejos y candelabros, tal
                encender de fuegos en los dormitorios, tal airear de sábanas y
                edredones de plumas en los hogares, nunca lo vi, ni antes ni desde
                entonces. Adèle corría como loca en medio de todo ello. Los
                preparativos para la compañía y la perspectiva de su llegada
                parecían sumirla en éxtasis. Quería que Sophie revisara todas sus
                «<i>toilettes»</i>, como llamaba a los vestidos; que adecentara los que
                estuvieran «<i>passées</i>» y que aireara y arreglara los nuevos. Por sí
                misma, no hacía más que dar saltos por las cámaras delanteras,
                subir y bajar de las camas y tumbarse en los colchones y los
                almohadones y almohadas apilados ante los enormes fuegos que
                rugían en las chimeneas. De los deberes escolares fue exonerada. La
                señora Fairfax me había reclutado para su servicio, y yo estaba todo
                el día en la despensa, ayudándola (o estorbándola) a ella y a la
                cocinera; aprendiendo a hacer natillas y pasteles de queso y
                pastelería francesa, a aderezar la caza y a guarnecer los platos de
                postre.</p>

                <p>Se esperaba que la comitiva llegara el jueves por la tarde, a
                tiempo para la cena a las seis. Durante el período intermedio no tuve
                tiempo de alimentar quimeras; y creo que estuve tan activa y alegre
                como cualquiera, excepto Adèle. Aun así, de vez en cuando, recibía
                un freno desalentador a mi alegría; y era, a pesar de mí misma,
                devuelta a la región de las dudas y los presagios, y las oscuras
                conjeturas. Esto ocurría cuando casualmente veía la puerta de la
                escalera del tercer piso (que últimamente siempre se había
                mantenido cerrada con llave) abrirse lentamente y dar paso a la
                figura de Grace Poole, con su cofia almidonada, su delantal blanco y
                su pañuelo; cuando la observaba deslizarse por la galería, con su
                paso silencioso amortiguado por una zapatilla de fieltro; cuando la
                veía mirar en los dormitorios bulliciosos y revueltos, decir solo una
                palabra, quizás, a la mujer de la limpieza sobre la forma correcta de
                pulir una rejilla, o limpiar una repisa de mármol, o quitar manchas
                de las paredes empapeladas, y luego seguir adelante. Así descendía
                a la cocina una vez al día, comía su cena, fumaba una pipa
                moderada junto al hogar y volvía a subir, llevando consigo su jarra
                de cerveza negra, para su solaz privado, en su sombrío refugio
                superior. Solo una hora de las veinticuatro pasaba con sus
                compañeros de servicio abajo; todo el resto de su tiempo lo pasaba
                en alguna cámara de techo bajo y de roble del segundo piso. Allí se
                sentaba y cosía —y probablemente reía lúgubremente para sí misma
                —, tan solitaria como un prisionero en su mazmorra.</p>

                <p>Lo más extraño de todo era que ni un alma en la casa, excepto yo,
                notaba sus hábitos o parecía maravillarse de ellos. Nadie discutía su
                posición o empleo; nadie se compadecía de su soledad o
                aislamiento. Una vez, en verdad, oí parte de un diálogo entre Leah y
                una de las mujeres de la limpieza, del cual Grace era el tema. Leah
                había estado diciendo algo que no capté, y la mujer de la limpieza
                comentó:</p>

                <p>—Gana un buen sueldo, ¿supongo?</p>

                <p>—Sí —dijo Leah—. Ojalá yo tuviera uno tan bueno; no es que del
                mío me queje, no hay tacañería en Thornfield; pero no es ni una
                quinta parte de la suma que recibe la señora Poole. Y está
                ahorrando. Va cada trimestre al banco de Millcote. No me extrañaría
                que tuviera ahorrado lo suficiente para mantenerse independiente si
                quisiera irse; pero supongo que se ha acostumbrado al lugar; y
                además, aún no tiene cuarenta años, y es fuerte y capaz para
                cualquier cosa. Es demasiado pronto para que deje el trabajo.</p>

                <p>—Es una buena trabajadora, me atrevo a decir —dijo la mujer de
                la limpieza.</p>

                <p>—¡Ah! Entiende lo que tiene que hacer, nadie mejor —replicó Leah
                significativamente—. Y no cualquiera podría llenar sus zapatos, ni
                por todo el dinero que gana.</p>

                <p>—¡Eso sí que no! —fue la respuesta—. Me pregunto si el amo...</p>

                <p>La mujer de la limpieza iba a continuar; pero aquí Leah se volvió y
                me vio, e instantáneamente le dio un codazo a su compañera.</p>

                <p>—¿No lo sabe ella? —oí susurrar a la mujer.</p>

                <p>Leah negó con la cabeza, y la conversación, por supuesto, se
                interrumpió. Todo lo que había deducido de ella se reducía a esto:
                que había un misterio en Thornfield; y que de la participación en ese
                misterio yo era excluida a propósito.</p>

                <p>Llegó el jueves. Todo el trabajo se había completado la tarde
                anterior; las alfombras estaban puestas, los cortinajes de las camas
                festoneados, las radiantes colchas blancas extendidas, los tocadores
                arreglados, los muebles frotados, las flores apiladas en jarrones.
                Tanto las cámaras como los salones parecían tan frescos y brillantes
                como las manos podían hacerlos. El vestíbulo también fue fregado; y
                el gran reloj tallado, así como los escalones y la barandilla de la
                escalera, fueron pulidos hasta el brillo del cristal. En el comedor, el
                aparador resplandecía de plata; en el salón y el tocador, jarrones de
                exóticas flores florecían por todas partes.</p>

                <p>Llegó la tarde. La señora Fairfax se puso su mejor vestido de
                satén negro, sus guantes y su reloj de oro; pues era su parte recibir
                a la compañía, conducir a las damas a sus habitaciones, etc. Adèle
                también sería vestida, aunque pensé que tenía pocas posibilidades
                de ser presentada a la comitiva ese día, al menos. Sin embargo, para
                complacerla, permití a Sophie ataviarla con uno de sus vestidos
                cortos y amplios de muselina. Por mi parte, no tenía necesidad de
                hacer ningún cambio; no se me llamaría a abandonar mi santuario
                del aula; pues un santuario se había vuelto ahora para mí, «un
                refugio muy agradable en tiempos de tribulación».</p>

                <p>Había sido un día de primavera suave y sereno, uno de esos días
                que, hacia finales de marzo o principios de abril, se alzan
                resplandecientes sobre la tierra como heraldos del verano. Estaba
                llegando a su fin ahora; pero la tarde era incluso cálida, y yo estaba
                sentada trabajando en el aula con la ventana abierta.</p>

                <p>—Se hace tarde —dijo la señora Fairfax, entrando en estado
                crujiente—. Me alegro de haber ordenado la cena una hora después
                de la hora que mencionó el señor Rochester; pues ya pasan de las
                seis. He enviado a John a las verjas para ver si hay algo en el
                camino; desde allí se ve muy lejos en dirección a Millcote. —Se
                acercó a la ventana—. ¡Aquí está! —dijo—. Bueno, John
                (asomándose), ¿alguna noticia?</p>

                <p>—Están llegando, señora —fue la respuesta—. Estarán aquí en
                diez minutos.</p>
                
                <p>Adèle voló a la ventana. Yo la seguí, cuidando de ponerme a un
                lado, para que, protegida por la cortina, pudiera ver sin ser vista.</p>

                <p>Los diez minutos que John había dado parecieron muy largos,
                pero al fin se oyeron ruedas; cuatro jinetes galoparon por el camino
                de entrada, y tras ellos venían dos carruajes abiertos. Velos
                ondeantes y plumas al viento llenaban los vehículos; dos de los
                caballeros eran jóvenes de aspecto gallardo; el tercero era el señor
                Rochester, en su caballo negro, Mesrour, con Pilot saltando delante
                de él; a su lado cabalgaba una dama, y él y ella eran los primeros de
                la comitiva. Su hábito de montar púrpura casi barría el suelo, su velo
                ondeaba largo al viento; mezclándose con sus pliegues
                transparentes y brillando a través de ellos, resplandecían ricos rizos
                de cuervo.</p>

                <p>—¡La señorita Ingram! —exclamó la señora Fairfax, y se apresuró
                a su puesto abajo.</p>

                <p>La cabalgata, siguiendo la curva del camino de entrada,
                rápidamente giró el ángulo de la casa, y la perdí de vista. Adèle pidió
                entonces bajar; pero la tomé en mi regazo y le di a entender que no
                debía, bajo ningún concepto, pensar en aventurarse a la vista de las
                damas, ni ahora ni en ningún otro momento, a menos que fuera
                llamada expresamente; que el señor Rochester se enfadaría mucho,
                etc. «Algunas lágrimas naturales derramó» al oír esto; pero como
                empecé a ponerme muy seria, consintió al fin en secarlas.</p>

                <p>Un alegre bullicio se oía ahora en el vestíbulo. Tonos profundos de
                caballeros y acentos plateados de damas se mezclaban
                armoniosamente, y distinguible por encima de todo, aunque no
                fuerte, estaba la sonora voz del amo de Thornfield Hall, dando la
                bienvenida a sus bellos y galantes invitados bajo su techo. Luego,
                ligeros pasos ascendieron las escaleras; y hubo un correteo por la
                galería, y suaves risas alegres, y abrir y cerrar de puertas, y, por un
                tiempo, un silencio.</p>

                <p>—<i>Elles changent de toilettes</i> —dijo Adèle, que, escuchando
            atentamente, había seguido cada movimiento; y suspiró.</p>

                <p>—<i>Chez maman</i>—dijo—<i>quand il y avait du monde, je le suivais
                partout, au salon et à leurs chambres; souvent je regardais les
                femmes de chambre coiffer et habiller les dames, et c'était si
                amusant: comme cela on apprend.</i></p>

                <p>—¿No tienes hambre, Adèle?</p>

                <p>—<i>Mais oui, mademoiselle: voilà cinq ou six heures que nous
                n'avons pas mangé.</i></p>

                <p>—Bueno, ahora, mientras las damas están en sus habitaciones,
                me aventuraré a bajar y traerte algo de comer.</p>

                <p>Y saliendo de mi asilo con precaución, busqué una escalera de
                servicio que conducía directamente a la cocina. Todo en esa región
                era fuego y conmoción; la sopa y el pescado estaban en la última
                fase de proyección, y la cocinera se inclinaba sobre sus crisoles en
                un estado de ánimo y cuerpo que amenazaba la combustión
                espontánea. En el salón de los criados, dos cocheros y tres lacayos
                de caballeros estaban de pie o sentados alrededor del fuego; las
                doncellas, supongo, estaban arriba con sus señoras; los nuevos
                criados que se habían contratado de Millcote bullían por todas
                partes. Atravesando este caos, llegué por fin a la despensa; allí me
                apoderé de un pollo frío, un panecillo, algunas tartas, uno o dos
                platos y un cuchillo y tenedor. Con este botín, hice una retirada
                apresurada. Había recuperado la galería y estaba cerrando la puerta
                de servicio tras de mí, cuando un zumbido acelerado me advirtió que
                las damas estaban a punto de salir de sus cámaras. No podía
                proceder al aula sin pasar por algunas de sus puertas y correr el
                riesgo de ser sorprendida con mi cargamento de vituallas; así que
                me quedé quieta en este extremo, que, al no tener ventanas, estaba
                oscuro; completamente oscuro ahora, pues el sol se había puesto y
                el crepúsculo se acumulaba.</p>

                <p>Al instante, las cámaras entregaron a sus bellas inquilinas una tras
                otra. Cada una salió alegre y etérea, con un vestido que brillaba
                lustroso a través de la penumbra. Por un momento se agruparon en
                el otro extremo de la galería, conversando en un tono de dulce y
                contenida vivacidad. Luego descendieron la escalera casi tan
                silenciosamente como una bruma brillante rueda por una colina. Su
                apariencia colectiva me había dejado una impresión de elegancia de
                alta cuna, como nunca antes había recibido.</p>

                <p>Encontré a Adèle espiando por la puerta del aula, que mantenía entreabierta.</p>

                <p>—¡Qué damas tan hermosas! —exclamó en inglés—. ¡Oh, ojalá
                pudiera ir con ellas! ¿Cree que el señor Rochester nos mandará
                llamar dentro de un rato, después de la cena?</p>

                <p>—No, por supuesto que no. El señor Rochester tiene otras cosas
                en qué pensar. No te preocupes por las damas esta noche; quizás las
                veas mañana. Aquí está tu cena.</p>

                <p>Tenía verdadera hambre, así que el pollo y las tartas sirvieron para
                desviar su atención por un tiempo. Fue bueno que asegurara este
                forraje, o tanto ella como yo, y Sophie, a quien llevé una parte de
                nuestro rancho, habríamos corrido el riesgo de no cenar en absoluto.
                Todos abajo estaban demasiado ocupados para pensar en nosotras.
                El postre no se sirvió hasta después de las nueve; y a las diez, los
                lacayos todavía corrían de un lado a otro con bandejas y tazas de
                café. Permití a Adèle quedarse levantada mucho más tarde de lo
                habitual, pues declaró que no podría dormirse mientras las puertas
                se abrieran y cerraran abajo y la gente bullera. Además, añadió,
                podría llegar un mensaje del señor Rochester cuando estuviera
                desvestida; «<i>et alors quel dommage!</i>».</p>

                <p>Le conté cuentos mientras quiso escucharlos; y luego, para variar,
                la saqué a la galería. La lámpara del vestíbulo estaba ahora
                encendida, y le divertía mirar por encima de la balaustrada y
                observar a los criados pasar de un lado a otro. Cuando la noche
                estaba muy avanzada, un sonido de música salió del salón, adonde
                se había trasladado el piano. Adèle y yo nos sentamos en el escalón
                superior de la escalera para escuchar. Al instante, una voz se mezcló
                con los ricos tonos del instrumento; era una dama que cantaba, y
                muy dulces eran sus notas. Terminado el solo, siguió un dúo, y luego
                un coro. Un alegre murmullo conversacional llenaba los intervalos.
                Escuché durante mucho tiempo. De repente descubrí que mi oído
                estaba completamente atento a analizar los sonidos mezclados y a
                tratar de discriminar, en medio de la confusión de acentos, los del
                señor Rochester; y cuando los captó, lo que hizo pronto, encontró
                una tarea adicional en dar forma de palabras a los tonos, vueltos
                inarticulados por la distancia.</p>

                <p>El reloj dio las once. Miré a Adèle, cuya cabeza se apoyaba en mi
                hombro; sus ojos se estaban volviendo pesados, así que la tomé en
                brazos y la llevé a la cama. Era cerca de la una antes de que los
                caballeros y las damas buscaran sus aposentos.</p>

                <p>El día siguiente fue tan bueno como su predecesor. Fue dedicado
                por la comitiva a una excursión a algún sitio de los alrededores.
                Partieron temprano por la mañana, algunos a caballo, el resto en
                carruajes. Fui testigo tanto de la partida como del regreso. La
                señorita Ingram, como antes, era la única dama amazona; y, como
                antes, el señor Rochester galopaba a su lado; los dos cabalgaban un
                poco apartados del resto. Señalé esta circunstancia a la señora
                Fairfax, que estaba en la ventana conmigo.</p>

                <p>—Dijo usted que no era probable que pensaran en casarse —dije
                —, pero ya ve que el señor Rochester evidentemente la prefiere a
                cualquiera de las otras damas.</p>

                <p>—Sí, me atrevo a decir. Sin duda la admira.</p>

                <p>—Y ella a él —añadí—. Mire cómo inclina la cabeza hacia él como
                si estuviera conversando confidencialmente. Ojalá pudiera verle la
                cara; aún no he tenido ni un atisbo de ella.</p>

                <p>—La verá esta tarde —respondió la señora Fairfax—. Casualmente
                le comenté al señor Rochester cuánto deseaba Adèle ser presentada
                a las damas, y él dijo: «¡Oh! Que entre en el salón después de la
                cena; y pida a la señorita Eyre que la acompañe».</p>

                <p>—Sí; lo dijo por pura cortesía. No necesito ir, estoy segura —respondí.</p>

                <p>—Bueno, le observé que como usted no estaba acostumbrada a la
                compañía, no creía que le gustaría aparecer ante una comitiva tan
                alegre, todos extraños; y él respondió, a su manera rápida:
                «¡Tonterías! Si se opone, dígale que es mi deseo particular; y si se
                resiste, diga que vendré a buscarla en caso de contumacia».</p>

                <p>—No le daré esa molestia —respondí—. Iré, si no hay más
                remedio; pero no me gusta. ¿Estará usted allí, señora Fairfax?</p>

                <p>—No; me excusé y él admitió mi excusa. Le diré cómo
                arreglárselas para evitar la vergüenza de hacer una entrada formal,
                que es la parte más desagradable del asunto. Debe entrar en el
                salón mientras esté vacío, antes de que las damas dejen la mesa de
                la cena; elija su asiento en cualquier rincón tranquilo que le guste;
                no necesita quedarse mucho tiempo después de que entren los
                caballeros, a menos que le plazca. Solo deje que el señor Rochester
                vea que está allí y luego escápese, nadie la notará.</p>

                <p>—¿Se quedará esta gente mucho tiempo, cree usted?</p>

                <p>—Quizás dos o tres semanas, ciertamente no más. Después del
                receso de Pascua, Sir George Lynn, que fue elegido recientemente
                miembro por Millcote, tendrá que ir a la ciudad y tomar posesión de
                su escaño; me atrevo a decir que el señor Rochester lo acompañará.
                Me sorprende que ya haya hecho una estancia tan prolongada en
                Thornfield.</p>

                <p>Fue con cierta trepidación que percibí acercarse la hora en que
                debía dirigirme con mi pupila al salón. Adèle había estado en un
                estado de éxtasis todo el día, después de oír que iba a ser
                presentada a las damas por la tarde; y no fue hasta que Sophie
                comenzó la operación de vestirla que se sosegó. Entonces la
                importancia del proceso la serenó rápidamente, y para cuando tuvo
                sus rizos arreglados en racimos bien lisos y caídos, su vestido de
                satén rosa puesto, su largo lazo atado y sus mitones de encaje
                ajustados, parecía tan grave como cualquier juez. No hubo
                necesidad de advertirle que no desordenara su atuendo. Cuando
                estuvo vestida, se sentó recatadamente en su sillita, cuidando
                previamente de levantar la falda de satén por temor a arrugarla, y
                me aseguró que no se movería de allí hasta que yo estuviera lista.
                Lo estuve rápidamente: mi mejor vestido (el de color gris plateado,
                comprado para la boda de la señorita Temple y nunca usado desde
                entonces) fue puesto en un momento; mi pelo fue alisado en un
                momento; mi único adorno, el broche de perlas, fue asumido en un
                momento. Descendimos.</p>

                <p>Afortunadamente, había otra entrada al salón además de la que
                pasaba por la sala donde todos estaban sentados a la cena.
                Encontramos el apartamento vacío; un gran fuego ardía
                silenciosamente en el hogar de mármol, y las velas de cera brillaban
                en brillante soledad, en medio de las exquisitas flores con las que se
                adornaban las mesas. La cortina carmesí colgaba ante el arco; por
                muy ligera que fuera la separación que este cortinaje formaba del
                grupo en el salón contiguo, hablaban en un tono tan bajo que nada
                de su conversación podía distinguirse más allá de un murmullo
                tranquilizador.</p>

                <p>Adèle, que parecía estar todavía bajo la influencia de una
                impresión de lo más solemnizadora, se sentó, sin una palabra, en el
                escabel que le señalé. Yo me retiré a un asiento de ventana y,
                tomando un libro de una mesa cercana, intenté leer. Adèle trajo su
                escabel a mis pies; al poco rato, me tocó la rodilla.</p>

                <p>—¿Qué pasa, Adèle?</p>

                <p>—<i>Est-ce que je ne puis pas prendre une seule de ces fleurs
                magnifiques, mademoiselle? Seulement pour compléter ma toilette.</i></p>

                <p>—Piensas demasiado en tu «<i>toilette</i>», Adèle; pero puedes tomar
                una flor. —Y tomé una rosa de un jarrón y se la prendí en el lazo.
                Suspiró un suspiro de inefable satisfacción, como si su copa de
                felicidad estuviera ahora llena. Aparté la cara para ocultar una
                sonrisa que no pude reprimir. Había algo de ridículo además de
                doloroso en la devoción seria e innata de la pequeña parisina por los
                asuntos del vestir.</p>

                <p>Un suave sonido de levantarse se hizo ahora audible; la cortina
                fue retirada del arco; a través de él apareció el comedor, con su
                candelabro encendido derramando luz sobre la plata y el cristal de
                un magnífico servicio de postre que cubría una larga mesa. Un grupo
                de damas estaba de pie en la abertura; entraron, y la cortina cayó
                tras ellas.</p>

                <p>Solo eran ocho; sin embargo, de alguna manera, al entrar en
                tropel, daban la impresión de un número mucho mayor. Algunas de
                ellas eran muy altas; muchas iban vestidas de blanco; y todas tenían
                una amplitud de vestimenta que parecía magnificar sus personas
                como una niebla magnifica la luna. Me levanté y les hice una
                reverencia. Una o dos inclinaron la cabeza a cambio; las otras solo
                me miraron fijamente.</p>

                <p>Se dispersaron por la habitación, recordándome, por la ligereza y
                flotabilidad de sus movimientos, a una bandada de pájaros blancos y
                plumosos. Algunas de ellas se arrojaron en posiciones medio
                reclinadas en los sofás y pufs; algunas se inclinaron sobre las mesas
                y examinaron las flores y los libros; el resto se reunió en un grupo
                alrededor del fuego. Todas hablaban en un tono bajo pero claro que
                parecía habitual en ellas. Supe sus nombres después, y bien podría
                mencionarlos ahora.</p>

                <p>Primero, estaba la señora Eshton y dos de sus hijas.
                Evidentemente había sido una mujer guapa y todavía se conservaba
                bien. De sus hijas, la mayor, Amy, era más bien pequeña: ingenua e
                infantil de rostro y modales, y picante de formas; su vestido de
                muselina blanca y su lazo azul le sentaban bien. La segunda, Louisa,
                era más alta y de figura más elegante, con un rostro muy bonito, de
                ese orden que los franceses llaman minois chiffoné. Ambas
                hermanas eran blancas como lirios.</p>

                <p>Lady Lynn era un personaje grande y corpulento de unos cuarenta
                años, muy erguida, de aspecto muy altivo, ricamente vestida con
                una túnica de satén de brillo cambiante. Su pelo oscuro brillaba
                lustrosamente bajo la sombra de una pluma azul y dentro del círculo
                de una banda de gemas.</p>

                <p>La señora del Coronel Dent era menos vistosa, pero, pensé, más
                distinguida. Tenía una figura esbelta, un rostro pálido y dulce y
                cabello rubio. Su vestido de satén negro, su pañuelo de rico encaje
                extranjero y sus adornos de perlas me gustaron más que el
                resplandor de arcoíris de la dama titulada.</p>

                <p>Pero las tres más distinguidas —en parte, quizás, porque eran las
                figuras más altas del grupo— eran la viuda Lady Ingram y sus hijas,
                Blanche y Mary. Las tres eran de la estatura más elevada de las
                mujeres. La viuda podría tener entre cuarenta y cincuenta años; su
                figura todavía era fina; su cabello (a la luz de las velas, al menos)
                todavía negro; sus dientes, también, todavía aparentemente
                perfectos. La mayoría de la gente la habría llamado una mujer
                espléndida para su edad; y así era, sin duda, físicamente hablando.
                Pero entonces había una expresión de altivez casi insoportable en su
                porte y semblante. Tenía rasgos romanos y una papada que
                desaparecía en un cuello como un pilar. Estos rasgos me parecieron
                no solo hinchados y oscurecidos, sino incluso surcados de orgullo; y
                la barbilla se sostenía por el mismo principio, en una posición de
                rectitud casi preternatural. Tenía, asimismo, una mirada fiera y dura;
                me recordó a la de la señora Reed. Modulaba las palabras al hablar;
                su voz era profunda, sus inflexiones muy pomposas, muy
                dogmáticas, muy intolerables, en resumen. Una túnica de terciopelo
                carmesí y un turbante de chal de alguna tela india labrada en oro la
                investían (supongo que ella pensaba) de una dignidad
                verdaderamente imperial.</p>

                <p>Blanche y Mary eran de igual estatura, rectas y altas como
                álamos. Mary era demasiado delgada para su altura, pero Blanche
                estaba moldeada como una Diana. La observé, por supuesto, con
                especial interés. Primero, deseaba ver si su apariencia concordaba
                con la descripción de la señora Fairfax; segundo, si se parecía en
                algo a la miniatura imaginaria que había pintado de ella; y tercero —
                ¡saldrá a la luz!— si era tal como me imaginaría que podría gustar al
                señor Rochester.</p>

                <p>En cuanto a la persona, respondía punto por punto, tanto a mi
                cuadro como a la descripción de la señora Fairfax. El noble busto, los
                hombros caídos, el cuello grácil, los ojos oscuros y los rizos negros,
                todo estaba allí. ¿Pero su rostro? Su rostro era como el de su madre;
                una semejanza juvenil y sin arrugas: la misma frente baja, los
                mismos rasgos altos, el mismo orgullo. ¡No era, sin embargo, un
                orgullo tan saturnino! Reía continuamente; su risa era satírica, y
                también lo era la expresión habitual de su labio arqueado y altivo.</p>

                <p>Se dice que el genio es consciente de sí mismo. No puedo decir si
                la señorita Ingram era un genio, pero era consciente de sí misma,
                notablemente consciente de sí misma, en verdad. Entabló un
                discurso sobre botánica con la amable señora Dent. Parecía que la
                señora Dent no había estudiado esa ciencia, aunque, como dijo, le
                gustaban las flores, «especialmente las silvestres». La señorita
                Ingram sí, y repasó su vocabulario con aire de suficiencia. Al instante
                percibí que estaba (lo que se llama vernáculamente) tomándole el
                pelo a la señora Dent; es decir, jugando con su ignorancia. Su toma
                de pelo podría ser inteligente, pero decididamente no era de buen
                natural. Tocó; su ejecución fue brillante. Cantó; su voz era fina.
                Habló en francés aparte con su mamá; y lo habló bien, con fluidez y
                con buen acento.</p>

                <p>Mary tenía un semblante más dulce y abierto que Blanche; rasgos
                más suaves también, y una piel algunos tonos más clara (la señorita
                Ingram era oscura como una española), pero a Mary le faltaba vida.
                Su rostro carecía de expresión, su ojo de lustre; no tenía nada que
                decir y, una vez que tomó asiento, permaneció fija como una estatua
                en su nicho. Ambas hermanas iban vestidas de un blanco
                inmaculado.</p>

                <p>¿Y pensaba yo ahora que la señorita Ingram era una elección tal
                como la que probablemente haría el señor Rochester? No podría
                decirlo, no conocía su gusto en belleza femenina. Si le gustaba lo
                majestuoso, ella era el mismo tipo de majestad. Además, era culta,
                vivaz. La mayoría de los caballeros la admirarían, pensé; y que él la
                admiraba, ya parecía haber obtenido pruebas. Para eliminar la última
                sombra de duda, solo quedaba verlos juntos.</p>

                <p>No debe suponer, lector, que Adèle ha estado todo este tiempo
                sentada inmóvil en el escabel a mis pies. No; cuando entraron las
                damas, se levantó, avanzó a su encuentro, hizo una majestuosa
                reverencia y dijo con gravedad:</p>

                <p>—<i>Bon jour, mesdames.</i></p>

                <p>Y la señorita Ingram la había mirado con aire burlón y exclamado:
                «¡Oh, qué pequeña marioneta!».</p>

                <p>Lady Lynn había comentado: «Es la pupila del señor Rochester,
                supongo, la pequeña francesa de la que hablaba».</p>

                <p>La señora Dent le había tomado amablemente la mano y le había
                dado un beso. Amy y Louisa Eshton habían exclamado
                simultáneamente:</p>

                <p>—¡Qué amor de niña!</p>

                <p>Y luego la habían llamado a un sofá, donde ahora se sentaba,
                acomodada entre ellas, parloteando alternativamente en francés y
                en un inglés chapurreado; absorbiendo no solo la atención de las
                jóvenes damas, sino también la de la señora Eshton y Lady Lynn, y
                siendo mimada a su gusto.</p>

                <p>Por fin se trae el café y se convoca a los caballeros. Me siento en
                la sombra, si es que hay alguna sombra en este apartamento
                brillantemente iluminado; la cortina de la ventana me oculta a
                medias. De nuevo el arco se abre de par en par; ellos entran. La
                apariencia colectiva de los caballeros, como la de las damas, es muy
                imponente. Todos van vestidos de negro; la mayoría son altos,
                algunos jóvenes. Henry y Frederick Lynn son unos petimetres muy
                apuestos, por cierto; y el Coronel Dent es un hombre de aspecto
                militar distinguido. El señor Eshton, el magistrado del distrito, es un
                caballero; su pelo es completamente blanco, sus cejas y patillas aún
                oscuras, lo que le da algo de la apariencia de un «<i>père noble de
                théâtre</i>». Lord Ingram, como sus hermanas, es muy alto; como
                ellas, también, es guapo; pero comparte la mirada apática y
                lánguida de Mary. Parece tener más longitud de miembros que
                vivacidad de sangre o vigor de cerebro.</p>

                <p>¿Y dónde está el señor Rochester?</p>

                <p>Entra el último. No estoy mirando el arco, pero lo veo entrar.
                Intento concentrar mi atención en esas agujas de hacer punto, en
                las mallas del monedero que estoy formando; deseo pensar solo en
                la labor que tengo en mis manos, ver solo las cuentas de plata y los
                hilos de seda que yacen en mi regazo; mientras que, distintamente
                contemplo su figura, e inevitablemente recuerdo el momento en que
                la vi por última vez; justo después de haberle prestado lo que él
                consideró un servicio esencial, y él, sosteniendo mi mano y
                mirándome a la cara, me examinó con ojos que revelaban un
                corazón lleno y ansioso por desbordarse, en cuyas emociones yo
                tenía parte. ¡Qué cerca me había acercado a él en ese momento!
                ¿Qué había ocurrido desde entonces, calculado para cambiar
                nuestras posiciones relativas? ¡Sin embargo, ahora, qué distantes,
                qué alejados estábamos! Tan alejados que no esperaba que viniera a
                hablarme. No me extrañó cuando, sin mirarme, tomó asiento al otro
                lado de la habitación y comenzó a conversar con algunas de las
                damas.</p>

                <p>Tan pronto como vi que su atención estaba clavada en ellas, y que
                yo podía mirar sin ser observada, mis ojos se sintieron atraídos
                involuntariamente hacia su rostro. No podía mantener mis párpados
                bajo control; se levantaban, y los iris se fijaban en él. Miré, y sentí
                un agudo placer al mirar, un placer precioso pero punzante; oro
                puro, con una acerada punta de agonía; un placer como el que
                podría sentir el hombre que perece de sed que sabe que el pozo al
                que se ha arrastrado está envenenado, pero se inclina y bebe sorbos
                divinos de todos modos.</p>

                <p>Muy cierto es que «la belleza está en el ojo de quien mira». El
                rostro incoloro, oliváceo de mi amo, su frente cuadrada y maciza,
                sus cejas anchas y de azabache, sus ojos profundos, sus rasgos
                fuertes, su boca firme y severa —todo energía, decisión, voluntad—
                no eran hermosos, según la regla; pero eran más que hermosos
                para mí. Estaban llenos de un interés, una influencia que me
                dominaba por completo, que arrebataba mis sentimientos de mi
                propio poder y los encadenaba al suyo. No había tenido la intención
                de amarlo; el lector sabe que había trabajado duro para extirpar de
                mi alma los gérmenes de amor allí detectados; y ahora, a la primera
                visión renovada de él, ¡llegaron espontáneamente, verdes y fuertes!
                Me hizo amarlo sin mirarme.</p>

                <p>Lo comparé con sus invitados. ¿Qué era la gallarda gracia de los
                Lynn, la lánguida elegancia de Lord Ingram, incluso la distinción
                militar del Coronel Dent, en contraste con su aspecto de médula
                nativa y genuino poder? No sentía ninguna simpatía por su
                apariencia, su expresión; sin embargo, podía imaginar que la
                mayoría de los observadores los llamarían atractivos, guapos,
                imponentes; mientras que declararían al señor Rochester a la vez de
                rasgos duros y de aspecto melancólico. Los vi sonreír, reír: no era
                nada; la luz de las velas tenía tanta alma como su sonrisa; el
                tintineo de la campanilla tanto significado como su risa. Vi sonreír al
                señor Rochester: sus rasgos severos se suavizaron; su ojo se volvió
                brillante y dulce a la vez, su rayo a la vez inquisitivo y dulce.
                Hablaba, en ese momento, con Louisa y Amy Eshton. Me extrañó
                verlas recibir con calma esa mirada que a mí me parecía tan
                penetrante. Esperaba que sus ojos bajaran, que su color subiera
                bajo ella; sin embargo, me alegré cuando descubrí que no se
                conmovían en absoluto. «No es para ellas lo que es para mí», pensé.
                «No es de su especie. Creo que es de la mía; estoy segura de que lo
                es, me siento afín a él, entiendo el lenguaje de su semblante y sus
                movimientos. Aunque el rango y la riqueza nos separen
                ampliamente, tengo algo en mi cerebro y en mi corazón, en mi
                sangre y en mis nervios, que me asimila mentalmente a él. ¿Dije,
                hace unos días, que no tenía nada que ver con él más que recibir mi
                salario de sus manos? ¿Me prohibí a mí misma pensar en él bajo
                otra luz que no fuera la de un pagador? ¡Blasfemia contra la
                naturaleza! Cada sentimiento bueno, verdadero y vigoroso que
                tengo se reúne impulsivamente a su alrededor. Sé que debo ocultar
                mis sentimientos; debo sofocar la esperanza; debo recordar que no
                puede importarle mucho. Porque cuando digo que soy de su especie,
                no quiero decir que tengo su fuerza para influir y su hechizo para
                atraer; solo quiero decir que tengo ciertos gustos y sentimientos en
                común con él. Debo, pues, repetir continuamente que estamos
                separados para siempre; y sin embargo, mientras respire y piense,
                debo amarlo».</p>

                <p>Se sirve el café. Las damas, desde que entraron los caballeros, se
                han vuelto vivaces como alondras; la conversación se vuelve
                animada y alegre. El Coronel Dent y el señor Eshton discuten sobre
                política; sus esposas escuchan. Las dos orgullosas viudas, Lady Lynn
                y Lady Ingram, confabulan juntas. Sir George —a quien, por cierto,
                he olvidado describir, un caballero rural muy grande y de aspecto
                muy fresco— está de pie ante su sofá, con la taza de café en la
                mano, y de vez en cuando interviene. El señor Frederick Lynn ha
                tomado asiento junto a Mary Ingram y le está mostrando los
                grabados de un espléndido volumen; ella mira, sonríe de vez en
                cuando, pero aparentemente dice poco. El alto y flemático Lord
                Ingram se apoya con los brazos cruzados en el respaldo de la silla de
                la pequeña y vivaz Amy Eshton; ella lo mira y parlotea como un
                reyezuelo. A ella le gusta más que el señor Rochester. Henry Lynn se
                ha apoderado de un puf a los pies de Louisa; Adèle lo comparte con
                él. Él está tratando de hablar francés con ella, y Louisa se ríe de sus
                errores. ¿Con quién se emparejará Blanche Ingram? Está de pie sola
                en la mesa, inclinándose graciosamente sobre un álbum. Parece
                esperar que la busquen; pero no esperará demasiado. Ella misma
                selecciona un compañero.</p>

                <p>El señor Rochester, habiendo dejado a las Eshton, está de pie en
                el hogar tan solitario como ella está junto a la mesa. Ella lo
                confronta, tomando su puesto en el lado opuesto de la repisa de la
                chimenea.</p>

                <p>—Señor Rochester, ¿creía que no le gustaban los niños?</p>

                <p>—Ni me gustan.</p>

                <p>—Entonces, ¿qué le indujo a hacerse cargo de una muñequita
                como esa? (señalando a Adèle). ¿Dónde la recogió?</p>

                <p>—No la recogí; me la dejaron en mis manos.</p>

                <p>—Debería haberla enviado a la escuela.</p>

                <p>—No podía permitírmelo; las escuelas son tan caras.</p>

                <p>—Vaya, supongo que tiene una institutriz para ella. Vi a una
                persona con ella hace un momento, ¿se ha ido? ¡Oh, no! Allí está
                todavía, detrás de la cortina de la ventana. Le paga, por supuesto;
                yo pensaría que es igual de caro, más aún, porque tiene que
                mantener a las dos además.</p>

                <p>Temí —¿o debería decir, esperé?— que la alusión a mí hiciera que
                el señor Rochester mirara en mi dirección; e involuntariamente me
                encogí más en la sombra. Pero nunca volvió los ojos.</p>

                <p>—No he considerado el asunto —dijo con indiferencia, mirando
                directamente al frente.</p>

                <p>—No, ustedes los hombres nunca consideran la economía y el
                sentido común. Debería oír a mamá sobre el capítulo de las
                institutrices. Mary y yo hemos tenido, creo, al menos una docena en
                nuestros días; la mitad de ellas detestables y el resto ridículas, y
                todas unos íncubos, ¿no es así, mamá?</p>

                <p>—¿Hablaste, tesoro mío?</p>

                <p>La joven dama así reclamada como propiedad especial de la viuda,
                reiteró su pregunta con una explicación.</p>

                <p>—Querida mía, no menciones a las institutrices; la palabra me
                pone nerviosa. He sufrido un martirio por su incompetencia y
                capricho. ¡Gracias al cielo que ya he terminado con ellas!</p>

                <p>La señora Dent se inclinó entonces hacia la piadosa dama y le
                susurró algo al oído. Supongo, por la respuesta obtenida, que fue un
                recordatorio de que una de la raza anatematizada estaba presente.</p>

                <p>—<i>Tant pis!</i> —dijo su Señoría—. ¡Espero que le sirva de algo! —
                Luego, en un tono más bajo, pero aún lo suficientemente alto como
                para que yo lo oyera—: La he observado; soy una jueza de la
                fisonomía, y en la suya veo todas las faltas de su clase.</p>

                <p>—¿Cuáles son, señora? —inquirió el señor Rochester en voz alta.</p>

                <p>—Se lo diré en privado —replicó ella, meneando su turbante tres
                veces con una significación portentosa.</p>

                <p>—Pero mi curiosidad habrá perdido el apetito; anhela alimento ahora.</p>

                <p>—Pregúntele a Blanche; está más cerca de usted que yo.</p>

                <p>—¡Oh, no me lo refiera a mí, mamá! Solo tengo una palabra que
                decir de toda la tribu: son una molestia. No es que yo haya sufrido
                mucho por ellas; me encargué de darles la vuelta a la tortilla. ¡Qué
                bromas les hacíamos Theodore y yo a nuestras señoritas Wilson, y
                señoras Grey, y Madame Joubert! Mary siempre estaba demasiado
                somnolienta para unirse a un complot con espíritu. La mejor
                diversión era con Madame Joubert. La señorita Wilson era una pobre
                criatura enfermiza, lacrimosa y de bajo espíritu, que no valía la pena
                el esfuerzo de vencer, en resumen; y la señora Grey era tosca e
                insensible; ningún golpe le hacía efecto. ¡Pero la pobre Madame
                Joubert! Aún la veo en sus furiosas pasiones, cuando la habíamos
                llevado al extremo: derramado nuestro té, desmigajado nuestro pan
                con mantequilla, lanzado nuestros libros hasta el techo y tocado un
                charivari con la regla y el pupitre, el guardafuegos y los hierros de la
                chimenea. Theodore, ¿recuerdas esos días alegres?</p>

                <p>—Yaas, por supuesto que sí —arrastró Lord Ingram—. Y la pobre
                vieja solía gritar: «¡Oh, bribones!». Y luego la sermoneábamos sobre
                la presunción de intentar enseñar a unos espabilados como
                nosotros, cuando ella misma era tan ignorante.</p>

                <p>—Lo hacíamos; y, Tedo, ya sabes, te ayudé a perseguir (o a
                atormentar) a tu tutor, el pálido señor Vining, el párroco con pepita,
                como solíamos llamarlo. Él y la señorita Wilson se tomaron la
                libertad de enamorarse el uno del otro, al menos Tedo y yo lo
                pensamos. Sorprendimos diversas miradas tiernas y suspiros que
                interpretamos como señales de «<i>la belle passion</i>», y te prometo que
                el público pronto tuvo el beneficio de nuestro descubrimiento. Lo
                empleamos como una especie de palanca para sacar nuestros pesos
                muertos de la casa. Querida mamá, en cuanto se enteró del asunto,
                descubrió que tenía una tendencia inmoral. ¿No es así, mi señora
                madre?</p>

                <p>—Ciertamente, mi bien. Y tenía toda la razón. Confía en eso. Hay
                mil razones por las que los líos entre institutrices y tutores nunca
                deben ser tolerados ni un momento en ninguna casa bien regulada.
                En primer lugar...</p>

                <p>—¡Oh, por Dios, mamá! ¡Ahórrenos la enumeración! Au reste,
                todos las conocemos: peligro de mal ejemplo para la inocencia de la
                infancia; distracciones y consiguiente negligencia del deber por parte
                de los enamorados; alianza y confianza mutuas; confianza de la que
                resulta la insolencia que la acompaña, el motín y el estallido general.
                ¿Tengo razón, baronesa Ingram, de Ingram Park?</p>

                <p>—Mi flor de lis, tienes razón ahora, como siempre.</p>

                <p>—Entonces no hay más que decir. Cambiemos de tema.</p>
                
                <p>Amy Eshton, no oyendo o no haciendo caso de este dictamen, se
                unió con su tono suave e infantil:</p>

                <p>—Louisa y yo también nos burlábamos de nuestra institutriz; pero
                era una criatura tan buena que lo aguantaba todo. Nada la sacaba
                de sus casillas. Nunca se enfadaba con nosotras, ¿verdad, Louisa?</p>

                <p>—No, nunca. Podíamos hacer lo que quisiéramos: registrar su
                escritorio y su costurero, y revolver sus cajones; y era tan
                bondadosa que nos daba cualquier cosa que le pidiéramos.</p>

                <p>—Supongo, ahora —dijo la señorita Ingram, curvando el labio
                sarcásticamente—, que tendremos un resumen de las memorias de
                todas las institutrices existentes. Para evitar tal visita, propongo de
                nuevo la introducción de un nuevo tema. Señor Rochester, ¿secunda
                mi moción?</p>

                <p>—Señora, la apoyo en este punto, como en cualquier otro.</p>

                <p>—Entonces, sobre mí recaiga el peso de presentarlo. <i>Signior</i>
                Eduardo, ¿está usted en voz esta noche?</p>

                <p>—<i>Donna Bianca</i>, si usted lo ordena, lo estaré.</p>

                <p>—Entonces, <i>signior</i>, le impongo mi soberano mandato de que
                prepare sus pulmones y otros órganos vocales, ya que serán
                necesarios para mi real servicio.</p>

                <p>—¿Quién no sería el <i>Rizzio</i> de tan divina María?</p>

                <p>—¡Un higo para <i>Rizzio</i>! —exclamó ella, echando hacia atrás la
                cabeza con todos sus rizos, mientras se dirigía al piano—. En mi
                opinión, el violinista David debió de ser un tipo insípido. Prefiero al
                negro Bothwell. A mi parecer, un hombre no es nada sin una pizca
                de diablo en él; y la historia puede decir lo que quiera de James
                Hepburn, pero tengo la idea de que era justo el tipo de héroe
                salvaje, feroz y bandido al que habría consentido en entregarle mi
                mano.</p>

                <p>—¡Caballeros, ya oyen! Ahora, ¿cuál de ustedes se parece más a
                Bothwell? —exclamó el señor Rochester.</p>

                <p>—Yo diría que la preferencia recae en usted —respondió el Coronel Dent.</p>

                <p>—Por mi honor, le estoy muy agradecido —fue la respuesta.</p>

                <p>La señorita Ingram, que ahora se había sentado con orgullosa
                gracia al piano, extendiendo sus níveas túnicas con amplitud regia,
                comenzó un brillante preludio, hablando mientras tanto. Parecía
                estar en sus trece esta noche; tanto sus palabras como su aire
                parecían destinados a excitar no solo la admiración, sino el asombro
                de sus oyentes. Estaba evidentemente empeñada en impresionarlos
                como algo muy audaz y atrevido, en verdad.</p>

                <p>—¡Oh, estoy tan harta de los jóvenes de hoy en día! —exclamó,
                mientras aporreaba el instrumento—. ¡Pobres criaturas
                insignificantes, no aptas para dar un paso más allá de las verjas del
                parque de papá, ni para ir siquiera tan lejos sin el permiso y la tutela
                de mamá! ¡Criaturas tan absortas en el cuidado de sus bonitos
                rostros, y sus manos blancas, y sus pies pequeños! ¡Como si un
                hombre tuviera algo que ver con la belleza! ¡Como si la hermosura
                no fuera la prerrogativa especial de la mujer, su legítimo patrimonio
                y herencia! Concedo que una mujer fea es una mancha en el
                hermoso rostro de la creación; pero en cuanto a los caballeros, que
                se preocupen solo de poseer fuerza y valor. Que su lema sea:
                «Cazar, disparar y luchar»; el resto no vale un comino. Tal debería
                ser mi lema, si yo fuera un hombre.</p>

                <p>—Cuando me case —continuó tras una pausa que nadie
                interrumpió—, estoy resuelta a que mi marido no sea un rival, sino
                un contraste para mí. No sufriré ningún competidor cerca del trono;
                exigiré un homenaje indiviso. Sus devociones no serán compartidas
                entre yo y la figura que ve en su espejo. Señor Rochester, ahora
                cante, y yo tocaré para usted.</p>

                <p>—Soy todo obediencia —fue la respuesta.</p>

                <p>—Aquí tiene entonces una canción de corsario. Sepa que adoro a
                los corsarios; y por esa razón, cántela con <i>spirito</i>.</p>

                <p>—Las órdenes de los labios de la señorita Ingram infundirían
                espíritu en una taza de leche con agua.</p>

                <p>—Tenga cuidado, entonces. Si no me complace, lo avergonzaré
                mostrando cómo deben hacerse tales cosas.</p>

                <p>—Eso es ofrecer una prima a la incapacidad. Ahora me esforzaré por fallar.</p>

                <p>—<i>Gardez-vous en bien!</i> Si yerra voluntariamente, idearé un castigo proporcionado.</p>

                <p>—La señorita Ingram debería ser clemente, pues está en su poder
                infligir un castigo más allá de la resistencia mortal.</p>

                <p>—¡Ja! ¡Explíquese! —ordenó la dama.</p>

                <p>—Perdóneme, señora. No hay necesidad de explicación; su propio
                fino sentido debe informarle que uno de sus ceños fruncidos sería un
                sustituto suficiente de la pena capital.</p>

                <p>—¡Cante! —dijo ella, y tocando de nuevo el piano, comenzó un
                acompañamiento con estilo enérgico.</p>

                <p>«Ahora es mi momento de escabullirme», pensé. Pero los tonos
                que entonces rasgaron el aire me detuvieron. La señora Fairfax
                había dicho que el señor Rochester poseía una hermosa voz. La
                tenía, un bajo melodioso y potente, en el que vertía su propio
                sentimiento, su propia fuerza; encontrando un camino a través del
                oído hasta el corazón, y despertando allí sensaciones extrañas.
                Esperé hasta que la última vibración profunda y plena hubo
                expirado, hasta que la marea de la conversación, contenida un
                instante, hubo reanudado su flujo. Entonces abandoné mi rincón
                protegido y salí por la puerta lateral, que afortunadamente estaba
                cerca. Desde allí, un pasillo estrecho conducía al vestíbulo. Al
                cruzarlo, me di cuenta de que mi sandalia estaba suelta; me detuve
                para atarla, arrodillándome para ello en la estera al pie de la
                escalera. Oí abrirse la puerta del comedor; salió un caballero.
                Levantándome apresuradamente, me encontré cara a cara con él:
                era el señor Rochester.</p>

                <p>—¿Cómo está usted? —preguntó.</p>

                <p>—Estoy muy bien, señor.</p>

                <p>—¿Por qué no vino a hablarme en la habitación?</p>

                <p>Pensé que podría haberle devuelto la pregunta a quien la
                formulaba, pero no me tomaría esa libertad. Respondí:</p>

                <p>—No deseaba molestarlo, ya que parecía ocupado, señor.</p>

                <p>—¿Qué ha estado haciendo durante mi ausencia?</p>

                <p>—Nada en particular; enseñando a Adèle como de costumbre.</p>

                <p>—Y poniéndose mucho más pálida de lo que estaba, como vi a
                primera vista. ¿Qué le pasa?</p>

                <p>—Nada en absoluto, señor.</p>

                <p>—¿Se resfrió aquella noche que casi me ahoga?</p>

                <p>—Ni lo más mínimo.</p>

                <p>—Vuelva al salón. Está desertando demasiado pronto.</p>

                <p>—Estoy cansada, señor.</p>

                <p>Me miró durante un minuto.</p>

                <p>—Y un poco deprimida —dijo—. ¿Por qué? Dígame.</p>

                <p>—Nada, nada, señor. No estoy deprimida.</p>

                <p>—Pero afirmo que lo está. Tan deprimida que unas pocas palabras
                más le traerían lágrimas a los ojos; de hecho, ya están ahí, brillando
                y nadando; y una gota se ha deslizado de la pestaña y ha caído
                sobre la losa. Si tuviera tiempo, y no estuviera en un temor mortal
                de que pase algún criado charlatán, sabría qué significa todo esto.
                Bueno, esta noche la excuso; pero entienda que mientras mis visitas
                se queden, espero que aparezca en el salón todas las tardes. Es mi
                deseo; no lo descuide. Ahora vaya, y envíe a Sophie a por Adèle.
                Buenas noches, mi... —Se detuvo, se mordió el labio y me dejó
                bruscamente.</p>

