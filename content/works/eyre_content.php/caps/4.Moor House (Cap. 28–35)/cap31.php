                <h1>Capítulo XXXI</h1>
    
                <p>Mi hogar, entonces, cuando por fin encuentro un hogar, es una
                cabaña; una pequeña habitación con paredes encaladas y un suelo
                enarenado, que contiene cuatro sillas pintadas y una mesa, un reloj,
                un armario con dos o tres platos y fuentes, y un juego de té de loza
                de Delft. Arriba, una cámara de las mismas dimensiones que la
                cocina, con un catre de pino y una cómoda; pequeña, pero
                demasiado grande para ser llenada con mi escaso vestuario, aunque
                la amabilidad de mis gentiles y generosos amigos lo ha aumentado
                con una modesta provisión de las cosas necesarias.</p>

                <p>Es de noche. He despedido, con el pago de una naranja, a la
                pequeña huérfana que me sirve de criada. Estoy sentada sola junto
                al hogar. Esta mañana se inauguró la escuela del pueblo. Tuve veinte
                alumnas. Pero solo tres de ellas saben leer; ninguna escribir ni
                contar. Varias tejen y unas pocas cosen un poco. Hablan con el
                acento más cerrado del distrito. Por el momento, ellas y yo tenemos
                dificultades para entendernos el idioma. Algunas de ellas son
                maleducadas, rudas, intratables, además de ignorantes; pero otras
                son dóciles, tienen ganas de aprender y manifiestan una disposición
                que me agrada. No debo olvidar que estas pequeñas campesinas
                toscamente vestidas son de carne y hueso tan buenos como los
                vástagos de la más noble genealogía; y que los gérmenes de la
                excelencia nativa, el refinamiento, la inteligencia, el sentimiento
                bondadoso, tienen tantas probabilidades de existir en sus corazones
                como en los de los mejor nacidos. Mi deber será desarrollar estos
                gérmenes. Seguramente encontraré alguna felicidad en el
                desempeño de ese oficio. No espero mucho goce en la vida que se
                abre ante mí; sin embargo, sin duda, si regulo mi mente y ejerzo mis
                poderes como debo, me proporcionará lo suficiente para vivir día a
                día.</p>

                <p>¿Estuve muy alegre, asentada, contenta, durante las horas que
                pasé en aquella desnuda y humilde aula esta mañana y esta tarde?
                Para no engañarme a mí misma, debo responder: no. Me sentí
                desolada hasta cierto punto. Me sentí —sí, idiota que soy— me sentí
                degradada. Dudé de haber dado un paso que me hundía en lugar de
                elevarme en la escala de la existencia social. Me consternó
                débilmente la ignorancia, la pobreza, la tosquedad de todo lo que
                oía y veía a mi alrededor. Pero no me odie ni me desprecie
                demasiado por estos sentimientos; sé que son erróneos, eso es un
                gran paso ganado; me esforzaré por superarlos. Mañana, confío, los
                superaré parcialmente; y en unas pocas semanas, quizás, estarán
                completamente sometidos. En unos pocos meses, es posible, la
                felicidad de ver el progreso y un cambio a mejor en mis alumnas
                pueda sustituir la gratificación por el asco.</p>

                <p>Mientras tanto, permítaseme hacerme una pregunta: ¿Qué es
                mejor? ¿Haberse rendido a la tentación; haber escuchado a la
                pasión; no haber hecho ningún esfuerzo doloroso, ninguna lucha;
                sino haberse hundido en la trampa de seda; haberse quedado
                dormida sobre las flores que la cubrían; haber despertado en un
                clima sureño, entre los lujos de una villa de placer? ¿Haber estado
                viviendo ahora en Francia, como la amante del señor Rochester;
                delirante con su amor la mitad de mi tiempo —porque él, oh, sí, me
                habría amado bien por un tiempo—? Él sí me amó, nadie volverá a
                amarme así. Nunca más conoceré el dulce homenaje rendido a la
                belleza, la juventud y la gracia, pues nunca para nadie más pareceré
                poseer esos encantos. Él me quería y estaba orgulloso de mí, es algo
                que ningún otro hombre será jamás. Pero, ¿adónde divago y qué
                estoy diciendo y, sobre todo, sintiendo? Pregunto, ¿qué es mejor:
                ser una esclava en el paraíso de un necio en Marsella —afiebrada
                con una dicha ilusoria una hora, sofocada con las lágrimas más
                amargas del remordimiento y la vergüenza a la siguiente— o ser una
                maestra de pueblo, libre y honesta, en un rincón ventoso de
                montaña en el sano corazón de Inglaterra?</p>

                <p>Sí; siento ahora que hice lo correcto cuando me adherí al principio
                y a la ley, y desprecié y aplasté los impulsos insensatos de un
                momento frenético. Dios me dirigió a una elección correcta.
                ¡Agradezco a Su providencia por la guía!</p>

                <p>Habiendo llevado mis cavilaciones vespertinas a este punto, me
                levanté, fui a mi puerta y miré el atardecer del día de la cosecha, y
                los campos tranquilos ante mi cabaña, la cual, junto con la escuela,
                distaba media milla del pueblo. Los pájaros cantaban sus últimas
                estrofas:</p>

                <p>«El aire era suave, el rocío era bálsamo».</p>

                <p>Mientras miraba, me creí feliz, y me sorprendió encontrarme
                llorando al poco rato. ¿Y por qué? Por el destino que me había
                privado de la adhesión a mi amo; por él, a quien no volvería a ver;
                por el dolor desesperado y la furia fatal —consecuencias de mi
                partida— que ahora, quizás, podrían estar arrastrándolo fuera del
                camino del bien, demasiado lejos para dejar esperanza de una
                restauración final allí. Ante este pensamiento, aparté mi rostro del
                hermoso cielo del atardecer y del solitario valle de Morton. Digo
                solitario, pues en esa curva visible para mí no había ningún edificio
                aparente salvo la iglesia y la rectoría, medio ocultas entre los
                árboles, y, justo en el extremo, el tejado de Vale Hall, donde vivían
                el rico señor Oliver y su hija. Oculté mis ojos y apoyé la cabeza en el
                marco de piedra de mi puerta; pero pronto un ligero ruido cerca del
                postigo que separaba mi diminuto jardín del prado de más allá me
                hizo levantar la vista. Un perro —el viejo Carlo, el pointer del señor
                Rivers, como vi en un momento— empujaba la verja con el hocico, y
                el propio St. John se apoyaba en ella con los brazos cruzados; el
                ceño fruncido, la mirada, grave casi hasta el disgusto, fija en mí. Le
                pedí que entrara.</p>

                <p>—No, no puedo quedarme; solo le he traído un pequeño paquete
                que mis hermanas dejaron para usted. Creo que contiene una caja
                de acuarelas, lápices y papel.</p>

                <p>Me acerqué a tomarlo. Era un regalo bienvenido. Examinó mi
                rostro, pensé, con austeridad, mientras me acercaba. Las huellas de
                las lágrimas eran, sin duda, muy visibles en él.</p>

                <p>—¿Ha encontrado el trabajo de su primer día más duro de lo que
                esperaba? —preguntó.</p>

                <p>—¡Oh, no! Al contrario, creo que con el tiempo me llevaré muy
                bien con mis alumnas.</p>

                <p>—¿Pero quizás su alojamiento, su cabaña, sus muebles, han
                decepcionado sus expectativas? Son, en verdad, bastante escasos;
                pero... —interrumpí.</p>

                <p>—Mi cabaña está limpia y es a prueba de intemperie; mis muebles
                son suficientes y cómodos. Todo lo que veo me ha hecho sentir
                agradecida, no abatida. No soy absolutamente tan tonta y
                sensualista como para lamentar la ausencia de una alfombra, un
                sofá y cubiertos de plata. Además, hace cinco semanas no tenía
                nada: era una paria, una mendiga, una vagabunda. Ahora tengo
                conocidos, un hogar, un oficio. Me maravillo de la bondad de Dios,
                de la generosidad de mis amigos, de la generosidad de mi suerte.
                No me quejo.</p>

                <p>—¿Pero siente la soledad como una opresión? La casita ahí detrás
                de usted está oscura y vacía.</p>

                <p>—Apenas he tenido tiempo aún de disfrutar de una sensación de
                tranquilidad, mucho menos de impacientarme bajo la soledad.</p>

                <p>—Muy bien; espero que sienta el contento que expresa. En
                cualquier caso, su buen sentido le dirá que es demasiado pronto
                para ceder a los temores vacilantes de la mujer de Lot. Lo que había
                dejado antes de que la viera, por supuesto, no lo sé; pero le
                aconsejo que resista firmemente toda tentación que la incline a
                mirar atrás. Siga su carrera actual con constancia, al menos durante
                algunos meses.</p>

                <p>—Es lo que pienso hacer —respondí. St. John continuó:</p>

                <p>—Es un trabajo duro controlar los impulsos de la inclinación y
                torcer el curso de la naturaleza; pero que puede hacerse, lo sé por
                experiencia. Dios nos ha dado, en cierta medida, el poder de forjar
                nuestro propio destino; y cuando nuestras energías parecen
                demandar un sustento que no pueden obtener, cuando nuestra
                voluntad se esfuerza por seguir un camino que no podemos seguir,
                no necesitamos morir de inanición ni quedarnos parados en la
                desesperación. Solo tenemos que buscar otro alimento para la
                mente, tan fuerte como el alimento prohibido que anhelaba probar, y
                quizás más puro; y labrar para el pie aventurero un camino tan
                directo y ancho como el que la Fortuna nos ha bloqueado, aunque
                más áspero que él.</p>

                <p>»Hace un año yo mismo era intensamente desdichado, porque
                pensé que había cometido un error al entrar en el ministerio. Sus
                deberes uniformes me cansaban hasta la muerte. Ardía por la vida
                más activa del mundo, por los trabajos más emocionantes de una
                carrera literaria, por el destino de un artista, autor, orador; cualquier
                cosa antes que el de un sacerdote. Sí, el corazón de un político, de
                un soldado, de un devoto de la gloria, un amante de la fama, un
                codicioso de poder, latía bajo mi sobrepelliz de cura. Lo consideré;
                mi vida era tan miserable que debía cambiar, o debía morir. Tras una
                temporada de oscuridad y lucha, la luz irrumpió y el alivio cayó. Mi
                existencia encorsetada se extendió de repente a una llanura sin
                límites; mis poderes oyeron una llamada del cielo para levantarse,
                reunir toda su fuerza, desplegar sus alas y ascender más allá de la
                vista. Dios tenía un encargo para mí; para llevarlo lejos, para
                entregarlo bien, se necesitaban habilidad y fuerza, coraje y
                elocuencia, las mejores cualidades de soldado, estadista y orador.
                Pues todas ellas se centran en el buen misionero.</p>

                <p>»Resolví ser misionero. Desde ese momento mi estado de ánimo
                cambió; los grilletes se disolvieron y cayeron de cada facultad, no
                dejando de la esclavitud más que su irritante llaga, que solo el
                tiempo puede curar. Mi padre, en verdad, impuso la determinación,
                pero desde su muerte no tengo un obstáculo legítimo con el que
                luchar; algunos asuntos resueltos, un sucesor para Morton provisto,
                un enredo o dos de los sentimientos rotos o cortados de raíz, un
                último conflicto con la debilidad humana, en el que sé que venceré,
                porque he jurado que venceré, y dejo Europa por el Este.</p>

                <p>Dijo esto, con su voz peculiar, contenida pero enfática; mirando,
                cuando hubo cesado de hablar, no a mí, sino al sol poniente, al que
                yo también miré. Ambos teníamos la espalda hacia el sendero que
                subía por el campo hacia el postigo. No habíamos oído ningún paso
                en esa senda cubierta de hierba; el agua que corría en el valle era el
                único sonido arrullador de la hora y la escena. Bien podríamos
                entonces sobresaltarnos cuando una voz alegre, dulce como una
                campana de plata, exclamó:</p>

                <p>—Buenas tardes, señor Rivers. Y buenas tardes, viejo Carlo. Su
                perro es más rápido para reconocer a sus amigos que usted, señor;
                aguzó las orejas y movió la cola cuando yo estaba al fondo del
                campo, y usted tiene la espalda hacia mí ahora.</p>

                <p>Era cierto. Aunque el señor Rivers se había sobresaltado al
                primero de aquellos acentos musicales, como si un rayo hubiera
                partido una nube sobre su cabeza, permanecía aún, al final de la
                frase, en la misma actitud en que la que hablaba lo había
                sorprendido: su brazo apoyado en la verja, su rostro dirigido hacia el
                oeste. Se volvió al fin, con medida deliberación. Una visión, me
                pareció, se había alzado a su lado. Apareció, a menos de tres pies de
                él, una forma vestida de blanco puro, una forma juvenil y grácil,
                llena pero de contorno fino; y cuando, después de inclinarse para
                acariciar a Carlo, levantó la cabeza y echó hacia atrás un largo velo,
                floreció bajo su mirada un rostro de perfecta belleza. Belleza
                perfecta es una expresión fuerte, pero no la retracto ni la matizo.
                Rasgos tan dulces como los que jamás moldeó el clima templado de
                Albión; tonos tan puros de rosa y lirio como los que jamás generaron
                y protegieron sus húmedos vientos y cielos vaporosos, justificaban,
                en este caso, el término. No faltaba ningún encanto, no se percibía
                ningún defecto. La joven tenía rasgos regulares y delicados; ojos con
                la forma y el color que vemos en los cuadros hermosos, grandes y
                oscuros y llenos; la pestaña larga y sombreada que rodea un ojo fino
                con tan suave fascinación; la ceja dibujada que da tal claridad; la
                frente blanca y lisa, que añade tal reposo a las bellezas más vivas de
                tinte y rayo; la mejilla ovalada, fresca y suave; los labios, frescos
                también, sonrosados, saludables, dulcemente formados; los dientes
                uniformes y relucientes sin defecto; la pequeña barbilla con
                hoyuelos; el adorno de trenzas ricas y abundantes; todas las
                ventajas, en resumen, que, combinadas, realizan el ideal de belleza,
                eran plenamente suyas. Me maravillé, al mirar a esta bella criatura.
                La admiré con todo mi corazón. La naturaleza seguramente la había
                formado en un estado de ánimo parcial; y, olvidando su habitual y
                mezquina dote de madrastra, había dotado a esta, su favorita, con la
                generosidad de una abuela.</p>

                <p>¿Qué pensaba St. John Rivers de este ángel terrenal?
                Naturalmente me hice esa pregunta al verlo volverse hacia ella y
                mirarla; y, naturalmente, busqué la respuesta a la pregunta en su
                semblante. Ya había retirado su ojo de la Peri y miraba un humilde
                mechón de margaritas que crecía junto al postigo.</p>

                <p>—Una tarde encantadora, pero tarde para que esté fuera sola —
                dijo, mientras aplastaba con el pie las níveas cabezas de las flores
                cerradas.</p>

                <p>—Oh, acabo de llegar a casa desde S... (mencionó el nombre de
                una gran ciudad a unas veinte millas de distancia) esta tarde. Papá
                me dijo que había abierto su escuela y que la nueva maestra había
                llegado; así que me puse el gorro después del té y subí corriendo
                por el valle para verla. ¿Es esta? —dijo, señalándome.</p>

                <p>—Lo es —dijo St. John.</p>

                <p>—¿Cree que le gustará Morton? —me preguntó, con una sencillez
                de tono y maneras directa e ingenua, agradable, aunque infantil.</p>

                <p>—Espero que sí. Tengo muchos incentivos para ello.</p>

                <p>—¿Encontró a sus alumnas tan atentas como esperaba?</p>

                <p>—Bastante.</p>

                <p>—¿Le gusta su casa?</p>

                <p>—Muchísimo.</p>

                <p>—¿La he amueblado bien?</p>

                <p>—Muy bien, por cierto.</p>

                <p>—¿Y he hecho una buena elección de una ayudante para usted en Alice Wood?</p>

                <p>—Sí, por cierto. Es dócil y hábil. —(Esta, entonces, pensé, es la
                señorita Oliver, la heredera; favorecida, al parecer, en los dones de la
                fortuna, así como en los de la naturaleza. ¡Qué feliz combinación de
                planetas presidió su nacimiento, me pregunto?).</p>

                <p>—Subiré a ayudarle a enseñar a veces —añadió—. Será un cambio
                para mí visitarla de vez en cuando; y me gusta un cambio. Señor
                Rivers, he estado tan alegre durante mi estancia en S... Anoche, o
                más bien esta mañana, estuve bailando hasta las dos. El
                regimiento... está acantonado allí desde los disturbios; y los oficiales
                son los hombres más agradables del mundo. Dejan en ridículo a
                todos nuestros jóvenes afiladores de cuchillos y comerciantes de
                tijeras.</p>

                <p>Me pareció que el labio inferior del señor St. John sobresalía y el
                superior se curvaba un momento. Su boca ciertamente parecía muy
                comprimida, y la parte inferior de su rostro inusualmente severa y
                cuadrada, mientras la chica risueña le daba esta información.
                Levantó la mirada, también, de las margaritas y la volvió hacia ella.
                Una mirada sin sonrisa, inquisitiva, significativa era. Ella le respondió
                con una segunda risa, y la risa le sentaba bien a su juventud, a sus
                rosas, a sus hoyuelos, a sus ojos brillantes.</p>

                <p>Mientras él permanecía mudo y grave, ella volvió a acariciar a Carlo.</p>

                <p>—El pobre Carlo me quiere —dijo—. Él no es severo y distante con
                sus amigos; y si pudiera hablar, no estaría en silencio.</p>

                <p>Mientras acariciaba la cabeza del perro, inclinándose con gracia
                nativa ante su joven y austero amo, vi un rubor subir al rostro de
                ese amo. Vi su ojo solemne derretirse con un fuego repentino y
                parpadear con una emoción irresistible. Así, sonrojado y encendido,
                parecía casi tan hermoso para un hombre como ella para una mujer.
                Su pecho se agitó una vez, como si su gran corazón, cansado de la
                constricción despótica, se hubiera expandido, a pesar de la voluntad,
                y hubiera dado un vigoroso salto para alcanzar la libertad. Pero lo
                refrenó, creo, como un jinete resuelto refrenaría un corcel
                encabritado. No respondió ni con palabras ni con movimientos a los
                gentiles avances que se le hicieron.</p>

                <p>—Papá dice que ya nunca vienes a vernos —continuó la señorita
                Oliver, levantando la vista—. Eres un completo extraño en Vale Hall.
                Está solo esta noche y no muy bien. ¿Volverás conmigo a visitarlo?</p>

                <p>—No es una hora oportuna para importunar al señor Oliver —
                respondió St. John.</p>

                <p>—¡No es una hora oportuna! Pero declaro que lo es. Es justo la
                hora en que papá más necesita compañía, cuando las fábricas están
                cerradas y no tiene negocios que lo ocupen. Ahora, señor Rivers,
                venga. ¿Por qué es usted tan tímido y tan sombrío? —Llenó el hiato
                que su silencio dejaba con una respuesta propia.</p>

                <p>—¡Lo olvidé! —exclamó, sacudiendo su hermosa cabeza rizada,
                como si estuviera escandalizada de sí misma—. ¡Soy tan atolondrada
                y desconsiderada! Discúlpeme. Se me había olvidado que tiene
                buenas razones para no estar dispuesto a unirse a mi cháchara.
                Diana y Mary lo han dejado, y Moor House está cerrada, y está
                usted tan solo. Estoy segura de que lo compadezco. Venga a ver a
                papá.</p>

                <p>—No esta noche, señorita Rosamond, no esta noche.</p>

                <p>El señor St. John habló casi como un autómata; solo él sabía el
                esfuerzo que le costaba negarse así.</p>

                <p>—Bueno, si es usted tan obstinado, lo dejaré; pues no me atrevo a
                quedarme más tiempo. El rocío empieza a caer. ¡Buenas noches!</p>

                <p>Le tendió la mano. Él apenas la tocó.</p>

                <p>—¡Buenas noches! —repitió, con una voz baja y hueca como un
                eco. Ella se volvió, pero al momento regresó.</p>

                <p>—¿Está usted bien? —preguntó. Bien podría hacer la pregunta: su
                rostro estaba tan blanco como el vestido de ella.</p>

                <p>—Completamente bien —enunció; y, con una reverencia, dejó la
                verja. Ella fue por un lado; él por otro. Ella se volvió dos veces para
                mirarlo mientras bajaba como un hada por el campo; él, mientras
                cruzaba con paso firme, no se volvió ni una sola vez.</p>

                <p>Este espectáculo del sufrimiento y el sacrificio de otro apartó mis
                pensamientos de la meditación exclusiva sobre los míos. Diana
                Rivers había calificado a su hermano de «inexorable como la
