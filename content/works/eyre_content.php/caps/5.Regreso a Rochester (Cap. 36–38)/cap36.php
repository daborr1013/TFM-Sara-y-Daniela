                <h1>Capítulo XXXVI</h1>
                
                <p>Llegó la luz del día. Me levanté al amanecer. Me ocupé durante una
                o dos horas en arreglar mis cosas en mi cámara, cajones y armario,
                en el orden en que desearía dejarlas durante una breve ausencia.
                Mientras tanto, oí a St. John salir de su habitación. Se detuvo en mi
                puerta; temí que llamara; no, pero un trozo de papel fue pasado por
                debajo de la puerta. Lo recogí. Llevaba estas palabras:</p>

                <p class="centradoP">«Me dejaste demasiado de repente anoche. Si te hubieras
                quedado un poco más, habrías puesto tu mano sobre la cruz del
                cristiano y la corona del ángel. Esperaré tu clara decisión cuando
                regrese dentro de quince días. Mientras tanto, vela y ora para que
                no entres en tentación: el espíritu, confío, está dispuesto, pero la
                carne, veo, es débil. Rezaré por ti cada hora. —Tuyo, ST. JOHN».</p>

                <p>«Mi espíritu», respondí mentalmente, «está dispuesto a hacer lo
                correcto; y mi carne, espero, es lo suficientemente fuerte para
                cumplir la voluntad del Cielo, una vez que esa voluntad me sea
                claramente conocida. En cualquier caso, será lo suficientemente
                fuerte para buscar, inquirir, para tantear una salida de esta nube de
                duda y encontrar el día abierto de la certeza».</p>

                <p>Era el primero de junio; sin embargo, la mańana estaba nublada y
                fría. La lluvia golpeaba con fuerza mi ventanal. Oí abrirse la puerta
                principal y salir a St. John. Mirando por la ventana, lo vi atravesar el
                jardín. Tomó el camino sobre los páramos neblinosos en dirección a
                Whitcross; allí encontraría la diligencia.</p>

                <p>«En unas pocas horas más te sucederé en esa ruta, primo»,
                pensé. «Yo también tengo una diligencia que encontrar en
                Whitcross. Yo también tengo a algunos a quienes ver y por quienes
                preguntar en Inglaterra, antes de partir para siempre».</p>

                <p>Faltaban aún dos horas para el desayuno. Llené el intervalo
                caminando suavemente por mi habitación y reflexionando sobre la
                visitación que había dado a mis planes su actual rumbo. Recordé esa
                sensación interna que había experimentado; pues podía recordarla,
                con toda su indecible extrańeza. Recordé la voz que había oído; de
                nuevo me pregunté de dónde venía, tan vanamente como antes.
                Parecía estar en mí, no en el mundo externo. Pregunté si era una
                mera impresión nerviosa, una ilusión. No podía concebirlo ni creerlo;
                era más como una inspiración. La maravillosa conmoción del
                sentimiento había llegado como el terremoto que sacudió los
                cimientos de la prisión de Pablo y Silas; había abierto las puertas de
                la celda del alma y soltado sus ataduras; la había despertado de su
                sueńo, de donde saltó temblando, escuchando, horrorizada; luego
                vibró tres veces un grito en mi oído sobresaltado y en mi corazón
                tembloroso y a través de mi espíritu, que ni temió ni tembló, sino
                que exultó como de alegría por el éxito de un esfuerzo que había
                tenido el privilegio de hacer, independiente del pesado cuerpo</p>

                <p>«Antes de muchos días», dije, al terminar mis cavilaciones, «sabré
                algo de aquel cuya voz pareció convocarme anoche. Las cartas han
                resultado inútiles; la indagación personal las reemplazará».</p>

                <p>En el desayuno anuncié a Diana y a Mary que iba a hacer un viaje
                y que estaría ausente al menos cuatro días.</p>

                <p>—żSola, Jane? —preguntaron.</p>

                <p>—Sí; era para ver u oír noticias de un amigo por el que llevaba
                algún tiempo inquieta.</p>

                <p>Podrían haber dicho, como no dudo que pensaron, que habían
                creído que yo no tenía más amigos que ellas; pues, en verdad, a
                menudo lo había dicho. Pero, con su verdadera delicadeza natural,
                se abstuvieron de hacer comentarios, excepto que Diana me
                preguntó si estaba segura de encontrarme lo suficientemente bien
                como para viajar. Parecía muy pálida, observó. Respondí que no me
                aquejaba nada salvo la ansiedad de la mente, que esperaba aliviar
                pronto.</p>

                <p>Fue fácil hacer mis arreglos posteriores, pues no me molestaron
                con preguntas ni conjeturas. Habiéndoles explicado una vez que no
                podía ahora ser explícita sobre mis planes, aceptaron amable y
                sabiamente el silencio con el que los perseguía, concediéndome el
                privilegio de la libre acción que yo, en circunstancias similares, les
                habría concedido a ellas.</p>

                <p>Salí de Moor House a las tres de la tarde y, poco después de las
                cuatro, estaba de pie al pie del poste indicador de Whitcross,
                esperando la llegada de la diligencia que me llevaría a la lejana
                Thornfield. En medio del silencio de aquellos solitarios caminos y
                desiertas colinas, la oí acercarse desde una gran distancia. Era el
                mismo vehículo del que, hacía un ańo, me había apeado una tarde
                de verano en este mismo lugar, ˇqué desolada, desesperanzada y sin
                objeto! Se detuvo cuando hice seńas. Entré, no obligada ahora a
                desprenderme de toda mi fortuna como precio de su acomodación.
                Una vez más en el camino a Thornfield, me sentí como la paloma
                mensajera que vuela a casa.</p>

                <p>Fue un viaje de treinta y seis horas. Había partido de Whitcross un
                martes por la tarde y, temprano en la mańana del jueves siguiente,
                la diligencia se detuvo para dar de beber a los caballos en una
                posada de camino, situada en medio de un paisaje cuyos verdes
                setos y grandes campos y bajas colinas pastorales (ˇqué suaves de
                rasgo y verdes de matiz en comparación con los severos páramos
                del centro-norte de Morton!) se encontraron con mis ojos como los
                rasgos de un rostro otrora familiar. Sí, conocía el carácter de este
                paisaje; estaba segura de que estábamos cerca de mi destino.</p>

                <p>—żA qué distancia está Thornfield Hall de aquí? —le pregunté al
                mozo de cuadra.</p>

                <p>—Solo dos millas, seńora, a través de los campos.</p>

                <p>«Mi viaje ha terminado», pensé para mis adentros. Salí de la
                diligencia, le di una caja que tenía al cargo del mozo de cuadra, para
                que la guardara hasta que la reclamara; pagué mi pasaje; satisfice al
                cochero y me iba. El día que clareaba brilló en el letrero de la
                posada y leí en letras doradas: «The Rochester Arms». Mi corazón
                dio un brinco: ya estaba en las mismas tierras de mi amo. Volvió a
                caer; el pensamiento lo golpeó:</p>

                <p>«Tu amo mismo puede estar más allá del Canal de la Mancha, por
                lo que sabes; y entonces, si está en Thornfield Hall, hacia donde te
                apresuras, żquién más hay allí? Su esposa lunática. Y no tienes nada
                que ver con él; no te atreves a hablarle ni a buscar su presencia.
                Has perdido el trabajo, será mejor que no sigas», instó el monitor.
                «Pide información a la gente de la posada; pueden darte todo lo que
                buscas; pueden resolver tus dudas de una vez. Acércate a ese
                hombre y pregunta si el seńor Rochester está en casa».</p>

                <p>La sugerencia era sensata y, sin embargo, no pude obligarme a
                actuar en consecuencia. Temía tanto una respuesta que me
                aplastara de desesperación. Prolongar la duda era prolongar la
                esperanza. Podría ver una vez más la mansión bajo el rayo de su
                estrella. Allí estaba la estela ante mí, los mismos campos por los que
                había corrido, ciega, sorda, distraída, con una furia vengativa que
                me rastreaba y azotaba, la mańana en que huí de Thornfield. Antes
                de que supiera bien qué rumbo había resuelto tomar, estaba en
                medio de ellos. ˇQué rápido caminaba! ˇCómo corría a veces! ˇCómo
                miraba hacia adelante para captar la primera vista de los conocidos
                bosques! ˇCon qué sentimientos acogía los árboles solitarios que
                conocía y los familiares atisbos de prados y colinas entre ellos!</p>

                <p>Por fin se alzaron los bosques; la grajera se agrupó oscura; un
                fuerte graznido rompió la quietud matutina. Un extrańo deleite me
                inspiró; seguí adelante. Otro campo cruzado, un sendero enhebrado,
                y allí estaban los muros del patio, las dependencias traseras. La casa
                misma, la grajera aún oculta. «Mi primera vista de ella será de
                frente», decidí, «donde sus audaces almenas golpearán la vista
                noblemente de una vez, y donde puedo distinguir la misma ventana
                de mi amo. Quizás esté de pie en ella, se levanta temprano. Quizás
                esté ahora paseando por el huerto o por el pavimento de enfrente.
                ˇSi pudiera verlo, aunque fuera un momento! Seguramente, en ese
                caso, żno estaría tan loca como para correr hacia él? No sabría
                decirlo, no estoy segura. Y si lo hiciera, żqué entonces? ˇDios lo
                bendiga! żQué entonces? żQuién resultaría herido por que yo
                probara una vez más la vida que su mirada puede darme? Desvarío.
                Quizás en este momento esté viendo salir el sol sobre los Pirineos o
                sobre el mar sin mareas del sur».</p>

                <p>Había bordeado el muro inferior del huerto, doblado su ángulo.
                Había una verja justo allí, que se abría al prado, entre dos pilares de
                piedra coronados por bolas de piedra. Desde detrás de un pilar podía
                espiar tranquilamente toda la fachada de la mansión. Avancé la
                cabeza con precaución, deseosa de cerciorarme si alguna persiana
                de los dormitorios ya estaba subida. Almenas, ventanas, larga
                fachada, todo desde esta estación resguardada estaba a mi
                disposición.</p>

                <p>Las cornejas que navegaban por encima quizás me observaron
                mientras hacía este reconocimiento. Me pregunto qué pensarían.
                Deben de haber considerado que al principio fui muy cuidadosa y
                tímida, y que gradualmente me volví muy audaz y temeraria. Una
                ojeada, y luego una larga mirada; y luego un abandono de mi nicho
                y un paseo por el prado; y una parada repentina justo enfrente de la
                gran mansión, y una mirada prolongada y osada hacia ella. «żQué
                afectación de timidez era esta al principio?», podrían haber
                preguntado; «żqué estúpida despreocupación ahora?».</p>

                <p>Escuchen una ilustración, lector.</p>

                <p>Un amante encuentra a su amada dormida en un banco de
                musgo; desea echar un vistazo a su hermoso rostro sin despertarla.
                Se desliza suavemente sobre la hierba, cuidando de no hacer ruido;
                se detiene, imaginando que ella se ha movido; se retira. Por nada
                del mundo querría ser visto. Todo está en calma. Avanza de nuevo;
                se inclina sobre ella. Un ligero velo descansa sobre sus rasgos. Lo
                levanta, se inclina más bajo; ahora sus ojos anticipan la visión de la
                belleza, cálida y floreciente y encantadora, en reposo. ˇQué
                apresurada fue su primera mirada! ˇPero cómo se fijan! ˇCómo se
                sobresalta! ˇCómo abraza súbita y vehementemente con ambos
                brazos la forma que no se atrevía, hace un momento, a tocar con el
                dedo! ˇCómo llama en voz alta un nombre, y deja caer su carga, y la
                contempla con fiereza! Así agarra y grita y contempla, porque ya no
                teme despertarla con ningún sonido que pueda proferir, con ningún
                movimiento que pueda hacer. Pensaba que su amor dormía
                dulcemente; la encuentra muerta como una piedra.</p>

                <p>Miré con tímida alegría hacia una majestuosa casa; vi una ruina ennegrecida.</p>

                <p>ˇNo había necesidad de agazaparse detrás de un poste de la verja,
                en verdad! ˇNi de espiar las celosías de las cámaras, temiendo que la
                vida estuviera agitada detrás de ellas! ˇNo había necesidad de
                escuchar puertas que se abrían, de imaginar pasos en el pavimento
                o en el camino de grava! El césped, los terrenos, estaban pisoteados
                y yermos. El portal bostezaba vacío. La fachada era, como la había
                visto una vez en un sueńo, solo un muro como una concha, muy alto
                y de aspecto muy frágil, perforado por ventanas sin cristales. Sin
                techo, sin almenas, sin chimeneas, todo se había derrumbado.</p>

                <p>Y había el silencio de la muerte a su alrededor; la soledad de un
                paraje desolado. No es de extrańar que las cartas dirigidas a la
                gente de aquí nunca hubieran recibido respuesta; lo mismo daría
                despachar epístolas a una cripta en el pasillo de una iglesia. La
                sombría negrura de las piedras delataba por qué destino había caído
                la mansión: por un incendio. żPero cómo se había iniciado? żQué
                historia pertenecía a este desastre? żQué pérdida, además de
                mortero y mármol y carpintería, había seguido a él? żSe había
                naufragado la vida además de la propiedad? Si era así, żla de quién?
                Pregunta terrible. No había nadie aquí para responderla, ni siquiera
                una seńal muda, un mudo indicio.</p>

                <p>Vagando alrededor de los muros destrozados y a través del
                interior devastado, reuní pruebas de que la calamidad no era de
                ocurrencia reciente. Las nieves invernales, pensé, se habían
                acumulado a través de ese arco vacío, las lluvias invernales habían
                golpeado en esos huecos ventanales; pues, en medio de los
                montones empapados de escombros, la primavera había nutrido la
                vegetación. Hierba y maleza crecían aquí y allá entre las piedras y
                las vigas caídas. Y, ˇoh!, żdónde, mientras tanto, estaba el
                desdichado dueńo de este naufragio? żEn qué tierra? żBajo qué
                auspicios? Mi ojo vagó involuntariamente hacia la torre gris de la
                iglesia cerca de las verjas y pregunté: «żEstá con Damer de
                Rochester, compartiendo el refugio de su estrecha casa de
                mármol?».</p>

                <p>Debía obtenerse alguna respuesta a estas preguntas. No podía
                encontrarla en ninguna parte más que en la posada, y hacia allí, al
                poco rato, regresé. El propio posadero me trajo el desayuno al salón.
                Le pedí que cerrara la puerta y se sentara; tenía algunas preguntas
                que hacerle. Pero cuando accedió, apenas supe cómo empezar; tal
                horror tenía de las posibles respuestas. Y sin embargo, el
                espectáculo de desolación que acababa de dejar me había preparado
                en cierta medida para un relato de miseria. El posadero era un
                hombre de aspecto respetable y de mediana edad.</p>

                <p>—Conoce usted Thornfield Hall, por supuesto —logré decir al fin.</p>
                
                <p>—Sí, seńora; viví allí una vez.</p>

                <p>—żDe veras? —«No en mi tiempo», pensé. «Es usted un extrańo para mí».</p>

                <p>—Fui el mayordomo del difunto seńor Rochester —ańadió.</p>

                <p>ˇEl difunto! Me pareció haber recibido, con toda su fuerza, el golpe
                que había estado tratando de evadir.</p>

                <p>—ˇEl difunto! —jadeé—. żHa muerto?</p>

                <p>—Me refiero al actual caballero, el padre del seńor Edward —
                explicó. Volví a respirar; mi sangre reanudó su flujo. Completamente
                asegurada por estas palabras de que el seńor Edward —mi seńor
                Rochester (ˇDios lo bendiga, dondequiera que esté!)— estaba al
                menos vivo, era, en resumen, «el actual caballero». ˇPalabras
                alentadoras! Me pareció que podía oír todo lo que estaba por venir
                —cualesquiera que fueran las revelaciones— con relativa
                tranquilidad. Ya que no estaba en la tumba, podría soportar, pensé,
                saber que estaba en las Antípodas.</p>

                <p>—żVive el seńor Rochester en Thornfield Hall ahora? —pregunté,
                sabiendo, por supuesto, cuál sería la respuesta, pero deseosa aún de
                aplazar la pregunta directa sobre dónde estaba realmente.</p>

                <p>—ˇNo, seńora, oh, no! Nadie vive allí. Supongo que es usted una
                extrańa en estas partes, o habría oído lo que sucedió el otońo
                pasado. Thornfield Hall está completamente en ruinas. Se quemó
                justo en la época de la cosecha. ˇUna calamidad espantosa! Se
                destruyó una cantidad inmensa de propiedad valiosa. Apenas se
                pudo salvar algo de los muebles. El fuego se declaró en plena noche
                y, antes de que llegaran las bombas de Millcote, el edificio era una
                masa de llamas. Fue un espectáculo terrible. Yo mismo lo presencié.</p>
                
                <p>—ˇEn plena noche! —murmuré. Sí, esa era siempre la hora de la
                fatalidad en Thornfield—. żSe supo cómo se originó? —demandé.</p>

                <p>—Lo supusieron, seńora; lo supusieron. De hecho, diría que se
                determinó sin lugar a dudas. Quizás no sepa usted —continuó,
                acercando un poco su silla a la mesa y hablando en voz baja— que
                había una dama, una... una lunática, recluida en la casa.</p>

                <p>—He oído algo de eso.</p>

                <p>—La mantenían en un confinamiento muy estricto, seńora. La
                gente, incluso durante algunos ańos, no estaba absolutamente
                segura de su existencia. Nadie la veía. Solo sabían por rumores que
                tal persona estaba en la mansión; y quién o qué era, era difícil de
                conjeturar. Decían que el seńor Edward la había traído del
                extranjero, y algunos creían que había sido su amante. Pero sucedió
                algo extrańo hace un ańo, algo muy extrańo.</p>

                <p>Temí ahora oír mi propia historia. Intenté devolverlo al hecho principal.</p>

                <p>—żY esta dama?</p>

                <p>—ˇEsta dama, seńora —respondió—, resultó ser la esposa del
                seńor Rochester! El descubrimiento se produjo de la manera más
                extrańa. Había una joven dama, una institutriz en la mansión, de la
                que el seńor Rochester se ena...</p>

                <p>—Pero el fuego —sugerí.</p>

                <p>—A eso voy, seńora. Que el seńor Edward se enamoró de ella. Los
                criados dicen que nunca vieron a nadie tan enamorado como él. La
                perseguía continuamente. Solían vigilarlo —los criados lo hacen, ya
                sabe, seńora— y la valoraba por encima de todo. Aunque nadie más
                que él la consideraba tan hermosa. Era una cosita pequeńa, dicen,
                casi como una nińa. Yo nunca la vi, pero he oído a Leah, la doncella,
                hablar de ella. A Leah le caía bastante bien. El seńor Rochester tenía
                unos cuarenta ańos y esta institutriz, no llegaba a los veinte; y ya
                ve, cuando los caballeros de su edad se enamoran de las
                muchachas, a menudo es como si estuvieran hechizados. Bueno,
                pues se iba a casar con ella.</p>

                <p>—Me contará esa parte de la historia en otro momento —dije—.
                Pero ahora tengo una razón particular para desear oír todo sobre el
                fuego. żSe sospechaba que esta lunática, la seńora Rochester, tuvo
                algo que ver?</p>

                <p>—Ha dado usted en el clavo, seńora. Es completamente seguro
                que fue ella, y nadie más que ella, quien lo provocó. Tenía una
                mujer para cuidarla llamada seńora Poole, una mujer capaz en su
                oficio y muy de fiar, salvo por un defecto, un defecto común a
                muchas de esas enfermeras y matronas: guardaba una botella
                privada de ginebra y de vez en cuando tomaba una gota de más. Es
                excusable, pues tuvo una vida dura; pero aun así era peligroso.
                Porque cuando la seńora Poole estaba profundamente dormida
                después de la ginebra con agua, la seńora loca, que era tan astuta
                como una bruja, le sacaba las llaves del bolsillo, salía de su cámara y
                se paseaba por la casa, haciendo cualquier travesura salvaje que se
                le ocurriera. Dicen que una vez casi quemó a su marido en la cama,
                pero de eso no sé. Sin embargo, en esta noche, prendió fuego
                primero a los cortinajes de la habitación contigua a la suya y luego
                bajó a un piso inferior y se dirigió a la cámara que había sido de la
                institutriz (era como si supiera de alguna manera cómo habían ido
                las cosas y le tuviera rencor) y prendió fuego a la cama allí; pero no
                había nadie durmiendo en ella, afortunadamente. La institutriz se
                había fugado dos meses antes; y por mucho que el seńor Rochester
                la buscó como si hubiera sido la cosa más preciosa que tenía en el
                mundo, nunca pudo saber nada de ella. Y se volvió salvaje,
                completamente salvaje por su decepción. Nunca fue un hombre
                alocado, pero se volvió peligroso después de que la perdió. También
                quería estar solo. Envió a la seńora Fairfax, el ama de llaves, con sus
                amigos a una distancia; pero lo hizo generosamente, pues le asignó
                una anualidad de por vida. Y se lo merecía, era una mujer muy
                buena. La seńorita Adčle, una pupila que tenía, fue enviada a la
                escuela. Rompió relaciones con toda la nobleza y se encerró como
                un ermitańo en la mansión.</p>

                <p>—ˇCómo! żNo se fue de Inglaterra?</p>

                <p>—żIrse de Inglaterra? ˇBendita sea, no! No cruzaba los umbrales
                de la casa, excepto por la noche, cuando paseaba como un fantasma
                por los terrenos y el huerto, como si hubiera perdido el juicio, lo
                cual, en mi opinión, había hecho. Pues un caballero más animado,
                más audaz, más agudo que él antes de que ese mosquito de
                institutriz se le cruzara, nunca vio usted, seńora. No era un hombre
                dado al vino, ni a las cartas, ni a las carreras, como algunos lo son, y
                no era tan guapo; pero tenía un coraje y una voluntad propios, si es
                que algún hombre los tuvo. Lo conocía desde nińo, ya ve; y por mi
                parte, a menudo he deseado que la seńorita Eyre se hubiera
                hundido en el mar antes de venir a Thornfield Hall.</p>

                <p>—żEntonces el seńor Rochester estaba en casa cuando se declaró el incendio?</p>

                <p>—Sí, por supuesto que lo estaba. Y subió a los desvanes cuando
                todo ardía arriba y abajo, y sacó a los criados de sus camas y los
                ayudó a bajar él mismo, y volvió para sacar a su esposa loca de su
                celda. Y entonces le gritaron que estaba en el tejado, donde estaba
                de pie, agitando los brazos, sobre las almenas, y gritando hasta que
                podían oírla a una milla de distancia. La vi y la oí con mis propios
                ojos. Era una mujer grande y tenía el pelo largo y negro. Podíamos
                verlo ondear contra las llamas mientras estaba de pie. Yo presencié,
                y varios más presenciaron, al seńor Rochester ascender por el
                tragaluz hasta el tejado. Lo oímos llamar: «ˇBertha!». Lo vimos
                acercarse a ella; y entonces, seńora, ella aulló, dio un salto y, al
                minuto siguiente, yacía destrozada en el pavimento.</p>

                <p>—żMuerta?</p>

                <p>—ˇMuerta! Sí, muerta como las piedras sobre las que se
                esparcieron sus sesos y su sangre.</p>

                <p>—ˇSanto Dios!</p>

                <p>—Bien puede decirlo, seńora. ˇFue espantoso!</p>

                <p>Se estremeció.</p>

                <p>—żY después? —insistí.</p>

                <p>—Bueno, seńora, después la casa se quemó hasta los cimientos.
                Ahora solo quedan algunos trozos de muros en pie.</p>

                <p>—żSe perdieron otras vidas?</p>

                <p>—No, aunque quizás hubiera sido mejor si así hubiera sido.</p>

                <p>—żQué quiere decir?</p>

                <p>—ˇPobre seńor Edward! —exclamó—. ˇPoco pensé que llegaría a
                ver esto! Algunos dicen que fue un justo castigo para él por
                mantener en secreto su primer matrimonio y querer tomar otra
                esposa mientras tenía una viva. Pero yo, por mi parte, lo
                compadezco.</p>

                <p>—żDijo usted que estaba vivo? —exclamé.</p>

                <p>—Sí, sí, está vivo; pero muchos piensan que sería mejor que
                estuviera muerto.</p>

                <p>—żPor qué? żCómo? —La sangre se me heló de nuevo—. żDónde
                está? —demandé—. żEstá en Inglaterra?</p>

                <p>—Sí, sí, está en Inglaterra; no puede salir de Inglaterra, me
                imagino. Ahora está fijo aquí.</p>

                <p>ˇQué agonía era esta! Y el hombre parecía resuelto a prolongarla.</p>

                <p>—Está completamente ciego —dijo al fin—. Sí, está
                completamente ciego, el seńor Edward.</p>

                <p>Había temido algo peor. Había temido que estuviera loco. Reuní
                fuerzas para preguntar qué había causado esta calamidad.</p>

                <p>—Fue todo su propio coraje, y se podría decir, su bondad, en
                cierto modo, seńora. No quiso abandonar la casa hasta que todos los
                demás hubieran salido antes que él. Cuando por fin bajaba la gran
                escalera, después de que la seńora Rochester se hubiera arrojado de
                las almenas, hubo un gran estruendo, todo se derrumbó. Lo sacaron
                de debajo de las ruinas, vivo, pero tristemente herido. Una viga
                había caído de tal manera que lo protegió en parte; pero un ojo le
                fue arrancado y una mano tan aplastada que el seńor Carter, el
                cirujano, tuvo que amputársela directamente. El otro ojo se le
                inflamó; perdió también la vista de ese. Ahora está indefenso, en
                verdad, ciego y tullido.</p>

                <p>—żDónde está? żDónde vive ahora?</p>

                <p>—En Ferndean, una casa solariega en una granja que tiene, a
                unas treinta millas de aquí. Un lugar bastante desolado.</p>

                <p>—żQuién está con él?</p>

                <p>—El viejo John y su esposa. No quiso a nadie más. Dicen que está
                completamente abatido.</p>

                <p>—żTiene usted algún tipo de transporte?</p>

                <p>—Tenemos una calesa, seńora, una calesa muy hermosa.</p>

                <p>—Que la preparen al instante; y si su postillón puede llevarme a
                Ferndean antes de que anochezca este día, les pagaré a ambos el
                doble de la tarifa que suelen pedir.</p>

