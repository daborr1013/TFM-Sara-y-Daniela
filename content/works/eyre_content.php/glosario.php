                <h4>Glosario</h4>
                <p>Alguna de las expresiones que se usan en <i>Jane Eyre</i> pueden llegar a resultarte un poco
                    complejas, por el poco uso que hoy en día les damos. Por ello, hemos creado este glosario para
                    ayudarte a entender mejor la obra.</p>
                <ul>
                    <li></li>
                </ul>

                <h4>Traducciones</h4>
                <p>Como habrás podido comprobar, en ciertos capítulos podemos encontrar fragmentos en francés. Esto se
                    debe a diversos factores. <a href="charlotte.php">Charlotte Brontë</a> utiliza estas
                    partes para reflejar la educación
                    y el estatus social de los personajes, ya que en el siglo XIX este idioma era un signo de cultura y
                    refinamiento. Además, ayudan a caracterizar a figuras como <a
                        href="personajes/thornfield/adele.php">Adèle Varens</a>
                    y aportan realismo a la historia, especialmente en contextos educativos. También cumplen una función
                    estética, pues añaden un tono más elegante.</p>
                <p>Aquí encontrarás una lista con las traducciones de los fragmentos y un botón para acceder al capítulo
                    en el que se encuentran.</p>
