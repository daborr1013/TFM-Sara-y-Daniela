                        es sombría y austera, marcando un fuerte contraste con el lujo de Gateshead. Al día siguiente,
                        <a href="../personajes/gateshead/jane.php">Jane</a> descubre la rígida disciplina del internado:
                        una rutina agotadora que comienza antes del amanecer, marcada por el frío, el hambre y las
                        oraciones constantes.
                    </p>

                    <p>En este entorno conoce a dos figuras opuestas: la amable superintendente <a
                            href="../personajes/lowood/temple.php">Miss Temple</a> y la severa
                        Miss Scatcherd, quien castiga
                        injustamente a una alumna llamada <a href="../personajes/lowood/helen.php">Helen Burns</a>. Jane
                        entabla amistad con Helen y descubre la
                        cruda realidad de la institución: Lowood es una escuela
                        de caridad para huérfanas financiada por donaciones, lo que confirma que la <a
                            href="../personajes/gateshead/senoraReed.php">señora Reed</a> no ha
                        gastado un céntimo en su educación. Además, comprende que
                        el <a href="../personajes/lowood/brocklehurst.php">Señor Brocklehurst</a> ejerce un control
                        tiránico sobre cada detalle del lugar.</p>
                </section>

                <section class="pjustificado">
                    <a class="boton" href="../caps/2.Etapa en Lowood (Cap. 5–10)/cap6.php">Capítulo V</a>

                    <p>La vida en Lowood se revela en toda su dureza: las niñas despiertan con el agua de las palanganas
                        congelada y sufren hambre constante
                        y sermones interminables. En este ambiente de privación, <a
                            href="../personajes/gateshead/jane.php">Jane</a> observa con asombro a <a
                            href="../personajes/lowood/helen.php">Helen Burns</a>,
                        quien soporta con una paciencia sobrehumana los castigos
                        y humillaciones de la Srta. Scatcherd.</p>
                    <p><a href="../personajes/lowood/helen.php">Helen</a> le explica a <a
                            href="../personajes/gateshead/jane.php">Jane</a> su filosofía de resignación cristiana: cree
                        en devolver bien por mal y en
                        aceptar el sufrimiento con humildad.
                        <a href="../personajes/gateshead/jane.php">Jane</a>, fiel a su sentido de la justicia, rechaza
                        frontalmente esta idea de sumisión ante la
                        tiranía. Mientras <a href="../personajes/gateshead/jane.php">Jane</a> admira la vasta cultura de
                        su amiga,
                        <a href="../personajes/lowood/helen.php">Helen</a> se critica a sí misma por ser "distraída" y
                        soñar con su hogar, demostrando un nivel de
                        autodisciplina que <a href="../personajes/gateshead/jane.php">Jane</a> aún no logra comprender.
                    </p>
                </section>

                <section class="pjustificado">
