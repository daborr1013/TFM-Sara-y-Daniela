            <section class="pjustificado">
                <a class="boton" href="../caps/2.Etapa en Lowood (Cap. 5–10)/cap5.php">Capítulo V</a>

                <p>Jane emprende sola un largo viaje en carruaje hasta llegar a Lowood en un día gris y lluvioso. La atmósfera del edificio 
                    es sombría y austera, marcando un fuerte contraste con el lujo de Gateshead. Al día siguiente, Jane descubre la rígida disciplina del internado: 
                    una rutina agotadora que comienza antes del amanecer, marcada por el frío, el hambre y las oraciones constantes.</p>
                        
                <p>En este entorno conoce a dos figuras opuestas: la amable superintendente Miss Temple y la severa Miss Scatcherd, quien castiga
                    injustamente a una alumna llamada Helen Burns. Jane entabla amistad con Helen y descubre la cruda realidad de la institución: Lowood es una escuela
                    de caridad para huérfanas financiada por donaciones, lo que confirma que la tía Reed no ha gastado un céntimo en su educación. Además, comprende que
                    el Señor Brocklehurst ejerce un control tiránico sobre cada detalle del lugar.</p>
            </section>

            <section class="pjustificado">
                <a class="boton" href="../caps/2.Etapa en Lowood (Cap. 5–10)/cap6.php">Capítulo V</a>

                <p>La vida en Lowood se revela en toda su dureza: las niñas despiertan con el agua de las palanganas congelada y sufren hambre constante
                    y sermones interminables. En este ambiente de privación, Jane observa con asombro a Helen Burns, quien soporta con una paciencia sobrehumana los castigos
                    y humillaciones de la Srta. Scatcherd.</p>
                <p>Helen le explica a Jane su filosofía de resignación cristiana: cree en devolver bien por mal y en aceptar el sufrimiento con humildad.
                    Jane, fiel a su sentido de la justicia, rechaza frontalmente esta idea de sumisión ante la tiranía. Mientras Jane admira la vasta cultura de su amiga, 
                    Helen se critica a sí misma por ser "distraída" y soñar con su hogar, demostrando un nivel de autodisciplina que Jane aún no logra comprender.</p>
            </section>

            <section class="pjustificado">
                <a class="boton" href="../caps/2.Etapa en Lowood (Cap. 5–10)/cap7.php">Capítulo VII</a> 

                <p>Tras un mes de ausencia, el Señor Brocklehurst regresa a Lowood, aumentando la ansiedad de Jane. Durante su inspección, 
                    Jane deja caer accidentalmente su pizarra, lo que el director aprovecha para descargar su furia. Cumpliendo la promesa que le hizo a la Sra. Reed, 
                    obliga a Jane a subirse a un taburete y la denuncia ante toda la escuela como una mentirosa, prohibiendo a las demás alumnas hablar con ella durante el resto del día.</p>
                <p>A pesar de la vergüenza extrema, Jane encuentra consuelo en Helen Burns. Mientras las demás la ignoran, Helen le dedica sonrisas 
                    cómplices y silenciosas cada vez que pasa a su lado, demostrando una lealtad que ayuda a Jane a soportar la humillación pública.</p>
            </section>

            <section class="pjustificado">
                <a class="boton" href="../caps/2.Etapa en Lowood (Cap. 5–10)/cap8.php">Capítulo VIII</a>    

                <p>Tras la humillación, Jane se derrumba creyendo que su reputación está destruida para siempre, pero Helen la consuela asegurándole que sus compañeras
                    sienten lástima por ella, no desprecio. Poco después, Miss Temple invita a ambas a su habitación y permite que Jane cuente su verdadera historia sobre Gateshead y la tía Reed.</p>
                <p>Miss Temple, mostrando una confianza ciega en Jane, escribe al Sr. Lloyd para confirmar los hechos. Mientras esperan la respuesta, 
                    comparte con las niñas un té con pastel, un gesto de humanidad inolvidable para Jane. Cuando llega la carta del boticario corroborando la versin de la nia, 
                    Miss Temple limpia el nombre de Jane ante toda la escuela. Liberada de la etiqueta de "mentirosa", Jane se entrega con pasin a sus estudios, destacando pronto en dibujo y francs.</p>
            </section>

            <section class="pjustificado">
                <a class="boton" href="../caps/2.Etapa en Lowood (Cap. 5–10)/cap9.php">Capítulo IX</a>    

                <p>Con la llegada de la primavera, la ubicación insalubre de Lowood provoca un brote masivo de tifus que afecta a más de la mitad de las niñas. 
                    Mientras Jane se mantiene sana y disfruta de una libertad inusual jugando al aire libre, descubre con horror que Helen Burns está agonizando, pero no de tifus, 
                    sino de tuberculosis (consunción).</p>
                <p>Una noche, Jane se cuela en la habitación de Miss Temple para ver a su amiga por última vez. En una escena desgarradora, Helen la consuela 
                    diciéndole que no teme a la muerte y que está en paz por abandonar el sufrimiento del mundo. Ambas se quedan dormidas abrazadas en la misma cama; a la mañana siguiente,
                Helen ha muerto. Quince años después, se coloca sobre su tumba una placa con la palabra <i>Resurgam</i> ("Resurgir"), simbolizando la fe de Helen y el impacto eterno que dejó en Jane.</p>
            </section>

            <section class="pjustificado">
                <a class="boton" href="../caps/2.Etapa en Lowood (Cap. 5–10)/cap10.php">Capítulo X</a>    

                <p>Tras el escándalo del tifus, una nueva directiva asume el control de Lowood, mejorando drásticamente las condiciones de vida. Jane pasa allí ocho años: 
                    seis como alumna brillante y dos como maestra. Sin embargo, tras el matrimonio y la partida de su mentora, Miss Temple, Jane siente que su mundo se ha quedado pequeño y 
                    decide buscar nuevos horizontes, aceptando un puesto de institutriz en una mansión llamada Thornfield.</p>
                <p>Antes de partir, recibe la visita de Bessie, quien le pone al día sobre los Reed: sus primas están enfrentadas tras un intento de fuga amorosa de Georgiana,
                y su primo John se ha hundido en la depravación. Lo más impactante es la noticia de que un tío paterno, John Eyre, la buscó hace siete años antes de partir hacia Madeira en 
                busca de fortuna. Con esta mezcla de pasado y futuro, Jane se despide de Bessie y se encamina, llena de determinació n, hacia su nueva vida.</p>
            </section>

