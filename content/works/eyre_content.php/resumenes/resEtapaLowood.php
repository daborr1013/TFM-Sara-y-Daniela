                            href="../personajes/gateshead/jane.php">Jane</a> siente que su mundo se ha quedado
                        pequeño y
                        decide buscar nuevos horizontes, aceptando un puesto de institutriz en una mansión llamada
                        Thornfield.</p>
                    <p>Antes de partir, recibe la visita de <a href="../personajes/gateshead/bessie.php">Bessie</a>,
                        quien
                        le pone al día sobre los Reed: sus primas
                        están enfrentadas tras un intento de fuga amorosa de <a
                            href="../personajes/gateshead/georgiana.php">Georgiana</a>,
                        y su primo <a href="../personajes/gateshead/john.php">John</a> se ha hundido en la depravación.
                        Lo más impactante es la noticia de que un tío
                        paterno, John Eyre, la buscó hace siete años antes de partir hacia Madeira en
                        busca de fortuna. Con esta mezcla de pasado y futuro, <a
                            href="../personajes/gateshead/jane.php">Jane</a>
                        se despide de <a href="../personajes/gateshead/bessie.php">Bessie</a> y se encamina,
                        llena de determinació n, hacia su nueva vida.</p>
