                    <p>Con la llegada de la primavera, la ubicación insalubre de Lowood provoca un brote masivo de tifus
                        que afecta a más de la mitad de las niñas.
                        Mientras <a href="../personajes/gateshead/jane.php">Jane</a> se mantiene sana y disfruta de una
                        libertad inusual jugando al aire libre,
                        descubre con horror que <a href="../personajes/lowood/helen.php">Helen Burns</a> está
                        agonizando, pero no de tifus,
                        sino de tuberculosis (consunción).</p>
                    <p>Una noche, <a href="../personajes/gateshead/jane.php">Jane</a> se cuela en la habitación de <a
                            href="../personajes/lowood/temple.php">Miss Temple</a> para ver a su amiga por última vez.
                        En
                        una escena desgarradora, <a href="../personajes/lowood/helen.php">Helen</a> la consuela
                        diciéndole que no teme a la muerte y que está en paz por abandonar el sufrimiento del mundo.
                        Ambas se quedan dormidas abrazadas en la misma cama; a la mañana siguiente,
                        <a href="../personajes/lowood/helen.php">Helen</a> ha muerto. Quince años después, se coloca
                        sobre
                        su tumba una placa con la palabra
                        <i>Resurgam</i> ("Resurgir"), simbolizando la fe de <a
                            href="../personajes/lowood/helen.php">Helen</a>
                        y el impacto eterno que dejó en <a href="../personajes/gateshead/jane.php">Jane</a>.
                    </p>
                </section>

