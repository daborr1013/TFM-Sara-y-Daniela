                    <p>Tras un mes de ausencia, el <a href="../personajes/lowood/brocklehurst.php">Señor
                            Brocklehurst</a> regresa a Lowood, aumentando la ansiedad de <a
                            href="../personajes/gateshead/jane.php">Jane</a>.
                        Durante su inspección,
                        <a href="../personajes/gateshead/jane.php">Jane</a> deja caer accidentalmente su pizarra, lo que
                        el director aprovecha para descargar su furia.
                        Cumpliendo la promesa que le hizo a la <a href="../personajes/gateshead/senoraReed.php">señora
                            Reed</a>,
                        obliga a <a href="../personajes/gateshead/jane.php">Jane</a> a subirse a un taburete y la
                        denuncia ante toda la escuela como una mentirosa,
                        prohibiendo a las demás alumnas hablar con ella durante el resto del día.
                    </p>
                    <p>A pesar de la vergüenza extrema, <a href="../personajes/gateshead/jane.php">Jane</a> encuentra
                        consuelo en <a href="../personajes/lowood/helen.php">Helen Burns</a>. Mientras las demás la
                        ignoran, <a href="../personajes/lowood/helen.php">Helen</a> le dedica sonrisas
                        cómplices y silenciosas cada vez que pasa a su lado, demostrando una lealtad que ayuda a <a
                            href="../personajes/gateshead/jane.php">Jane</a> a
                        soportar la humillación pública.</p>
                </section>

