            <h1>Resumen bloque <i>Moor House</i></h1>
            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap28.php">Capítulo XXVIII</a>
            </h3>
            <p class="pjustificado">Tras huir de Thornfield con apenas unas pocas monedas, Jane se ve obligada a gastar lo último que tiene en un carruaje 
                que la deja en un lugar remoto llamado Whitcross. Sin dinero, sin comida y sin techo, Jane vive la degradación absoluta de la pobreza: 
                duerme al raso entre los brezales, intenta cambiar su pañuelo por un poco de pan y llega a mendigar sobras que un granjero iba a dar a sus cerdos.</p>
            <p class="pjustificado">En un estado de agotamiento extremo y bajo una lluvia torrencial, Jane sigue una luz en el páramo que la lleva hasta una casa
                llamada Marsh End (o Moor House). A través de la ventana, observa a dos mujeres cultas, Diana y Mary, y a su criada Hannah. Cuando intenta pedir ayuda, 
                Hannah la rechaza confundiéndola con una vagabunda común. Justo cuando Jane se desploma aceptando su muerte, aparece St. John Rivers, el hermano de las
                jóvenes y clérigo del lugar, quien la rescata citando la voluntad de Dios. Los tres hermanos la acogen, le dan alimento y calor, mientras Jane, para
                proteger su pasado, les da un nombre falso: Jane Elliott.</p>

            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap29.php">Capítulo XXIX</a>
            </h3>
            <p class="pjustificado">Jane baja a la cocina y tiene un pequeño enfrentamiento con Hannah, la sirvienta, quien la juzga por su apariencia de mendiga. 
                Jane, con su característica dignidad, le da una lección de moral: le recuerda que la pobreza no es un crimen y que no debería juzgar a alguien solo por su 
                falta de dinero. Hannah, avergonzada, se disculpa y terminan haciendo las paces.</p>
            <p class="pjustificado">Cuando St. John le pide que cuente su historia, Jane (bajo el pseudónimo de Jane Elliott) les confiesa que es huérfana y que ha huido 
                de una situación dolorosa, pero se niega rotundamente a dar detalles sobre su pasado o el lugar de donde viene. St. John acepta su secreto y le promete 
                ayudarla a encontrar un trabajo que le permita ser independiente.</p>

            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap30.php">Capítulo XXX</a>
            </h3>
            <p class="pjustificado">Jane entabla una amistad profunda con Diana y Mary. Las tres comparten el amor por la lectura y el estudio (especialmente de idiomas como el alemán). 
                Jane admira su independencia y su intelecto, sintiéndose por primera vez tratada como una igual en un entorno familiar sano. 
                Sin embargo, St. John sigue siendo un enigma: es un hombre entregado a su deber religioso, pero Jane nota que no es feliz; parece devorado por una ambición
                severa y un deseo de hacer algo "grande" que lo aleja de la paz doméstica.</p>
            <p class="pjustificado">Los Rivers se enteran de que un tío suyo ha muerto y les ha dejado casi nada, legando su inmensa fortuna a otro pariente desconocido.
                Esto entristece a las hermanas, no por el dinero en sí, sino porque esperaban que esa herencia les permitiera dejar de trabajar como institutrices en casas ajenas.</p>
            <p class="pjustificado">St. John finalmente le encuentra un trabajo a Jane. Le ofrece ser la maestra de una escuela para niñas pobres en el pueblo de Morton.
                El salario es bajo y el trabajo humilde, pero Jane acepta con gratitud porque le ofrece lo que más desea: independencia total.</p>

            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap31.php">Capítulo XXXI</a>
            </h3>
            <p class="pjustificado">Jane inaugura su escuela con solo veinte alumnas, de las cuales solo tres saben leer. Al principio, se siente desanimada y lucha 
                contra una sensación de degradación; después de haber sido la compañera intelectual de Rochester, verse enseñando el abecedario en una aldea rural le
                resulta un cambio brusco. Sin embargo, su autodisciplina vence de nuevo: se prohíbe a sí misma mirar atrás con nostalgia y se compromete a hacer su
                trabajo con dignidad.</p>
            <p class="pjustificado">En este capítulo aparece un personaje clave: Rosamond Oliver, la hija del hombre más rico de la zona. Rosamond es "la mujer más
                hermosa que Jane ha visto jamás" (incluso más que Blanche Ingram). Ella llega a la escuela y su presencia ilumina el lugar. Jane observa con atención
                la reacción de St. John Rivers ante ella, ya que es evidente que ambos se atraen pero St.John se obliga a ser distante con ella por su ambición
                religiosa, que le impide casarse con alguien que solo busca placeres mundanos.</p>

            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap32.php">Capítulo XXXII</a>
            </h3>
            <p class="pjustificado">Jane gana el respeto y el cariño de sus alumnas en Morton, descubriendo que incluso las niñas más humildes tienen talentos ocultos. 
                Su vida es tranquila, pero sus noches están llenas de sueños vívidos y dolorosos con Rochester.</p>
            <p class="pjustificado">Rosamond Oliver visita la escuela con frecuencia, claramente buscando encontrarse con St. John. Un día, Jane se ofrece a hacerle un
                retrato a lápiz a Rosamond. Mientras trabaja, St. John llega y queda cautivado por la imagen. Jane, valiente y directa, aprovecha la oportunidad para 
                confrontarlo. St. John explica que su verdadera vocación es ser misionero en la India. Sabe que Rosamond, con su amor por el lujo y la vida social, 
                moriría de aburrimiento y calor en un año. Prefiere sacrificar su felicidad personal por lo que considera su deber sagrado.</p>

            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap33.php">Capítulo XXXIII</a>
            </h3>
            <p class="pjustificado">En una noche de tormenta de nieve, St. John Rivers llega a la cabaña de Jane. Tras un preámbulo lleno de tensión, le cuenta una 
                historia que parece salida de un libro: habla de una tal Jane Eyre, una institutriz que desapareció de Thornfield Hall tras un intento de boda ilegal
                con el Sr. Rochester. St. John revela cómo la encontró: el trozo de papel que arrancó en el capítulo anterior tenía escrito el nombre real de ella; 
                el abogado de Londres, el Sr. Briggs (el mismo que detuvo la boda), ha estado buscándola desesperadamente porque su tío, John Eyre, ha muerto en 
                Madeira y le ha dejado a Jane toda su fortuna: 20,000 libras.</p>
            <p class="pjustificado">Lo más conmovedor para Jane no es el dinero. Al hacer preguntas sobre la familia de St. John, descubre la verdad definitiva:
                el padre de St. John, Diana y Mary era el hermano de la madre de Jane. Los River son sus primos. En vez de regodearse en su riqueza, salta de alegría
                al saber que tiene familia. Inmediatamente decide dividir las 20,000 libras en cuatro partes iguales ($5,000$ para cada uno), para que sus primos 
                también sean independientes y puedan dejar de trabajar como sirvientes o institutrices.</p>

            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap34.php">Capítulo XXXIV</a>
            </h3>
            <p class="pjustificado">Tras repartir la herencia, las hermanas Diana y Mary regresan a Moor House, y por primera vez en su vida, Jane disfruta de un hogar 
                lleno de afecto y libertad económica. Sin embargo, St. John no se permite disfrutar. Se vuelve cada vez más exigente con Jane, pidiéndole que deje de 
                estudiar alemán y aprenda hindustani, preparándola para su misión en la India</p>
            <p class="pjustificado">Jane se siente anulada por la voluntad de hierro de su primo. Ella describe su influencia como una "neblina helada" que apaga su 
                chispa natural. El clímax del capítulo llega cuando St. John le hace una propuesta de matrimonio, pero de una forma totalmente desprovista de amor. 
                No le pide que sea su esposa por afecto, sino porque la considera una "herramienta adecuada" para su labor misionera.</p>
            <p class="pjustificado">Jane acepta ir a la India como su "hermana" o colaboradora, pero se niega a casarse con él. Ella sabe que un matrimonio sin amor 
                con un hombre tan frío sería una forma de suicidio emocional. Él se siente profundamente ofendido por su negativa, tachándola de rebelde ante la voluntad
                de Dios, y comienza a tratarla con una cortesía gélida que hiere a Jane más que cualquier grito.</p>

            <h3>
                <a class="boton" href="../caps/4.Moor House (Cap. 28–35)/cap35.php">Capítulo XXXV</a>
            </h3>
            <p class="pjustificado">Tras el rechazo de Jane, St. John utiliza su arma más poderosa: la culpa religiosa. Durante una semana, la trata con una frialdad gélida 
                que hace que Jane se sienta como una pecadora ante Dios. Diana intenta advertirle que casarse con él sería una locura, pero St. John redobla su presión.</p>
            <p class="pjustificado">Una noche, St. John reza con tal fervor y autoridad que Jane se siente al borde de la rendición. Por un momento, cree que su destino
                es sacrificarse y casarse con él para servir a la misión en la India. Está a punto de decir "sí", cuando de repente ocurre algo sobrenatural: 
                    en el silencio absoluto de la noche, Jane escucha una voz conocida que la llama, pero no es una voz que esté en su cabeza, la está oyendo realmente. 
                    Reconoce al instante que es la voz de Edward Rochester.</p>
            <p class="pjustificado">Este fenómeno le devuelve la fuerza y la claridad. Rompe el hechizo de St. John, lo aparta y declara que ahora es ella quien tiene 
                un deber que cumplir.</p>
            
