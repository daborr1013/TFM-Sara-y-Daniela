            <h1>Resumen bloque <i>Infancia en Gateshead</i></h1>
            <h3>
                <a class="boton" href="../caps/1.Infancia en Gateshead (Cap. 1–4)/cap1.php">Capítulo I</a>
            </h3>
            <p class="pjustificado">La historia comienza en una tarde gris de noviembre. Jane Eyre, una huérfana de 10 ańos, intenta pasar desapercibida leyendo La historia de los 
                pájaros británicos de Bewick tras una cortina. Su tía, la Sra. Reed, la excluye del círculo familiar, mientras su primo John Reed la acosa física y verbalmente, 
                recordándole que vive allí solo por caridad.</p>
            <p class="pjustificado">Tras recibir un golpe con un libro lanzado por John, Jane finalmente explota y se defiende, tachándolo de "asesino" y "esclavo". La Sra. Reed interviene de inmediato, 
                culpando exclusivamente a Jane y ordenando su castigo más temido: el encierro en el "Cuarto Rojo", el lugar donde murió su tío Reed.</p> 

            <h3>
                <a class="boton" href="../caps/1.Infancia en Gateshead (Cap. 1–4)/cap2.php">Capítulo II</a>   
            </h3>
            <p class="pjustificado">Jane es arrastrada por las criadas Abbott y Bessie al temido Cuarto Rojo. Al verse reflejada en el espejo, se siente como un espíritu extrańo y comienza a meditar sobre
                su injusta situación: recuerda que su tío Reed la acogió tras la muerte de sus padres y que, en su lecho de muerte, hizo jurar a su esposa que la criaría como a una hija,
                promesa que la tía ha roto sistemáticamente.</p>
            <p class="pjustificado">El aislamiento y la oscuridad alimentan su imaginación hasta que cree ver la aparición del fantasma de su tío, quien vendría a vengarla. Aterrorizada, Jane suplica que la 
                dejen salir, pero la Sra. Reed ignora sus gritos tachándolos de artimańa dramática. Finalmente, el pánico y el agotamiento hacen que Jane se desmaye.</p>

            <h3>
                <a class="boton" href="../caps/1.Infancia en Gateshead (Cap. 1–4)/cap2.php">Capítulo III</a>   
            </h3>
            <p class="pjustificado">Jane despierta en su habitación bajo el cuidado del amable boticario, el Sr. Lloyd, y de Bessie, quien muestra cierta compasión por el trato recibido por la nińa. 
                Tras el trauma, el Sr. Lloyd conversa con Jane y, al notar su infelicidad, sugiere a la Sra. Reed que la envíen a una escuela, idea que despierta en Jane una cautelosa esperanza.</p>
            <p class="pjustificado">Mientras se recupera, Jane escucha una charla entre las criadas donde descubre su origen: su madre, de la familia Reed, fue desheredada por casarse con un clérigo pobre. 
                Ambos murieron de tifus cuando Jane era un bebé, dejando a la nińa en manos de una familia que nunca la quiso.</p>

            <h3>
                <a class="boton" href="../caps/1.Infancia en Gateshead (Cap. 1–4)/cap4.php">Capítulo IV</a>
            </h3>
            <p class="pjustificado">Tras dos meses de espera y mayor aislamiento, Jane conoce al Sr. Brocklehurst, el severo director de la escuela Lowood. Durante un interrogatorio humillante sobre religión, 
                Jane admite con honestidad que los salmos no le interesan, lo que indigna al clérigo. Para colmo, la Sra. Reed la calumnia ante él tildándola de mentirosa, asegurando que esa 
                será su carta de presentación en el colegio.</p>
            <p class="pjustificado">Una vez que Brocklehurst se marcha, Jane, herida profundamente, explota contra su tía en una famosa declaración de independencia: le comunica que no es su pariente, 
                que nunca la volverá a llamar "tía" y que su recuerdo le produce asco. Ante esta furia, la Sra. Reed se queda sin palabras, derrotada por primera vez. 
                Antes de partir hacia su nueva vida, Jane recibe un inusual gesto de carińo de Bessie, quien le confiesa que la prefiere a ella antes que a los nińos Reed.</p>

