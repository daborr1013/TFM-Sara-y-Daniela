                            href="../personajes/thornfield/adele.php">Adèle</a> del edificio en
                        llamas. <a href="../personajes/thornfield/edwardRochester.php">Rochester</a> subió al tejado
                        para intentar salvar a su
                        <a href="../personajes/thornfield/bertha.php">esposa</a>, pero ella, entre gritos y risas
                        dementes, se lanzó al vacío y murió en el acto. Al
                        intentar bajar, una parte de la estructura colapsó sobre <a
                            href="../personajes/thornfield/edwardRochester.php">Rochester</a>.
                        El resultado fue devastador: perdió la vista y le tuvieron que amputar una mano.
                    </p>
                </section>

                <section class="pjustificado">
                    <a class="boton" href="../caps/5.Regreso a Rochester (Cap. 36–38)/cap37.php" target="_blank">Capítulo XXXVII</a>

                    <p><a href="../personajes/gateshead/jane.php">Jane</a> se acerca a la casa y ve a <a
                            href="../personajes/thornfield/edwardRochester.php">Rochester</a> salir por la puerta. Se
                        queda impactada: su aspecto es
                        descuidado, su mirada fija y sin brillo, y
                        se mueve con una tristeza errática. Parece un "león enjaulado". Jane entra en la casa, se
                        encuentra con los antiguos criados (Mary y John) y toma una bandeja
                        con agua para llevársela ella misma.</p>
                    <p>Al principio, <a href="../personajes/thornfield/edwardRochester.php">Rochester</a> no cree que
                        sea
                        ella. Al ser ciego, la toca con manos temblorosas y
                        piensa que es un espíritu o un sueño.
                        <a href="../personajes/gateshead/jane.php">Jane</a> le asegura que es ella, de carne y hueso, y
                        le
                        revela que ahora es una mujer rica e
                        independiente. Esto es crucial: <a href="../personajes/gateshead/jane.php">Jane</a> ya no vuelve
                        como una humilde institutriz,
                        sino como su igual (o incluso con más recursos que él en ese momento).
                    </p>
                    <p><a href="../personajes/thornfield/edwardRochester.php">Rochester</a> confiesa que, en un momento
                        de
                        desesperación absoluta, gritó el nombre de <a href="../personajes/gateshead/jane.php">Jane</a>
                        al
                        viento. Coinciden en que ambos escucharon la llamada
                        a la misma hora y el mismo día (el evento místico del Capítulo 35).</p>
                </section>

                <section class="pjustificado">
                    <a class="boton" href="../caps/5.Regreso a Rochester (Cap. 36–38)/cap38.php" target="_blank">Capítulo XXXVIII</a>

                    <p><a href="../personajes/gateshead/jane.php">Jane</a> y <a
                            href="../personajes/thornfield/edwardRochester.php">Rochester</a> celebran una boda
                        tranquila y
                        privada. No hay grandes lujos ni invitados
                        pretenciosos; su unión se basa ahora en una igualdad total.
                        <a href="../personajes/gateshead/jane.php">Jane</a> explica que ser su esposa es ser la parte
                        más
                        feliz de su propia existencia: ella es sus
                        ojos (le describe el mundo) y él es su guía espiritual.
                    </p>
                    <p><a href="../personajes/gateshead/jane.php">Jane</a> saca a <a
                            href="../personajes/thornfield/adele.php">Adèle</a> de la estricta escuela donde estaba y la
                        lleva a una más amable cerca de casa.
                        <a href="../personajes/thornfield/adele.php">Adèle</a> crece para convertirse en una joven
                        dócil,
                        agradable y muy unida a <a href="../personajes/gateshead/jane.php">Jane</a>.
                    </p>
                    <p><a href="../personajes/moorhouse/dianaRivers.php">Diana</a> y <a
                            href="../personajes/moorhouse/maryRivers.php">Mary</a> Rivers se casan con hombres que las valoran
                        y
                        mantienen una relación estrecha y
                        frecuente con su prima <a href="../personajes/gateshead/jane.php">Jane</a>.</p>
                    <p><a href="../personajes/moorhouse/johnRivers.php">St. John Rivers</a>: Se marcha a la India como
                        misionero. El libro termina con una nota sobre él:
                        nunca se casó y, aunque está agotado y presiente su muerte cercana,
                        se siente realizado por haber cumplido lo que él considera su deber sagrado hacia Dios.</p>
                    <p>Tras dos años de matrimonio, <a href="../personajes/thornfield/edwardRochester.php">Rochester</a>
                        recupera gradualmente la visión en un ojo. Esto le permite
                        ver por primera vez a su hijo primogénito
                        (que heredó sus mismos ojos negros y brillantes). La novela cierra con una imagen de plenitud
                        familiar, donde el sufrimiento pasado ha sido transformado en una paz profunda y merecida.</p>
                </section>

