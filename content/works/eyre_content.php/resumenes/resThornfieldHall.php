            <section class="hero">
                <h1>Resumen bloque <i>Thornfield Hall</i></h1>
            </section>
            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap11.php">Capítulo XI</a>
            </h3>
            <p class="pjustificado">Jane llega a Thornfield Hall de noche, encontrando un interior acogedor que contrasta con la oscuridad exterior. Allí la recibe la Sra. Fairfax, 
                una anciana amable que Jane confunde con la dueña, aunque pronto aclara que es solo la ama de llaves. El verdadero dueño es el Sr. Rochester, un hombre excéntrico y viajero 
                que rara vez está en casa. Jane conoce su misión: ser la institutriz de Adèle, una niña francesa de ocho años protegida de Rochester.</p>
            <p class="pjustificado">Sin embargo, la calma se rompe cuando Jane escucha una risa tétrica y antinatural que resuena por los pasillos. La Sra. Fairfax resta importancia al incidente 
                culpando a Grace Poole, una costurera de carácter difícil y errático que trabaja en la casa. Aunque Jane acepta la explicación, la inquietud queda sembrada en el ambiente.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap12.php">Capítulo XII</a>
            </h3>
            <p class="pjustificado">La vida en Thornfield resulta agradable para Jane; Adèle es una alumna vivaz aunque algo consentida, y Jane disfruta de la paz de la mansión, 
                aunque a veces siente una inquietud errante que la lleva a recorrer los pasillos del piso superior. Una tarde de invierno, mientras pasea sola bajo la luna, escucha 
                la llegada de un caballo y un perro, lo que le recuerda a los "Gytrash" (espíritus en forma de animales) de los cuentos de Bessie.</p>
            <p class="pjustificado">El hechizo se rompe cuando el caballo resbala en el hielo y su jinete cae al suelo. Jane, lejos de asustarse, ayuda al hombre a levantarse. 
                Nota que es un hombre de unos 35 años, de rasgos severos, tez oscura y ceño fruncido. Al regresar a la casa, Jane descubre al perro (Pilot) en la alfombra y se entera 
                por los criados de que el hombre al que ayudó no es otro que el Sr. Rochester, quien acaba de regresar a su hogar con un tobillo torcido tras el accidente.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap13.php">Capítulo XIII</a>
            </h3>
            <p class="pjustificado">Al día siguiente de su accidentado encuentro, Rochester invita a Jane y a Adèle a tomar el té. Su comportamiento es brusco, frío y errático, rompiendo
                con todas las etiquetas sociales esperadas de un anfitrión. Sin embargo, se queda fascinado al examinar los dibujos de Jane, reconociendo en ellos una mente profunda y 
                original que no encaja con la de una simple institutriz.</p>
            <p class="pjustificado">Tras el encuentro, Jane confiesa a la Sra. Fairfax que encuentra a su amo "cambiante y abrupto". El ama de llaves justifica su carácter explicando su
                complicado pasado familiar: Rochester era el "paria" de la familia, mientras que su hermano mayor heredó todo. Solo tras la muerte de su padre y de su hermano, hace nueve 
                años, Edward se convirtió en el dueño de Thornfield, una posición que parece ocupar más por deber que por gusto.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap14.php">Capítulo XIV</a>
            </h3>
            <p class="pjustificado">Una noche, en un estado de ánimo más relajado (y algo ebrio), Rochester manda llamar a Jane y a Adèle. Mientras la niña se entretiene con sus regalos, 
                Rochester se muestra inusualmente parlanchín y desafiante. En un momento de sinceridad brutal, le pregunta a Jane si lo encuentra guapo; ella, sin pensarlo, responde con un 
                rotundo "no", lo que divierte al amo de la casa por su falta de hipocresía.</p>
            <p class="pjustificado">La conversación se vuelve profunda y extraña: Rochester insiste en que no la ve como una sirvienta y discuten sobre temas densos como el pecado, 
                el remordimiento y la redención. Él insinúa que ha cometido errores en el pasado y que busca un camino para ser "limpio" de nuevo. El capítulo termina con una mención 
                a la madre de Adèle, dejando a Jane con la intriga de un secreto que Rochester promete revelarle pronto.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap15.php">Capítulo XV</a>
            </h3>
            <p class="pjustificado">Rochester cumple su promesa y revela a Jane su pasado: tuvo un romance con una bailarina francesa, Céline Varens, quien le fue infiel con otro hombre.
                Aunque Rochester duda que Adèle sea su hija biológica (ya que no se le parece en nada), decidió traerla a Inglaterra para que no quedara abandonada cuando su madre la dejó. 
                Esta confesión humaniza a Rochester ante los ojos de Jane.</p>
            <p class="pjustificado">Esa misma noche, el misterio gótico estalla: Jane oye pasos en el pasillo y una risa demoníaca. Al salir, descubre que la habitación de Rochester está en llamas. 
                Jane actúa con rapidez y le salva la vida arrojando agua sobre su cama. Tras el incidente, Rochester sube misteriosamente al tercer piso y, al volver, confirma la sospecha de Jane
                de que la culpable es Grace Poole, pero le ordena guardar silencio absoluto sobre lo ocurrido. El capítulo termina con una nota de intensa gratitud y una conexión emocional que
                deja a Jane incapaz de dormir.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap16.php">Capítulo XVI</a>
            </h3>
            <p class="pjustificado">Al día siguiente, Jane se asombra al ver que el intento de asesinato no ha causado ningún escándalo; los criados creen que fue un accidente con una
                vela y la propia Grace Poole cose tranquilamente como si nada hubiera pasado. Jane no entiende por qué Rochester protege a una criminal, pero sus pensamientos pronto se
                desvían hacia sus propios sentimientos: se da cuenta de que está enamorándose de su amo.</p>
            <p class="pjustificado">La decepción llega cuando se entera de que Rochester se ha marchado por varios días para asistir a una fiesta donde estará la bella Blanche Ingram. 
                Para "curarse" de sus ilusiones, Jane se impone un castigo psicológico: dibuja un autorretrato tosco de sí misma y un retrato idealizado de la hermosa Blanche.
                Al compararlos, se obliga a recordar su posición como una "pobre institutriz plana" para no hacerse falsas esperanzas con un hombre de la clase de Rochester.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap17.php">Capítulo XVII</a>
            </h3>
            <p class="pjustificado">Tras una semana de angustia pensando que Rochester se irá a Europa por un año, la Sra. Fairfax recibe noticias de que él regresará con una gran comitiva de invitados. 
                Mientras preparan la casa, Jane sigue desconcertada por Grace Poole: escucha a los criados murmurar sobre el sueldo inusualmente alto que recibe la costurera, lo que confirma sus sospechas
                de que Grace guarda un secreto que le da poder sobre Rochester.</p>
            <p class="pjustificado">Finalmente, llega la caravana de nobles, incluida la altiva Blanche Ingram. Jane es obligada a bajar al salón, pero se refugia en el hueco de una ventana, observando
                desde las sombras. Blanche y su madre no ocultan su desprecio por las institutrices, tratándola como un objeto insignificante. Cuando Jane intenta escapar de la reunión
                abrumada por la humillación, Rochester la intercepta. Al ver que ella está a punto de llorar, la deja ir, pero no sin antes casi traicionarse con un "Buenas noches, mi...", 
                cortando la frase justo a tiempo.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap18.php">Capítulo XVIII</a>
            </h3>
            <p class="pjustificado">La estancia de los invitados continúa y Jane observa con dolor cómo Rochester y Blanche Ingram actúan como la pareja perfecta, compitiendo juntos en juegos de mímica
                (charades). Jane llega a la amarga conclusión de que se casarán pronto, aunque nota que no hay amor: Blanche busca la fortuna de él, y él busca la belleza y el estatus de ella.</p>
            <p class="pjustificado">En medio de estas fiestas, llega un invitado inesperado: el Sr. Mason, un hombre que a Jane le produce una instintiva repulsión por su mirada vacía. De él descubre
                un dato nuevo: Rochester vivió un tiempo en las Indias Occidentales (el Caribe). La noche se vuelve aún más extraña con la llegada de una vieja gitana que se ofrece a leer la fortuna
                de los invitados. Blanche entra primero, pero sale de la consulta visiblemente decepcionada y de mal humor, lo que sugiere que la gitana le ha dicho algo que no quería oír sobre su 
                futuro con Rochester.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap19.php">Capítulo XIX</a>
            </h3>
            <p class="pjustificado">Jane entra en la biblioteca con escepticismo, pero pronto queda fascinada por las palabras de la supuesta gitana. La anciana demuestra conocer los sentimientos
                más íntimos de Jane y le asegura que está muy cerca de alcanzar la felicidad. Además, le confiesa el truco que usó con Blanche Ingram: le dijo que la fortuna de Rochester no era tan
                grande como ella pensaba, lo que explica el mal humor de la aristócrata (quien solo busca el dinero).</p>
            <p class="pjustificado">A medida que la "anciana" habla, su voz se vuelve más profunda y familiar. Jane descubre con asombro que la gitana es, en realidad, el propio Rochester disfrazado. 
                Aunque Jane lo reprende por el engaño, la atmósfera juguetona desaparece cuando ella le menciona la llegada del Sr. Mason. Al oír el nombre, Rochester se transmuta: se pone pálido y 
                se apoya en Jane, visiblemente perturbado por la presencia de este hombre de su pasado.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap20.php">Capítulo XX</a>
            </h3>
            <p class="pjustificado">La paz de la noche se rompe con un grito desgarrador. Rochester calma a los invitados diciendo que ha sido la pesadilla de un criado, pero poco después busca
                a Jane en secreto. Tras preguntarle si teme a la sangre, la lleva al tercer piso, donde encuentran al Sr. Mason apuñalado y mordido en el brazo. Rochester deja a Jane sola cuidando
                al herido con la orden estricta de que no hablen entre ellos. En ese silencio tenso, Jane observa un tapiz de la crucifixión, sintiendo la pesadez del misterio que rodea a Grace Poole.</p>
            <p class="pjustificado">Tras ayudar a Mason a marchar de la casa en secreto antes del amanecer, Rochester lleva a Jane al huerto. Allí, le cuenta una historia "hipotética": un joven 
                comete un "error capital" en un país extranjero y, tras años de disipación, intenta redimirse con una mujer pura, pero las convenciones sociales se lo impiden. Le pregunta a Jane 
                si ese hombre tendría derecho a "saltarse los obstáculos de la costumbre". Jane, firme en su moral, le responde que debe buscar la redención en Dios, no en otra persona. 
                Rochester termina la charla sugiriendo que casarse con Blanche es su única salida, dejándola sumida en la confusión.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap21.php">Capítulo XXI</a>
            </h3>
            <p class="pjustificado">Tras una serie de sueños premonitorios con bebés (mal augurio en el folclore inglés), Jane recibe noticias devastadoras: su primo John Reed se ha suicidado
                tras una vida de excesos, y su tía, la Sra. Reed, ha sufrido un derrame cerebral. Jane regresa a su antiguo hogar, donde se reencuentra con una Bessie fiel y con sus primas: 
                Eliza, ahora fría y volcada en la religión, y Georgiana, tan bella como superficial. Ambas hermanas se detestan profundamente.</p>
            <p class="pjustificado">A pesar del odio que recibió de niña, Jane intenta reconciliarse con su tía moribunda. En un último acto de confesión, la Sra. Reed le entrega una carta de 
                hace tres años de su tío, John Eyre, quien quería adoptarla y dejarle su fortuna. La tía confiesa que, por puro rencor, ocultó la carta para evitar que Jane prosperara. 
                Aun sabiendo esto, Jane le ofrece su perdón, pero la Sra. Reed muere a medianoche manteniendo su hostilidad hasta el último aliento, incapaz de aceptar la bondad de la sobrina
                que tanto maltrató.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap22.php">Capítulo XXII</a>
            </h3>
            <p class="pjustificado">Jane prolonga su estancia en Gateshead un mes más para ayudar a sus primas a organizar sus vidas tras la muerte de su tía: Georgiana se marcha a Londres y Eliza
                ingresa en un convento en Francia (donde acabará siendo Madre Superiora). Antes de irse, Jane recibe noticias de la Sra. Fairfax: los invitados se han ido y Rochester ha viajado 
                a Londres para comprar un nuevo carruaje, lo que todos interpretan como el paso definitivo para su boda con Blanche Ingram.</p>
            <p class="pjustificado">El reencuentro se produce de forma inesperada en el camino hacia la mansión. Al ver a Rochester, Jane no puede ocultar su alegría. Él la provoca hablando del 
                nuevo carruaje y de lo bien que le sentará a la futura "Sra. Rochester", manteniendo la farsa de su compromiso. En un arrebato de sinceridad que nace del alivio de volver a verlo,
                Jane pronuncia una de las frases más famosas del libro: "Dondequiera que usted esté, es mi hogar; mi único hogar". Al llegar a Thornfield, el cálido recibimiento de los criados y
                de Adèle refuerza esa sensación de pertenencia que Jane nunca antes había sentido.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap23.php">Capítulo XXIII</a>
            </h3>
            <p class="pjustificado">Tras dos semanas de tensa calma, Rochester encuentra a Jane en el jardín y la invita a pasear. Con crueldad aparente, le confirma que se casará con Blanche y
                que ha encontrado para ella un puesto de institutriz en Irlanda. La idea de cruzar el mar y alejarse de él rompe la compostura de Jane. Sentados bajo un gran castaño, Rochester
                habla de un "vínculo de comunicación" que los une, como si un hilo invisible conectara sus corazones.</p>
            <p class="pjustificado">Jane, incapaz de contenerse más, explota en un llanto amargo y le confiesa que lo ama, gritando que no es una "máquina sin sentimientos" y que sus almas son 
                iguales. Para su absoluta sorpresa, Rochester le pide que sea su esposa. Tras una inicial desconfianza (él admite que usó a Blanche solo para despertar sus celos), Jane acepta emocionada.
                Sin embargo, la naturaleza parece protestar: una tormenta estalla de inmediato y, esa misma noche, un rayo parte en dos el castaño donde minutos antes se habían jurado amor, 
                un símbolo clarísimo de que su unión está marcada por la tragedia.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap24.php">Capítulo XXIV</a>
            </h3>
            <p class="pjustificado">Los preparativos de la boda comienzan, pero no traen la paz que Jane esperaba. La Sra. Fairfax se muestra fría y desaprobadora; incluso tras saber que hay
                un compromiso formal, advierte a Jane que sea cautelosa, sugiriendo que la diferencia de edad y clase social es un peligro. Jane misma se siente inquieta, casi asustada,
                cuando Rochester la llama "Jane Rochester", como si ese nombre borrara su verdadera identidad.</p>
            <p class="pjustificado">Rochester intenta colmarla de joyas, vestidos caros y lujos, tratando de convertirla en una figura de cuento de hadas. Sin embargo, Jane reacciona con pánico
                y autoprotección: ella no quiere ser una "esclava" comprada con seda, sino seguir siendo ella misma. Siente el presentimiento de que esta felicidad es demasiado irreal para durar.
                Para ganar algo de independencia económica y sentirse más igual a su prometido, decide escribir a su tío John Eyre en Madeira, con la esperanza de que una futura herencia la ponga 
                en una posición de mayor dignidad frente a la fortuna de Rochester.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap25.php">Capítulo XXV</a>
            </h3>
            <p class="pjustificado">La víspera de la boda, Jane espera a Rochester con una ansiedad creciente. Cuando él llega, ella le confiesa una experiencia aterradora que ocurrió la noche anterior: 
                mientras dormía, una mujer de aspecto "salvaje" y facciones gruesas entró en su habitación, se probó el velo de novia de Jane ante el espejo y, acto seguido, lo rasgó en dos antes de pisotearlo.</p>
            <p class="pjustificado">Jane también le cuenta dos sueños inquietantes en los que aparece un niño llorando (el mal augurio que ya conocíamos), simbolizando su propia vulnerabilidad. 
                Rochester, visiblemente nervioso pero tratando de mantener la calma, insiste en que todo fue una alucinación o que la responsable fue Grace Poole en un episodio de locura. 
                Le hace una promesa extraña: le explicará todo el misterio cuando lleven casados un año y un día. Esa noche, Jane busca consuelo durmiendo con Adèle, llorando ante la inminente pérdida
                de su vida conocida.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap26.php">Capítulo XXVI</a>
            </h3>
            <p class="pjustificado">El día de la boda comienza con una nota de presagio cuando Jane ve a dos extraños en el cementerio de la iglesia. En plena ceremonia, cuando el clérigo pregunta
                si hay algún impedimento, uno de los extraños, el abogado Sr. Briggs, interviene: Rochester ya está casado. Revela que hace quince años, en Jamaica, Rochester contrajo matrimonio
                con Bertha Mason, quien aún vive. El propio Richard Mason aparece para confirmar la historia.</p>
            <p class="pjustificado">Tras un estallido de furia, Rochester confiesa la verdad y lleva a todos a Thornfield para que vean por qué ocultó su secreto. En una habitación escondida tras
                un tapiz en el tercer piso, encuentran a Bertha: una figura que Jane describe casi como un animal salvaje, con el cabello enmarañado, que gruñe y camina a cuatro patas. Es ella
                quien intentó quemar a Rochester y quien atacó a Mason. Rochester explica que su locura lo obligó a esconderla bajo la vigilancia de Grace Poole.</p>
            <p class="pjustificado">Finalmente, Jane descubre que su tío, John Eyre, fue quien envió a Mason a detener la boda tras recibir la carta de Jane (la que escribió en el Capítulo 24).
                Al quedarse sola, Jane se encierra en su habitación, despojándose de su traje de novia y sumergiéndose en una pena indescriptible, buscando en la oración la fuerza para sobrevivir
                a la traición del hombre que ama.</p>

            <h3>
                <a class="boton" href="../caps/3.Thornfield Hall (Cap. 11–27)/cap27.php">Capítulo XXVII</a>
            </h3>
            <p class="pjustificado">Jane sale de su habitación y encuentra a Rochester esperándola. Aunque lo perdona internamente, se mantiene firme. Rochester la lleva a la biblioteca y
                le hace una propuesta desesperada: huir juntos al sur de Francia para vivir como marido y mujer. Jane se niega rotundamente, pues sabe que, ante la ley y su propia conciencia, 
                nunca sería su esposa, sino su amante.</p>
            <p class="pjustificado">Para justificar su engaño, Rochester revela que su padre y su hermano lo enviaron a Jamaica para casarse con Bertha por su fortuna ($30,000$ libras), 
                ocultándole que la madre de ella estaba en un manicomio. Descubrió demasiado tarde que Bertha era violenta y grosera, rasgos que precedieron a su locura total. 
                Tras la muerte de su padre y hermano, regresó a Inglaterra y ocultó a Bertha en Thornfield para proteger su dignidad y la de ella, buscando consuelo en diversas amantes en Europa, 
                lo cual solo le trajo más vacío.</p>
            <p class="pjustificado">Jane siente una pena inmensa. Se siente tentada a quedarse con el único hombre que la ha amado. Sin embargo, su respeto propio y su moral vencen. 
                Tras un sueño profético donde su madre le ordena huir de la tentación, Jane toma su pequeña bolsa de dinero, sale sigilosamente de la casa y abandona Thornfield, 
                dejando atrás su corazón por salvar su alma.</p>
            
