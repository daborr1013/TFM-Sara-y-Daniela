                <h3>El pájaro. <i>Eyre/Air</i></h3>
                <p>Este símbolo define la esencia de Jane, se la describe como un pájaro ella misma lo utiliza para
                    hablar de sí misma. A lo largo de la novela, evoluciona con ella, ya que comienzan como criaturas
                    frágiles y heridas que acaban siendo capaces de cualquier cosa por su propia voluntad.</p>
                <p>De pequeña, su vía de escape a los maltratos sufridos eran sus libros de aves, que a su vez
                    representaban su deseo de volar lejos de allí.</p>
                <p>Al principio, Rochester suele llamar a Jane pajarillo de forma cariñosa pero posesiva. Jane rechaza
                    ser enjaulada en Thornfield, porque el amor no puede existir si uno tiene que sacrificar su
                    libertad.</p>
                <p>Por otro lado, el apellido de la protagonista no es casual. En inglés, Eyre se pronuncia exactamente
                    igual que "Air" (aire), lo cual añade una capa simbólica profunda a su búsqueda de libertad. El aire
                    representa la capacidad de Jane para desplazarse entre diferentes mundos (clases sociales, casas,
                    estados mentales) sin perder su esencia.</p>

                <h3>Las madres sustitutas</h3>
                <p>Como huérfana, Jane no tiene una raíz biológica, por lo que debe "construirse" a través de las
                    mujeres que encuentra. De cada una coge lo que más le gusta para construir su identidad.</p>
