                <h2>Temas</h2>
                <h3>Amor y moralidad</h3>
                <p>La relación romántica que mantienen Jane y Rochester no es solo sentimental, también tiene un
                    trasfondo ético conflictivo.</p>
                <p>Rochester sigue casado con Bertha en secreto, está dispuesto a quebrantar la ley de entonces
                    cometiendo bigamia para librarse de su mala suerte y acabar con el amor de su vida,
                    alcanzando así la felicidad. Para él, el amor que siente por Jane justifica sus actos, además
                    defiende que su primer matrimonio fue un gran engaño y una conspiración en su contra.</p>
                <p>Jane, en cuanto se entera de la existencia de Bertha, huye de Thornfield, poniéndo sus principios por
                    delante y reforzando su identidad. Lucha por mantener sus principios y por eso
                    huye de la casa. Esta decisión transforma la obra, dejando de tratarse de una historia de amor
                    romántico convencional para convertirse en una búsqueda del amor propio, junto a la
                    necesidad de sentir pertenencia sin tener que sacrificar los principios de uno mismo. </p>
                <p>En esto también Jane busca un equilibrio. No se queda con Rochester en un inicio por no renunciar a
                    sus principios, pero tampoco se queda con su primo John, ya que a pesar de luchar
                    ambos por una misma causa moral, el suyo sería un matrimonio sin amor. Solo cuando se demuestra a sí
                    misma su valía independiente, regresa con Rochester como una igual, mostrando
                    cómo debe ser el amor verdadero.</p>
                <p>Respecto a la identidad de Jane, vemos cómo a lo largo de su historia se va perfilando. De pequeña,
                    Jane era pura pasión y se dejaba llevar por sus sentimientos más puros. Cuando
                    va creciendo y madurando, recibe ayuda externa de gente como Helen o la señorita Temple para
                    conducir sus emociones y descifrar qué es lo correcto, hasta encontrar el equilibrio
                    entre sentimiento y razón, un conflicto que aparece constantemente en sus decisiones.</p>

                <h3>Clases sociales, independencia y desigualdad</h3>
                <p>En la novela vemos reflejadas en repetidas ocasiones las diferencias de clases sociales presentes en
                    la
                    época victoriana de Londres. Además, la autora usa distintas formas de hacerlo. Brontë se centra en
                    las
                    desigualdades entre los ricos y los pobres y cómo estas afectan tanto a las relaciones personales
                    como a
                    las oportunidades vitales. La trayectoria de Jane demuestra que factores como el origen familiar o
                    la riqueza
                    no solo determinan las oportunidades vitales, sino cómo la sociedad percibe a alguien. La
                    procedencia de Jane
                    es un misterio al principio de la obra, no sabemos nada de su familia más cercana a parte de los
                    Reed, los cuales
                    la repudian por el simple hecho de haber sido la protegida de su tío mientras seguía vivo. </p>
                <p>Un elemento clave de esta crítica es la figura de la institutriz, ya que debían tener unos modales
                    adecuados y una educación elevada
                    para poder enseñar a los hijos de las clases altas, pero ellas mismas seguían siendo vistas como
                    sirvientas. Lo vemos en cómo Blanche
                    Ingram trata a Jane en su visita a Thornfield, despreciándola abiertamente delante de los demás
                    invitados. La educación es sumamente
                    importante para Jane, la considera, por su posición social, su única arma frente a un mundo que la
                    ve como inferior por su </p>
                <p>Por eso cuando su relación con Rochester va a más, incluso sabiendo la verdad de Bertha, Jane se
                    plantea casarse con su amado para “desencasillarse”
                    de su clase social y poder evolucionar. Es más, siente que Rochester le está haciendo un favor por
                    querer estar con ella. Sin embargo, sus
                    principios se mantienen fuertes y no cae en la tentación. </p>
                <p>Desde que quedó a cargo de la familia Reed, Jane ha sido “una carga” y ha tenido que ser mantenida
                    por otros, es por esto por lo que su
                    búsqueda de libertad es tan importante. Quiere ser libre y dueña de sí misma, un pensamiento poco
                    común para las mujeres de aquella época,
                    motivo por el cual también esta novela es tan moderna.</p>
                <p>Incluso le cuesta encontrar un “igual” con el que pasar el resto de sus días . A pesar de pasarse
                    gran parte de su vida sintiéndose sola o
                    fuera de lugar, Jane acaba encontrando un sitio en el que la quieren por quien es, Moor House, y en
                    el que realmente siente que pertenece.
                    Allí le llega su libertad económica, lo que conlleva su libertad social que tanto ha ansiado a lo
                    largo de la novela. Jane no regresa con Rochester
                    para ser "salvada", sino para elegir, desde su propia autonomía, al hombre que ama, quien además ha
                    pasado por su propio proceso de purificación y pérdida de poder.</p>

                <h3>Religión y espiritualidad</h3>
                <p>La religión es otro de los pilares de la novela, y aparece representada en distintos personajes y de
                    distintas formas. Además, Jane la usa
                    para encontrar su propia identidad también.</p>
                <ul>
                    <li>
                        <p>Brocklehurst: representa la religión como herramienta de control social. Les dice a las
                            huérfanas que tienen que privarse de placeres
                            y ser humildes, mientras que sus propias hijas viven entre lujos. Jane rechaza esta visión
                            porque carece de la esencia del cristianismo: la caridad y la verdad.</p>
                    </li>
                    <li>
                        <p>Helen: a pesar de que su forma de ver la religión y el perdón absoluto es muy puro, Jane no
                            es capaz de ser como ella, ya que le parece
                            que es demasiado pasiva para una mujer con las inquietudes que ella tiene.</p>
                    </li>
                    <li>
                        <p>John Rivers: representa una religión en la que se sacrifica el amor. St. John tiene una fe
                            fría, ambiciosa y sacrificada. Él renuncia a
                            su amor por Rosamond por esto mismo y trata de convencer a Jane de seguir sus pasos. Ella
                            percibe esto como una esclavitud espiritualy
                            se ve obligada a recharzarla para no traicionarse a sí misma.</p>
                    </li>
                </ul>
                <p>A través de estos tres personajes, Jane construye su propia forma de ver la religión. Su
                    espiritualidad llega a su clímax en Moor House, cuando
                    vive el momento místico de escuchar la voz de Rochester llamarla. Su religión no la tiene sometida,
                    sino que le permite disfrutar del amor humano con la conciencia tranquila.</p>

                <h3>Justicia y redención</h3>
                <p>No hablamos de justicia legal, sino justicia moral o poética que los personajes han de enfrentar,
                    afrontando las consecuencias de sus actos.</p>
                <p>Rochester es la principal muestra de esta idea. Recibe un castigo físico que le sirve de
                    purificación. En el incendio de Thornfield, pierde su casa,
                    su vista, su mano, pagando así por sus pecados (la vida que llevó en Europa y su intención de
                    cometer bigamia). Como hemos dicho, a pesar de esta
                    tragedia, Rochester se ve recompensado con una transformación espiritual que permite que Jane
                    regrese a su lado.</p>
                <p>Los Reed también acaban con finales fatales que sirven como justicia poética por cómo han tratado a
                    Jane durante toda su vida. La señora Reed muere
                    sintiéndose culpable y con la carga del fracaso de sus propios hijos, en especial John, que acaba
                    arruinado por el juego y cometiendo suicidio.</p>
                <p>Igualmente Brocklehurst tiene un mal final en esta historia. Es humillado públicamente (irónicamente
                    como hizo él con Jane llamándola mentirosa) cuando
                    la epidemia de tifus arrasa Lowood y tiene que cerrar. Su caída demuestra que la maldad disfrazada
                    de religión tarde o temprano es desenmascarada.</p>
                <p>Pero no toda la justicia es negativa en la obra. Jane se ve recompensada cuando hereda la fortuna de
                    su tío (recuperando lo que los Reed le negaron) y
                    así puede volver con Rochester bajo sus condiciones como mujer libre. Su recompensa es alcanzar la
                    felicidad sin haber sacrificado su moral.</p>
                <p>Incluso Bertha encuentra una forma de crear justicia. Su muerte en el incendio de la casa es lo que
                    consigue limpiar Thorfield, ya que ella deja de
                    sufrir y libera a Rochester del ancla legal que era estar casado con ella.</p>


                <h2>Símbolos</h2>
                <h3>El fuego y el hielo</h3>
                <p>El fuego y el hielo son usados por Brontë para representar una dicotomía en el amor. Mientras que el
                    frío es la represión emocional, soledad, rigidez representada por John Rivers, el fuego es la pasión
                    y el deseo de Rochester, sin embargo, es peligroso.</p>
                <p>El fuego es el elemento que representa la relación que mantienen Jane y Rochester. Representa el
                    calor y el hogar, pero como hemos dicho es peligroso debido a las emociones descontroladas. También
                    es un elemento purificador.</p>
                <p>Lo vemos presente en las conversaciones que mantienen frente a la chimenea, en el incendio final de
                    Thornfield…</p>
                <p>El hielo, por otro lado, es el elemento que domina las etapas en las que Jane sufre. El frío es la
                    carencia de amor que Jane sufre en Lowood, a John Rivers se lo describe como un glaciar que busca
                    apagar el fuego en su prima… El frío casi mata a Jane cuando huye de Thornfield y duerme a la
                    intemperie: se despide del calor recibido por Rochester y ahora le toca enfrentarse a su ausencia.
                </p>
                <p>Jane busca encontrar en este aspecto de su vida también el equilibrio, ya que sabe que mucho fuego
                    conlleva a la locura y la destrucción, y mucho hielo lleva a la tiranía y a la muerte del alma. Ella
                    es la única que logra equilibrar ambos.</p>
