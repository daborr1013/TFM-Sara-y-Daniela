                <h3>El Cuarto Rojo</h3>
                <p>Es la habitación en la que el tío Reed murió y dónde encierran a Jane de pequeña como castigo. Es un
                    espacio de transición en el que la muchacha entra sumisa y aterrorizada y sale rebelde, consciente
                    de su propia existencia al haberse visto reflejada en el espejo.</p>
                <p>Todos los elementos del cuarto tienen significado, incluso el color. El rojo está portadas partes
                    (cortinas, paredes, alfombras…) y simboliza la ira reprimida de Jane. Sufre aquí su primer ataque de
                    pánico que la marcará para siempre, cada vez que se siente atrapada o mal tratada su mente regresa a
                    esta habitación.</p>
                <p>Podemos ver el Cuarto Rojo como un anticipo de lo que Bertha Mason sufre en el ático de Thornfield.
                    Ambas son mujeres que han sido encerradas injustamente por haber sido consideradas una amenaza
                    difícil de controlar.</p>
