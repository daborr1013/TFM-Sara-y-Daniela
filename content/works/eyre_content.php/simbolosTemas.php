                <h3>La casa</h3>
                <p>Thornfield simboliza el secreto victoriano, la fachada es la respetabilidad, pero el interior es un
                    tormento.</p>
                <p>Los lugares a los que llega la luz, como el salón, la biblioteca o el comedor, son lugares en los que
                    hay vida social, representando la vida pública de Rochester: un hombre rico y sofisticado. Jane
                    intenta encajar aquí, sin embargo, no puede evitar sentirse como una extraña. </p>
                <p>A medida que la oscuridad invade la casa, en el tercer piso y el ático, Jane puede sentir cosas
                    extrañas. Aquí es donde se esconde el pasado reprimido de Rochester, su mujer Bertha. Podemos ver el
                    ático como el subconsciente de la casa, ya que sabe lo que realmente está ocurriendo. Mientras todo
                    parece tranquilo abajo, arriba se esconde la ira que acaba destruyendo toda la casa. Los ruidos, las
                    risas y los incendios son "fugas" de ese secreto que no puede mantenerse encerrado para siempre.</p>
                <p>Que Thornfield termine reducida a cenizas es un símbolo de purificación total.</p>
                <p>Podemos comparar esta casa con las demás:</p>
                <ul>
                    <li>Gateshead: la cárcel de la infancia en la que se encuentra el lugar de castigo que inicia todo,
                        el Cuarto Rojo.</li>
                    <li>Lowood: la institución fría y deshumanizada.</li>
                    <li>Moor House: hogar en el que no hay secretos ni pasillos cerrados en el que la mente y la
                        igualdad pueden florecer.</li>
                    <li>Ferndean: una casa modesta, rodeada de naturaleza, sin grandiosidades ni pretenciosidad, en la
                        que el amor ya no se rige por estatus ni secretos.</li>
                </ul>

                <h3>La luz y la oscuridad. Bertha Manson</h3>
                <p>En el apartado anterior hemos adelantado un poco el significado que se le da a este símbolo. La luz
                    es un arma de doble filo, pues permite el descubrimiento, pero la realidad duele.</p>
                <p>La oscuridad, aparte de ser los secretos, es el elemento que envuelve a Bertha Mason. Vive casi en
                    una oscuridad total, que la deshumaniza y la borra de la existencia. Además, Bertha no es un simple
                    personaje, es el doble oscuro de Jane, ya que representa los impulsos que ella intenta reprimir. una
                    es luz, razón y autocontrol, mientras que la otra es oscuridad, locura, instinto.</p>
                <p>Resulta irónico que Rochester acabe ciego, ya que tuvo que perder la vista física y sumirse en la
                    oscuridad para ganar la visión moral y espiritual.</p>
